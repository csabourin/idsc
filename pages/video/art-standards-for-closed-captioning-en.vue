    <ul>
      <li>If viewers see mouths moving, they will be looking for corresponding captions. Therefore, all obvious speech must have captions.</li>
      <li>When captioning spontaneous conversation, as a rule, do not include nonverbal utterances, repeated words, or false starts to sentences. However, false starts and utterances must be included when they contribute to the understanding of conversation or personality or are part of a dramatic effect or joke.</li>
      <li>Descriptive captions are those that represent the non-speech information in program audio. They are a useful tool for creating the mood, setting the scene, and establishing the context for dialogue. They are often used to describe music, background noises, and sound effects that are present in the audio track. They can also be used to convey speech inflections that are not animated in the video presentation, but which are central to the storyline. The effective use of descriptive captions is often what distinguishes exceptional captioning. Skilled caption writers and caption stenographers can use descriptive captions to significantly enhance a viewer‟s enjoyment and comprehension of a program.</li>
      <li>Lyrics should be written for all songs to the extent that the words are discernible. Music notes are necessary at the beginning and end of each lyrical phrase (not each new line of text) to indicate that the lines are being sung and not spoken.</li>
      <li>When lyrics cannot be discerned, a parenthetical description should be used. Whenever possible, a description is preferred to the parenthetical "(Music)".</li>
