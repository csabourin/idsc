    <h2>Standard for descriptive transcripts and closed captioning of videos</h2>
    <h3>Introduction and rationale</h3>
    <p>
      Per Section 6.1.1 of the <em>Standard on Web Accessibility</em><a id="_ftnref1" href="#_ftn1" name="_ftnref1" title="" />, all web pages on Government of Canada sites must conform to the Web Content Accessibility Guidelines (WCAG) 2.0 at the AA level. For a video that has an audio track, these guidelines require that a video has both captions and a descriptive transcript<a id="_ftnref2" href="#_ftn2" name="_ftnref2" title="" />.<br>
      <strong>Captions</strong> "meet the needs of Deaf, deaf, deafened, and hard of hearing people."<a id="_ftnref3" href="#_ftn3" name="_ftnref3" title="" /> It also serves to provide written text for language that is difficult to decipher or in another language than the video's target audience. It works in concert with the video by supplying the information in the audio track. In addition to spoken words, it also describes sounds that are needed to understand the video, such as musical cues.<br>
      <strong>Descriptive transcripts</strong> serve as an <em>alternative format</em> for a video, not something that works in conjunction with a video. One should be able to read the descriptive transcript independently of the video and receive the same information and experience as if watching and hearing the video.<br>
      It is understood that video is more than the sum of words and images. Through shot selection, pacing, musical cues and transitions, a video can convey a "feel" or emotional impression (e.g., inspiring for Canada Day, cautious for marriage fraud). Therefore, descriptive transcripts should provide both the factual content of the video as well as details to provide that impression.<br>
      As well, captions and descriptive transcripts do not need to replicate <em>verbatim</em> what is said in the video, but rather aid in understanding the video. How we interpret information visually and aurally is different. It is better to edit the captions to aid the viewer in understanding what is being said than to faithfully transcribe what someone said since, when viewed onscreen, it could be disjointed and difficult to read.<br>
      As written materials, descriptive transcripts must respect the spelling, formatting, punctuation and other rules of <em>The Canadian Style</em> and <em>Le guide du rédacteur</em>, both available on Termium<a id="_ftnref4" href="#_ftn4" name="_ftnref4" title="" />.
    </p>
    <h3>Overview of standards</h3>
    <h4>Technical standards</h4>
    <h5>Captions</h5>
    <p>
      The Government of Canada uses closed captions in its multimedia player that is to be used across all government websites. Captions must follow the TTML 1 standard<a id="_ftnref5" href="#_ftn5" name="_ftnref5" title="" /> defined by the World Wide Web Consortium.<br>
      If a department uses YouTube to post its videos, closed captions should be provided for the videos on YouTube. Per the <em>Guideline on Official Use of Social Media</em>, departments using social media must: "use the options provided by the platform to make the content more accessible."<a id="_ftnref6" href="#_ftn6" name="_ftnref6" title="" /> There are various captioning standards supported by YouTube and more information can be found at <a href="//support.google.com/youtube/answer/2734698?hl=en">https://support.google.com/youtube/answer/2734698?hl=en</a>.
    </p>
    <h5>Descriptive transcripts</h5>
    <p>
      There are no technical standards for a descriptive transcript file.<br>
      Still, to aid in posting transcripts online, they should be delivered in Word or some other file format capable of supporting rich text and the formatting that follows.
    </p>
    <h4>Timing</h4>
    <h5>Captions</h5>
    <p>When closed captions appear onscreen should match, as closely as possible, when those words or sounds are happening in the video. However, timing can be adjusted to put them slightly out of sync in order for the words to remain onscreen long enough to be read.</p>
    <h5>Descriptive transcripts</h5>
    <p>Since descriptive transcripts are not time-sensitive, there are no standards for them regarding timing.</p>
    <h4>Formatting</h4>
    <p>Both captions and descriptive transcripts must follow the standards for formatting in <em>The Canadian Style</em> and <em>Le guide du rédacteur</em>.</p>
    <h5>Captions</h5>
    <p>Given that captions are time-sensitive, in order to facilitate ease-of-reading captions should be in plain text unless formatting is necessary.</p>
    <h5>Descriptive transcripts</h5>
    <p>Since descriptive transcripts are not time sensitive, formatting can be used to enhance onscreen readability so long as the formatting respects <em>The Canadian Style</em> and <em>Le guide du rédacteur</em> as well as accessibility requirements.</p>
    <h4>Dialogue, narration and voice-overs</h4>
    <p>
      Capturing spoken words must balance faithfully reflecting what the speaker is saying while providing clarity for the reader. Therefore, extraneous words, run-on sentences, pauses and filler words (e.g., "uh", "um") can be removed and/or edited to enhance the clarity of the message. In these cases, clarity for the reader is more important than faithfully replicating what someone said.<br>
      To aid in understanding who is speaking, spoken words should be preceded by an attribution tags indicating the speaker by name, title or role. An exception can be made in captions when it is clear who is speaking.
    </p>
    <h4>Dealing with other languages</h4>
    <p>The language of captions and descriptive transcripts must be in the language of the page, not the language the person is speaking unless the fact that another language is being used is important. The goal is to clearly convey the message and information of the video, not describe the video.</p>
    <h4>Music</h4>
    <p>In general, music does not need to be mentioned in captions or descriptive transcripts unless its inclusion reflects the "feel" of the video or is important content, such as:</p>
    <ul>
      <li>A group singing the National Anthem</li>
      <li>Someone singing a song onscreen</li>
      <li>Background music that is used to evoke a mood, such as a tension or gloom</li>
    </ul>
    <h4>Onscreen action</h4>
    <h5>Captions</h5>
    <p>Since captions work in concert with what is happening on screen, they do not contain descriptions of actions.</p>
    <h5>Descriptive transcripts</h5>
    <p>
      Descriptive transcripts are independent of the video. As such, they must contain descriptions of important actions happening onscreen. They are to be written in the present tense and third person.<br>
      Descriptions can be nuanced or evocative to reflect the "feel" of the video. They do not need to be literal and objective descriptions; they also do not need to be exhaustively detailed.<br>
      In addition, descriptive transcripts must not contain information one would not learn from watching the video.
    </p><br clear="all">
    <h3>Specific details and scenarios</h3>
    <h4>Open captions and referring to closed captions</h4>
    <p>
      In the Government of Canada multimedia player, closed captions are activated by clicking a small "CC" icon embedded in the player. If a video is largely in a language that is not the language of the page (e.g., an interview in French on an english page), place a note on the page indicating that closed captions in the language of the page can be accessed by clicking the CC button.<br>
      The same or similar phrase should be used on YouTube and should be the first line of the YouTube description.<br>
      Open captions embedded in the video file itself are <em>not</em> to be used. The reasons for this include:
    </p>
    <ul>
      <li>Letters must have sufficient contrast with the background. The closed captioning systems for the GOC Media Player and on YouTube provide this contrast. To do it within the video may require shading or other features that might be distracting.</li>
      <li>Some assistive technologies can read closed captioning aloud. If open captions are used for some parts of a video (e.g., dialogue in another language) and closed captions for others (e.g., the rest of the dialogue) a user may miss out.</li>
      <li>If closed captioning is used for all dialogue and open captions used for some, the two captions might appear over top of each other or be slightly out of sync, which will confuse the viewer.</li>
      <li>It is easier to correct an error in the closed captioning file than in a working video file.</li>
    </ul>
    <p>
      If possible, provide voice-over in the video using the language of the page. Doing so will make the audio accessible to those who can hear it in that language. Closed captions would then match the timing of the voice-over.<br>
      This restriction on open captions does not forbid, however, using onscreen titles to introduce someone by name, highlight key information, etc. appearing onscreen.
    </p>
    <h4>Timing for captions files</h4>
    <h5>How long to leave captions onscreen</h5>
    <p>Per the <em>Closed Captioning Standards and Protocol for Canadian english Language Television Programming Services<a id="_ftnref7" href="#_ftn7" name="_ftnref7" title="" /></em>, best practices for the timing of closed captions are:</p>
    <ul>
      <li>Captions, regardless of how short they are, should remain on screen for at least 1.5 seconds.</li>
      <li>Captions should remain onscreen for 1.5 seconds for every 32 characters (or part thereof).</li>
      <li>The maximum amount of time captions should be visible is three seconds for each 32 characters of text.</li>
    </ul>
    <h5>Leaving captions onscreen longer than when someone is speaking</h5>
    <p>It is acceptable for captions to remain onscreen longer than when the person is speaking so long as it is clear from the captions that they continue to reflect the words of the speaker. For example:</p>
    <ul>
      <li>Someone is speaking, they stop speaking, and the videos cuts to a street scene not focusing on one person. In this case, captions for the speaker can continue briefly into the street scene since it would be understood that the captions are still the words being spoken by that person. In this instance, do <em>not</em> use "voice-over" (see below) with the captions since that implies the speaker is narrating the street scene.</li>
      <li>Someone is speaking, they stop speaking, and the video cuts to someone else beginning to speak. In this case, the captions for the first speaker cannot carry into the second scene since it might appear they are the captions for the second speaker.</li>
    </ul>
    <p>If someone is speaking very quickly and makes the timing problematic, it is acceptable to edit the speaker's words to fit the best practices for timing rather than presenting the captions too quickly. every effort must be made, though, to accurately capture the ideas that the person is expressing.</p>
    <h4>Header for transcripts</h4>
    <p>
      The beginning of each transcript should contain the word "Transcript" and the title of a video using the Heading 2 style while the rest of the transcript should be in regular text. The length of the video, in boldface, should follow.<br>
      For example:
    </p>
    <h4>Transcript: "TITLe"</h4>
    <p><strong>Video length: X:YY minutes</strong></p>
    <h4>Transcription&nbsp;: «&nbsp;TITRe&nbsp;»</h4>
    <p>
      <strong>Durée de la vidéo&nbsp;: X:YY minutes</strong><br>
      The rest of the text follows as the normal style using the formatting described elsewhere in this document.
    </p>
    <h4>Attribution tags, dialogue, narration and voice-overs</h4>
    <p>In descriptive transcripts and when needed in captions, attribution tags should proceed spoken words as the method to indicate who is speaking.</p>
    <ul>
      <li>Attribution tags must be in ALL CAPS, followed by a colon.</li>
      <li>If there is an unnamed narrator, refer to them as NARRATOR.</li>
      <li>If someone is introduced by name, use their first name or title (e.g., Dr., Judge, Hon.) and last name, if appropriate.</li>
      <li>If people are not named, use a generic indicator like "MAN BeING INTeRVIeWeD". If there are several, use "FIRST MAN BeING INTeRVIeWeD".</li>
    </ul>
    <p>
      If the person speaking is not onscreen, use "(voice-over)" after the attribution tag to indicate someone is speaking over the video.<br>
      Do not use quotation marks unless the person speaking is quoting someone.<br>
      While spoken words can be edited for clarity, do <em>not</em> edit someone quoting religious scripture, literary passages, direct quotes, and so on. If they make an error, leave the error intact.
    </p>
    <h5>Captions</h5>
    <p>
      If needed to aid in understanding who is speaking, such as multiple voice-overs, introduce spoken words with an attribution tag. It only needs to be presented once per string of dialogue.<br>
      If it is clear from onscreen context who is speaking (i.e., their lips can clearly be seen to be moving), there is no need for an attribution tag before the caption. This is true even if there are several people onscreen, but only one person is speaking at one time and the timing of the captions is such that their words are onscreen while that person is speaking.
    </p>
    <h6>Indicating voice over</h6>
    <p>Use "(voice-over)" only once per speaker and when it is not clear who is speaking. For example:</p>
    <ul>
      <li>The video begins with a voice-over, changes to that person speaking onscreen, and returns to that person speaking in voice-over. In this case, begin the first caption with "JeAN-PAUL (voice-over):" for the first caption. The viewer will understand Jean-Paul is continuing to speak. (This example assumes Jean-Paul has been or will be introduced by name.)</li>
      <li>If the video begins with someone speaking onscreen and it changes to voice-over, there is no need to use "(voice over)" since the viewer will understand the same person is speaking.</li>
      <li>If the video begins with someone speaking onscreen, it changes to voice-over and then a narrator begins to speak, begin the narrator's caption with "NARRATOR (voice over):" to make it clear someone else is speaking.</li>
    </ul>
    <h6>When it appears someone is speaking</h6>
    <p>If someone is onscreen and their lips are moving, but they are not speaking, someone unable to hear the audio track will assume that the person is speaking. Provide some indication of what is happening such as "(Draws a breath)" or use the first word of what they eventually will say.</p>
    <h5>Descriptive transcripts</h5>
    <p>
      All dialogue, including the attribution tag, is to be in <strong>boldface</strong>. This helps set it apart from onscreen description.<br>
      Since the descriptive transcript is independent of the video, all spoken words must have an attribution tag. Have the attribution tag and what is being said on the same line. Do not have a line break or a new paragraph after the attribution tag.<br>
      If using several generic indicators (e.g., "FIRST MAN BeING INTeRVIeWeD", "SeCOND MAN BeING INTeRVIeWeD"), make sure there is coordination between the attribution tags and the description.<br>
      Do NOT use ellipses if there is a pause in the dialogue. The transcript is not-time sensitive. ellipses should only be used when:
    </p>
    <ul>
      <li>Someone does not complete a sentence or thought and it is important to communicate that fact, or</li>
      <li>It is necessary to communicate the pauses for the reader to understand the "feel" of the video, such as humour or frustration.</li>
    </ul>
    <p>Break up dialogue into different paragraphs if a new idea is expressed, just like in formal writing, even if same person is speaking. Do not use another attribution tag, but continue to use boldface.</p>
    <h4>Dealing with other languages</h4>
    <p>
      Dialogue and narration is <em>always</em> to be in the language of the page <em>unless</em> the use of another language is appropriate, such as for emphasis, a foreign language expression, or the fact that another language was spoken is important.<br>
      Leave words in another language in italics. These should be words or expressions that one would consider commonly understood or are defined later.<br>
      Spell words in that language, not phonetically, unless it uses another alphabet.
    </p>
    <h5>Captions</h5>
    <p>
      If a speaker repeats the same thing in several languages <em>and</em> the timing allows for the caption in the language of the page to be onscreen while the person is speaking in the other language, only provide what was in the page's language <em>unless</em> the fact that it is in many languages is important. For example, if the person says "Good morning! Bon jour!" and continues in english, only say "Good morning!" in the captions. That is, while the speaker's lips are moving a caption is shown.<br>
      If the speaker repeats the same thing in several languages and the timing does <em>not</em> allow showing only the caption in that language (i.e., the speaker's lips move well beyond what the caption indicates), an explanation of why they are still speaking is needed., indicate the speaker is repeating themselves in another language. The message does not need to be written again unless the phrasing in the other language conveys a different meaning. For example:<br>
      It is such a wonderful day to be in Montreal with all of you!<br>
      (Repeats message in French.)<br>
      Or, if the meaning of the other language is different or has a different nuance.<br>
      It is such a wonderful day to be in Montreal with all of you!<br>
      (In French) I am so happy to be back home with you in Montreal on this glorious afternoon.
    </p>
    <h5>Descriptive transcripts</h5>
    <p>If speaker repeats the same thing in several languages, only write out what was said once <em>unless</em> the fact that it is in many languages is important.</p>
    <h4>Music</h4>
    <h5>Captions</h5>
    <p>
      Background music <em>should</em> be mentioned if it is being played over stretches of video without dialogue in order to assure the viewer they are not missing voice-over narration.<br>
      Lyrics should be written for all songs to the extent that the words are discernible. A single music note ("♪" - ALT + 13) should be added to the beginning and end of each lyrical phrase to indicate that the lines are being sung and not spoken.<br>
      ♪ The rolling of the sea Is so blue ♪<br>
      When lyrics cannot be discerned, a parenthetical description should be used if the music is worth noting. Whenever possible, a description of what is being played is preferred to just "(Music)".
    </p>
    <h5>Descriptive transcripts</h5>
    <p>
      How music and/or singing is described can be determined based on the context of the situation so long as it is clear what is happening.<br>
      If there is signing, each line of lyrics should be on its own line. Musical notes do not need to be used, but that the lines are being sung should be communicated. For example:<br>
      A girl wearing a red and white dress holding a small Canadian flag move to the podium. She sings into the microphone.<br>
      <strong>GIRL (singing): O Canada! Our home and native land!<br>
        True patriot love in all thy sons command<br>
        With glowing hearts we see thee rise<br>
        The true North strong and free!<br>
        From far and wide, O Canada we stand on guard for thee<br>
        God keep our land glorious and free!<br>
        O Canada, we stand on guard for thee<br>
        O Canada, we stand on guard for thee</strong>
    </p>
    <h4>Onscreen action</h4>
    <p>Only descriptive transcripts contain descriptions of onscreen action since they are independent of the video. However, descriptive transcripts cannot reveal information that one would not learn from watching the video. For example, if a painting is shown onscreen but is not identified, it should not be named in the transcript.</p>
    <h5>Writing style and tense</h5>
    <p>
      Description is in the third person ("There is a portrait on the wall.") <em>not</em> first person ("We see a portrait on the wall.") Write all action in the present tense.<br>
      Try to avoid the passive voice, but there will be instances where it cannot be avoided.
    </p>
    <h5>Transitions</h5>
    <p>Visual transitions and the details do not need to be described unless it is important.</p>
    <ul>
      <li>USe: There is a montage of landscapes across Canada, quickly fading one into the next.</li>
      <li>DO NOT USe: An image of the Pacific coast fades suddenly into the Canadian Rockies which fades quickly into a wheat field which fades suddenly into a cityscape…</li>
      <li>USe: The image of the modern freighter slowly fades into a painting of an old whaling ship.</li>
      <li>DO NOT USe: There is a picture of a modern freighter. Fade. There is a picture of an old whaling ship.</li>
    </ul>
    <h5>Title cards and onscreen text</h5>
    <p>
      Simply state "Text on screen:" followed by the text in quotes, or something to that effect. How the text appears (e.g., fades in, sweeps in from the left) does not need to be communicated unless that motion is important. For example, to indicate that the size of words has meaning in an animation:<br>
      The words "Infrastructure improvements" slide in onscreen. The bottom edge of the letters is even with the bottom of the screen and the letters are about half as high as the screen. The figure "55%" appears to their right.<br>
      <strong>NARRATOR: Improving our IT infrastructure has helped reduce our deficits by 55%.</strong><br>
      The words "Process efficiencies" slides in above "Infrastructure improvements", but the letters are not as tall. The figure "33%" appears to the right of "Process efficiencies".<br>
      <strong>NARRATOR: Improving how we process your application has helped reduce deficits by an additional 33%.</strong><br>
      The words "Increased user fees" slides in above "Process efficiencies", but these words are less tall than "Process efficiencies". The figure "12%" appears to the right of "Increased user fees". The figure "12%" become bold and grows in size.<br>
      <strong>NARRATOR: The increase in user fees accounts for only 12% of our efforts to reduce our deficit.</strong><br>
      For a graphic introducing someone or something by name:
    </p>
    <ul>
      <li>USe:</li>
    </ul>
    <p>
      On screen is a man working in an office. The title "Dr. Craig Jones" appears.<br>
      Text appears on screen: "Banff National Park"
    </p>
    <ul>
      <li>DO NOT USe:</li>
    </ul>
    <p>
      On screen is a man working in an office. The title "Dr. Craig Jones" appears near the bottom of the screen in white, serif letters.<br>
      The text "Banff National Park" sweeps onscreen.
    </p>
    <h4>embedding links in descriptive transcripts</h4>
    <p>
      Since descriptive transcripts are hypertext, they can contain hyperlinked text. To that end, if the video mentions a URL, the URL in the transcript should be hyperlinked.<br>
      If a vanity URL is used (e.g., canada.ca/taxes), present the vanity URL as clickable text, but code the link to point to the actual page URL (e.g., <a href="http://www.cra-arc.gc.ca/gncy/txnf/menu-eng.html">http://www.cra-arc.gc.ca/gncy/txnf/menu-eng.html</a>).<br clear="all">
