    <h2>Considerations for designing<br>a good instructional video</h2>
    <p><img src="~/assets/man_clicking_on_video.jpg" width="275" height="182" alt="man clicking on video" class="pull-right">Videos are an effective learning tool that can relieve many classroom constraints. Whether used on their own, or complementing online and classroom learning, videos have been shown to enhance directed and self-directed learning by increasing a learner’s ability to understand new information. The videos you design should therefore not merely be a medium to present information. They should be a tool to enhance learner problem-solving skills, foster critical thinking, stimulate active discussions in class or online, make learning more engaging, and encourage self-learning.</p>
    <p>What are the characteristics of an effective instructional video? What should you keep in mind when designing an instructional video? The Canada School of Public Service has developed and tested best practices applicable to learning tools for the design and creation of highly effective videos to support your program needs.</p>
    <h3>Planning</h3>
    <ul>
      <li>establish clear timelines for your project that account for locating film talent, booking a film location, filming, post-production, reviewing draft footage, translation, transcription, closed captioning, approval points and distribution to GCcampus.</li>
      <li>Clarify your budget. For instance, high-production studio videos with professional graphics will increase costs.</li>
    </ul>
    <h3>Audience</h3>
    <ul>
      <li>Define the target audience for your learning content. evaluate level of knowledge—novice, intermediate, or advanced—beforehand, and remain within the scope of the intended level.</li>
      <li>Identify the professionals within your target audience. engage them to participate in your video to add credibility to your larger audience.</li>
    </ul>
    <h3>Content</h3>
    <ul>
      <li>establish one clear learning take-away message for your learning video. Aim for simplicity, not simple.</li>
      <li>Focus on real-world, applicable examples to help your audiences increase their capacity to deliver government priorities and programs to Canadians.</li>
      <li> Too much content? Create additional videos and cross-link them to your original video to add learning interactivity. </li>
      <li>Build in opportunities for critical analysis and reflection</li>
      <li>Design your content with adult learning principles in mind</li>
    </ul>
    <h3>Style</h3>
    <ul>
      <li>Optimal duration for online videos is 2 to 5 minutes.</li>
      <li>Photos, diagrams, statistics, text boxes are all powerful visual tools that are beneficial to increasing the learner’s ability to understand new information.</li>
      <li>For scripted videos, make the learner feel engaged by using the words &quot;we&quot; and &quot;you&quot;.</li>
