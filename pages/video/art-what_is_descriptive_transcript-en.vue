        width="201"
      >When storyboarding your videos prior to production, you must
      not only include the on-screen content, but you must also include an
      alternate, accessible version, also called a descriptive transcript.
    </p>

    <p>
      <strong>Descriptive transcripts</strong> serve as an alternative format
      for a video, not something that works in conjunction with a video. One
      should be able to read the descriptive transcript independently of the
      video and receive the same information and experience as if watching and
      hearing the video.
    </p>

    <p>
      It is understood that video is more than the sum of words and images.
      Through shot selection, pacing, musical cues and transitions, a video can
      convey a &quot;feel&quot; or emotional impression (e.g., inspiring for
      Canada Day, cautious for marriage fraud). Therefore, descriptive
      transcripts should provide both the factual content of the video as well as
      details to provide that impression.
    </p>

    <p>
      As well, captions and descriptive transcripts do not need to replicate
      verbatim what is said in the video, but rather <em>aid in understanding the
        video</em>. How we interpret information visually and aurally is different.
      It is better to edit the captions to aid the viewer in understanding what
      is being said than to faithfully transcribe what someone said since, when
      viewed onscreen, it could be disjointed and difficult to read.
    </p>

    <p>
      As written materials, descriptive transcripts must respect the spelling,
      formatting, punctuation and other rules of The Canadian Style and Le guide
      du rédacteur, both available on <a
        href="http://www.btb.termiumplus.gc.ca/"
        target="_blank"
      >Termium</a> .
    </p>

    <h4>Attribution tags in descriptive transcripts</h4>In descriptive
    transcripts and when needed in captions, attribution tags should proceed
    spoken words as the method to indicate who is speaking.

    <ul>
      <li>Attribution tags must be in ALL CAPS, followed by a colon.</li>

      <li>If there is an unnamed narrator, refer to them as NARRATOR.</li>

      <li>
        If someone is introduced by name, use their first name or title
        (e.g., Dr., Judge, Hon.) and last name, if appropriate.
      </li>

      <li>
        If people are not named, use a generic indicator like &quot;MAN
        BeING INTeRVIeWeD&quot;. If there are several, use &quot;FIRST MAN
        BeING INTeRVIeWeD&quot;.
      </li>
    </ul>

    <p>
      If the person speaking is not onscreen, use &quot;(voice-over)&quot;
      after the attribution tag to indicate someone is speaking over the
      video.
    </p>

    <p>
      Do not use quotation marks unless the person speaking is quoting
      someone.
    </p>

    <p>
      While spoken words can be edited for clarity, do not edit someone
      quoting religious scripture, literary passages, direct quotes, and so on.
      If they make an error, leave the error intact.
    </p>

    <p>
      In descriptive transcripts, all dialogue, including the attribution tag,
      is to be in <strong>boldface</strong>. This helps set it apart from
      onscreen description.
    </p>

    <p>
      Since the descriptive transcript is independent of the video, all spoken
      words must have an attribution tag. Have the attribution tag and what is
      being said on the same line. Do not have a line break or a new paragraph
      after the attribution tag.
    </p>

    <p>
      If using several generic indicators (e.g., &quot;FIRST MAN BeING
      INTeRVIeWeD&quot;, &quot;SeCOND MAN BeING INTeRVIeWeD&quot;), make sure
      there is coordination between the attribution tags and the description.
    </p>

    <p>
      Do NOT use ellipses if there is a pause in the dialogue. The transcript
      is not-time sensitive. ellipses should only be used when:
    </p>

    <ul>
      <li>
        Someone does not complete a sentence or thought and it is important
        to communicate that fact, or
      </li>

      <li>
        It is necessary to communicate the pauses for the reader to
        understand the “feel” of the video, such as humour or frustration.
      </li>
    </ul>

    <p>
      Break up dialogue into different paragraphs if a new idea is expressed,
      just like in formal writing, even if same person is speaking. Do not use
      another attribution tag, but continue to use <strong>boldface</strong>.
