    <h2>Qu’est-ce qu’une transcription descriptive?</h2>

    <p>
      <img
        alt="Feuille de papier avec écriture et clap de cinéma."
        height="201"
        src="~/assets/descriptive_transcript.jpg"
        class="pull-right"
        width="201"
      >Lorsque vous rédigez un scénario de vidéo, une étape qui
      précède la production, vous devez non seulement y inclure le contenu qui
      s’affichera à l’écran, mais vous devez également créer une autre version
      accessible, que l’on appelle transcription descriptive.
    </p>

    <p>
      <strong>Les transcriptions descriptives</strong> sont <em>un autre
        format</em> de la vidéo et non un outil que l’on utilise avec la vidéo. On
      doit pouvoir lire la transcription descriptive indépendamment de la vidéo
      et en tirer la même information et la même impression que si l’on regardait
      et entendait la vidéo.
    </p>

    <p>
      Une vidéo est bien plus que la somme des mots et des images qu’elle
      contient. Grâce à la sélection des prises, au rythme choisi, aux extraits
      musicaux et aux transitions, une vidéo peut véhiculer des impressions ou
      des émotions (p. ex. un message inspirant pour la fête du Canada, un
      avertissement pour se protéger de la fraude relative au mariage). Par
      conséquent, les transcriptions descriptives doivent communiquer le contenu
      factuel de la vidéo ainsi que les détails qui suscitent ces impressions ou
      émotions.
    </p>

    <p>
      Également, les sous titres et transcriptions descriptives ne doivent pas
      reprendre mot pour mot ce qui est dit dans la vidéo; elles ont plutôt pour
      but d’aider à comprendre la vidéo. Notre façon d’interpréter l’information
      visuellement et oralement est différente. Pour aider le spectateur à
      comprendre ce qu’une personne dit et éviter que le message à l’écran manque
      de fluidité et soit difficile à lire, il vaut mieux modifier les sous
      titres plutôt que de retranscrire fidèlement le texte.
    </p>

    <p>
      Comme tout autre document écrit, les transcriptions descriptives doivent
      respecter les règles d’orthographe, de mise en page et de ponctuation,
      autres entres, décrites dans <em>Le guide du rédacteur</em> et <em>The
        Canadian Style</em>, tous deux disponibles sur <a
        href="http://www.btb.termiumplus.gc.ca/"
        target="_blank"
      >Termium</a> .
    </p>

    <h4>Identificateurs dans les transcriptions descriptives</h4>

    <p>
      Dans les transcriptions descriptives et, au besoin, dans les sous
      titres, des identificateurs doivent précéder le texte parlé afin de
      préciser qui parle.
    </p>

    <ul>
      <li>
        Les identificateurs doivent être eN MAJUSCULeS, suivis de deux
        points.
      </li>

      <li>If there is an unnamed narrator, refer to them as NARRATOR.</li>

      <li>Si le narrateur n’est pas nommé, indiquez seulement NARRATeUR.</li>

      <li>
        Si quelqu’un est présenté par son nom, n’utilisez que le prénom ou
        le titre (p. ex. docteur, juge, honorable) et le nom, le cas
        échéant.
      </li>
    </ul>

    <p>
      Si la personne qui parle n’apparaît pas à l’écran, utilisez &laquo;
      (voix hors champ) &raquo; après l’identificateur pour indiquer qu’une
      personne parle pendant la vidéo.
    </p>

    <p>
      Il ne faut pas utiliser de guillemets, sauf si la personne qui parle
      cite une autre personne.
    </p>

    <p>
      Même si les mots parlés peuvent être modifiés par souci de clarté, il ne
      faut jamais modifier les paroles d’une personne qui cite des textes
      religieux ou des passages d’une &oelig;uvre littéraire, qui fait une
      citation directe, etc. Si ces personnes font une erreur, il faut laisser
      l’erreur telle quelle.
    </p>

    <p>
      Dans les transcriptions descriptives, tous les dialogues, incluant
      l’identificateur, doivent être en <strong>caractères gras</strong>. Cela
      permet de distinguer les dialogues des descriptions à l’écran.
    </p>

    <p>
      Comme la transcription descriptive est indépendante de la vidéo, tous
      les mots parlés doivent être précédés d’un identificateur. L’identificateur
      et les paroles prononcées doivent figurer sur la même ligne. Il ne faut pas
      faire de saut de ligne ou créer un nouveau paragraphe après
      l’identificateur.
    </p>

    <p>
      Si vous utilisez plusieurs identificateurs génériques (p. ex. &laquo;
      PReMIeR HOMMe INTeRVIeWÉ &raquo;, &laquo; DeUXIÈMe HOMMe INTeRVIeWÉ
      &raquo;), assurez-vous de bien coordonner les identificateurs et la
      description.
    </p>

    <p>
      N’UTILISeZ PAS les points de suspension pour indiquer une pause dans un
      dialogue. La transcription n’indique pas le temps qui s’écoule. Il convient
      d’employer les points de suspension seulement dans les cas suivants :
    </p>

    <ul>
      <li>
        Une personne ne termine pas sa phrase ou sa pensée, et il est
        important de préciser cette information;
      </li>

      <li>
        Il faut communiquer les pauses au lecteur pour qu’il comprenne
        l’&laquo; ambiance &raquo; de la vidéo, par exemple, si ces pauses
        reflètent des traits d’humour ou de la frustration.
      </li>
    </ul>

    <p>
      Subdivisez le dialogue en différents paragraphes si une nouvelle idée
      est exprimée, comme dans un texte écrit habituel, même si c’est la même
      personne qui parle. N’utilisez pas un autre identificateur, mais conservez
      les caractères gras.
    </p>
