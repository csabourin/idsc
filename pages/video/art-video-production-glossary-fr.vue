  <div id="articleContent" class="ajaxed-in">
    <h2>Glossaire de la production vidéo</h2>
    <dl class="dl-horizontal">
      <dt>Rouleau B</dt>
      <dd>Séquence supplémentaire ou optionnelle s'ajoutant à la séquence principale dans une vidéo et venant enrichir le récit et donner plus de souplesse au moment du montage.</dd>

      <dt>Sous titrage codé</dt>
      <dd>Fonction visant l'accessibilité, qui peut être activée ou désactivée par l'utilisateur de la vidéo. elle peut servir si la vidéo n'existe que dans une langue qui n'est pas celle de l'auditeur visé.</dd>

      <dt>Transcription descriptive</dt>
      <dd>Fonction qui décrit ce qui se passe à l'écran et qui permet donc aux personnes ayant une déficience visuelle de connaître le contexte.</dd>

      <dt>Liste de montage</dt>
      <dd>Liste indiquant où couper la vidéo (minutes et plan d'ouverture/de fermeture). </dd>

      <dt>Montage final</dt>
      <dd>Dernière ébauche avant que la version finale ne soit produite. Tous les éléments sont inclus. Les changements apportés ne visent que le texte, les formes, etc. Aucun changement n'est apporté au contenu.</dd>

      <dt>Éléments graphiques</dt>
      <dd>Tout élément graphique d'une vidéo (page d'introduction, blocs de nom et de titre, générique et tout autre graphique figurant dans la vidéo).</dd>

      <dt>Premier assemblage (audio)</dt>
      <dd>Premier assemblage d'une vidéo, sans montage aucun. Sert à créer la liste de montage pour pouvoir couper la vidéo comme il se doit.</dd>

      <dt>Premier assemblage (vidéo)</dt>
      <dd>Vidéo qui intègre des segments audio et des éléments graphiques généraux, et qui donne un aperçu de l'orientation générale de la vidéo et de ses principales caractéristiques visuelles.</dd>

      <dt>Script </dt>
      <dd>Document rédigé à l'intention des acteurs dans lequel figure le texte convenant autant aux productions vidéo qu'aux productions audio.</dd>

      <dt>Sous titres</dt>
      <dd>Codés sous forme fixe (ne peuvent être activés ou désactivés), ils servent habituellement à fournir l'information dans une autre langue. Les sous titres n'ont pas à respecter les normes relatives à l'admissibilité.</dd>

      <dt>Transcription </dt>
      <dd>Transcription écrite des mots prononcés dans une vidéo. Ne comprend pas les effets sonores ni le contexte.</dd>
    </dl>
  </div>
</template>
