    <h2>The Creative Concept Meeting</h2>
    <p>As an instructional designer, one of the first tasks you will likely be involved in when designing a video is the creative concept meeting. Whether production happens in-house or is outsourced, you play a vital role in ensuring the effectiveness and quality of your program’s video.</p>
    <p>The general goal of the creative concept meeting is to begin the design and planning tasks by discussing the concept, its effectiveness, its design and the expectations for the final product. This meeting typically includes a learning advisor or instructional designer, a subject-matter expert and a video production specialist. Ideally, all these roles should work together early on in the design phase in order to mitigate any risk of redesign or unrealistic timelines or budgets.</p>
    <p>To help you prepare for a creative concept meeting, make sure you have thought about the following questions:</p>
    <ul>
      <li>What is your target audience?</li>
      <li>Who is the end user? </li>
      <li>What is the main goal of your video? Are you trying to persuade, to inform, to present or to teach? </li>
      <li>What is the intended learning outcome?</li>
      <li>Where will this product be used? Online or in classrooms, or both?</li>
      <li>What is the available timeline?</li>
      <li>What is your budget?</li>
    </ul>
    <p>At CSPS, the Learning Technologies and Services’ design and development team has video production experts who can support program video design and assist you in making the right design choices.</p>
