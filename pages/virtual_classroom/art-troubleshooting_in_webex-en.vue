        <h2>Troubleshooting in WebEx</h2>
        <p>The following are common technical difficulties that may arise in the WebEx virtual classroom, including problems with logging in and audio and visual issues.  Use the following information to prevent and/or troubleshoot such situations.</p>
        <details>
          <summary><h3>Log-in problems</h3></summary>

          <h4>Participant cannot log in to the class</h4>
          <p>For sessions requiring registration:</p>
          <ul>
            <li>Forward the participant the session e-mail invitation.</li>
          </ul>
          <p>For sessions not requiring registration:</p>
          <ul>
            <li>
              From the WebEx classroom, go to the Participant menu and select “Invite & Remind.”
              Send the participant an automatic e-mail invitation from WebEx.
            </li>
          </ul>
          <h4>Facilitator cannot log in to the class</h4>
          <ol>
            <li>
              Forward the facilitator  the session e-mail invitation
              and have them log in as a participant.
            </li>
            <li>
              Right click on their name in the Participant
              panel and assign them the role of panelist.
            </li>
          </ol>
          <h4>Calling in</h4>
          <ol>
            <li>
              Click the Audio button under the Participant panel or
              Select the Audio menu at the top left-hand corner of the screen.
            </li>
            <li>
              Select “Audio Conference.”  If connecting by phone, select “Call Me at a New Number,”
              enter area code and phone number, and select “Call Me.”
            </li>
            <li>If using computer for audio, select “Use Computer for Audio” and “Call Using Computer.”</li>
          </ol>
        </details>
        <details>
          <summary><h3>Audio problems</h3></summary>
          <h4>Producer cannot hear a participant</h4>
          <p>Using a computer headset, please follow these steps:</p>
          <ol>
            <li>Make sure participant’s headset is equipped with a microphone.</li>
            <li>ensure that the mute button on the headset cord is not enabled.</li>
            <li>
              Go to the Audio menu at the top of your screen, select “Microphone/speaker Test.”
              Select the appropriate headset and microphone in the dropdown menus that appear; i.e.
              if you have a Platronic headset, select “Platronic” in both dropdown menus.
            </li>
            <li>Close the audio test window and test your audio by saying “Hello?”</li>
          </ol>
          <h4>Producer cannot hear a participant</h4>
          <p>Using a telephone, please follow these steps:</p>
          <ol>
            <li>
              If the participant’s mic is muted, the telephone icon next to their name will
              have a red ‘X’ next to it. To unmute, instruct participant to click the microphone
              icon next to their name and the red ‘X’ icon will disappear.
            </li>
          </ol>
          <h4>Producer hears background noise</h4>
          <ol>
            <li>Ask participant to mute their mic by clicking the microphone button beside their name in the participant list.</li>
            <li>Click again to unmute.</li>
          </ol>
          <h4>Participant appears as “call-in user no. ‘X’”   rather than by their name*</h4>
          <ol>
            <li>Ask participant to hang up and call back.</li>
            <li>
              They must select “Audio Conference” and “I Will Call In.” The phone number, session number, and ID number will be displayed. They must dial each number, starting with the first.
              *This happens when participants don’t use their attendee number when calling in.
            </li>
          </ol>
        </details>
        <details>
          <summary><h3>Visual problems</h3></summary>
          <h4>Slides are not advancing</h4>
          <ol>
            <li>Check to be sure the facilitator still has control of the ball. If not, return the ball to the facilitator.</li>
          </ol>
          <h4>“Chat” is not visible</h4>
          <ol>
            <li>Go to the top right-hand corner of the WebEx classroom screen? and click on the dropdown arrow.</li>
            <li>Select “Manage Panels” and make sure “Chat” is one of the available panels.</li>
            <li>Alternatively, reduce the size of the Participant panel by holding down the line and dragging it to increase or decrease the size. </li>
          </ol>
          <h4>Whiteboard text not appearing</h4>
          <ol>
            <li>Click on the ‘T’ button at the top left-hand corner of the screen.</li>
            <li>Write your text on the whiteboard.</li>
            <li>Click anywhere on the screen and the text will appear.</li>
          </ol>
          <h3>Other technical issues</h3>
          <h4>Participant has lost WebEx connection but is still on the phone</h4>
          <ol>
            <li>Keep the participant on the phone for the next two steps.</li>
            <li>
              Ask participant to return to the session invitation e-mail and click on the session link.
              The Session Information page will appear.
            </li>
            <li>
              Ask participant to enter session password and click “Join Now.”
              The session window will appear.  Then, the Audio Conference dialog box will appear.
            </li>
            <li>Ask participant to hang up and call back.</li>
            <li>
              enter the participant’s area code and phone number beside Use Phone and click “Call Me.”
              WebEx will call the number provided.
            </li>
            <li>
              Participant should accept the call and follow the instructions
              to be placed into the WebEx session.
            </li>
          </ol>
        </details>
        <details>
          <summary><h3>Breakout Rooms</h3></summary>
          <h4>Participant is stuck in the main session</h4>
          <ol>
            <li>Have participant manually join the Breakout Room.</li>
            <li>
              Alternatively, the host needs to remind the participant to click
              “Yes” to all pop-ups inviting them to join a breakout session.
            </li>
          </ol>
          <h4>Audio issues</h4>
          <ol>
            <li>
              Participant appears as a call-in user. The host needs to resolve any call-in
              user issues before starting the breakout sessions, otherwise the call-in user will
              stay in the main session. Ask participant to hang up and call back.
            </li>
            <li>
              Non-integrated teleconference line: The use of WebEx Audio
              is required for breakout sessions in the training centre.
            </li>
          </ol>
          <h4>Participant leaves breakout rooms and needs to return</h4>
          <ol>
            <li>The participant can select which session they wish to join in the Breakout Session panel and click “Join.”</li>
          </ol>
        </details>
        <details>
          <summary><h3>Using Polls and Quizzes</h3></summary>
          <h4>Participant can’t see other panels (e.g. Chat panel, Participant panel) after poll is completed</h4>
          <ol>
            <li>
              Instruct participants to click on the small arrow to the left of the blue bars
              reading “Participant” and “Chat” at the top right of the screen.
            </li>
            <li>
              Ask participants to give you a green checkmark to
              indicate they successfully reopened their panels.
            </li>
          </ol>
        </details>
        <details>
          <summary><h3>Using Video</h3></summary>
          <h4>Video lags</h4>
          <p>The host needs to disable participant webcams.</p>
          <ol>
            <li>Go to the Participant menu.</li>
            <li>Select “Assign Privileges.”</li>
            <li>Uncheck “Send Video.”</li>
          </ol>
          <h4>Wrong webcam selected</h4>
          <p>To use a webcam other than your laptop’s integrated webcam:</p>
          <ol>
            <li>Plug an external webcam into your laptop.</li>
            <li>Click on the Settings icon at the top right-hand of the video panel.</li>
            <li>Under Capture Device, select the webcam requested.</li>
            <li>Click OK.</li>
          </ol>
        </details>
