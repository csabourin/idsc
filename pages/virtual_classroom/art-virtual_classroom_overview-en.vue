          <h4>extra Tips on event Type</h4>
          <ul>
            <li>
              One of the advantages of a synchronous classroom is that it is relatively easy to involve a subject
              matter expert (SMe), co-presenters or panels of experts during virtual presentations in an event. The
              entire session may revolve around an expert or they may be a small part of an overall lesson/course. Keep
              participant attendance to 15 or less so that people have the opportunity to interact with the SMe,
              co-presenters or panel of experts, or, if you wish to deliver to a larger group, manage questions through
              chat during the session or email in advance (in this way the session moderator will choose which questions
              to address and will act more as a host, saying, "Thank you, expert, let's move to our next question…). The
              audience can be 10 to more people.
            </li>
            <li>
              A good rule of thumb is that the expert should plan on preparing content that fills 50% of the
              scheduled event time. For example, a 60-minute event should contain 30 minutes of lecture and 30 minutes
              should be allocated for introduction, questions, discussions, and collaboration. If the session is one
              hour, you may want to stop for Q&amp;A at the half-way point and again just before the end; if the session
              is 45 minutes or less, you may hold questions until the end.
            </li>
            <li>
              Assessments or evaluations are not necessary since the primary purpose of an expert Q&amp;A is to
              gather information from said expert. If you choose to evaluate an expert Q&amp;A session, it would
              primarily be in order to evaluate the presenter's ability to speak with authority and credibility on the
              subject (Level 1).
            </li>
            <li>Session can be recorded for on demand playback.</li>
          </ul>
