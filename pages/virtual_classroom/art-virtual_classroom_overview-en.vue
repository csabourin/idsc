          <h4>extra Tips on event Type</h4>
          <ul>
            <li>
              If participants need to learn new information essential to their job, or if they need to be able to do
              something new at the end of the session, you are designing training. Training is characterized by
              collaborative interactions among participants, some type of assessment, and potentially work that needs to
              be completed before and after the live event(s) takes place.
            </li>
            <li>
              Training may be done in one session, but you will frequently find that an effective training program
              requires multiple sessions with "intersession work" as part of the mix (intersession work will require the
              trainer to conduct "administrative" tasks such as making sure assignments are done and answering questions
              from participants between sessions).
            </li>
            <li>
              Training classes can range from 1–2 hours; <em>never</em> more than two. If a two-hour session is
              planned, a short, 10-minute break should be placed in the middle of the session.
            </li>
            <li>
              This type of delivery is the least passive for the participants (compared with the other two) and needs
              to be designed quite purposefully to ensure that the correct knowledge/skill is transmitted and that the
              sessions are interactive enough to hold people's attention for a long period of time. Training programs
              always require leader guides and participant materials/workbooks.
            </li>
            <li>Class size should not be more than 15 participants.</li>
            <li>
              Assessments or evaluations can be conducted at Level 1 and 2; Level 3 evaluations can be conducted
              off-line via managers or online via a follow-up session where participants are asked to explain how they've
              used what they learned and discuss what problems they've encountered.
            </li>
          </ul>
