        <li>Choisissez l'option &laquo;&nbsp;Conférence audio&nbsp;&raquo;. Si vous vous connectez par téléphone, choisissez l'option &laquo;&nbsp;Appelez-moi à un nouveau numéro &raquo; saisissez un code régional et un numéro de téléphone et choisissez &laquo;&nbsp;Appelez-moi&nbsp;&raquo;.</li>
