    <h2>Dépannage de WebEx</h2>
    <p>L'information suivant comprend des problèmes techniques communs qui peuvent survenir dans la salle de classe virtuelle WebEx, y compris les problèmes liés à la connexion, à l'audio, au visuel et autres situations techniques. Utilisez cette information comme outil de travail pour vous aider à prévenir et/ou à résoudre ces situations.</p>

