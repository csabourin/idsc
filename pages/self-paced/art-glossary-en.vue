        <details>
          <summary> A </summary>
          <dl>
            <dt>Accessibility</dt>
            <dd>
              A characteristic of technology that enables people with disabilities to use it. For example,
              accessible websites can be navigated by people with visual, hearing, motor, or cognitive
              impairments. Accessible design also benefits people with older or slower software and
              hardware.
            </dd>
            <dt>ADDIe model</dt>
            <dd>
              A traditional instructional model that recommends the following steps Analysis, Design,
              Development, Implementation, and evaluation.
            </dd>
            <dt>Adult learning theory</dt>
            <dd>
              This approach recognizes the role that the adult trainee plays in terms of their goals, past
              experience, expectations and the need and ability to control their learning experience. See also
              andragogy.
            </dd>
            <dt>Advanced Distributed Learning (ADL)</dt>
            <dd>
              An initiative guided by the U.S. Department of Defense to develop a shared technical framework
              or computer standard, especially for web based content. Two of the main aims of this process would
              be interoperability and reusability.
            </dd>
            <dt>Alpha version</dt>
            <dd>
              Generally referred to as the pilot version. The aim of the pilot version is to test the overall
              usability, look and feel and training effectiveness of the course. Based on feedback from the test
              audience the program will be refined and any necessary changes will be made. Making use of an Alpha
              version is a good idea because it generally speeds up the development process by cutting down on
              rework.
            </dd>
            <dt>Analysis</dt>
            <dd>
              The first step in the classic ADDIe model. Although the Analysis step is often overlooked it is
              in fact the most important step in the development of eLearning materials.
            </dd>
            <dt>Andragogy</dt>
            <dd>
              The process of developing learning materials for an adult audience. The term was coined by
              Knowles who contended that adults are different from children in that their learning experienced is
              not only shaped by their interaction with the materials but also by their ability to control their
              interactions. Adults may also have preconceived ideas about particular types of learning based on
              past experiences.
            </dd>
            <dt>Animation</dt>
            <dd>
              "Presenting images in a rapid manner to create the illusion of movement. Television cartoons
              essentially introduced and perfected the art of animation. The rapid sequential presentation of
              slightly differing graphics to create the illusion of motion. Animation can have greater purpose in
              illustrating a process than a static visual, but it requires more information to be processed by
              the computer and thus higher bandwidth. Compare to audio, video, text, and graphic."
            </dd>
            <dt>Assessment</dt>
            <dd>
              A process whereby a trainee's progress is systematically measured and then evaluated using a
              set up questions, activities, case studies or simulations.
            </dd>
            <dt>Asynchronous eLearning</dt>
            <dd>
              Interaction that does not take place in real time. examples of asynchronous e-learning can
              include but are not limited to online learning using an eLearning course, visiting an eLearning
              portal, instant messaging a mentor. Asynchronous learning is extensively used because it allows the
              learner to access the information at their own pace and at their own convenience.
            </dd>
            <dt>Asynchronous learning</dt>
            <dd>
              When learners participate in an online learning course at different times, it is known as
              asynchronous learning. This might also be called eLearning or web-based training (WBT).
              Asynchronous learning allows learners to go through a course at their own pace and on their own
              schedule.
            </dd>
            <dt>Audio conferencing</dt>
            <dd>
              Audio conferencing refers to a connection between three or more locations that involves a
              voice-only connection. This can be done via telephone or via the computer. When the audio
              conference is done between computers over the Internet, it uses a technology known as VOIP (Voice
              Over Internet Protocol).
            </dd>
            <dt>Audio video interleaved (AVI)</dt>
            <dd>
              Uncompressed video file format generally used for DVD applications. AVI file formats can also
              be compressed and streamed over the internet in ASF file format.
            </dd>
            <dt>Authoring</dt>
            <dd>
              Similar to "programming," developers assemble discrete media components using a tool called an
              authoring system.
            </dd>
            <dt>Authoring tool</dt>
            <dd>
              "The software program that is used to develop the eLearning materials. For interactive
              eLearning materials Macromedia Director or Authorware are generally used. Web based content is
              generally developed and deployed using Flash or Shockwave players. A software application or
              program used by trainers and instructional designers to create e-learning courseware. Types of
              authoring tools include instructionally focused authoring tools, Web authoring and programming
              tools, template-focused authoring tools, knowledge capture systems, and text and file creation
              tools."
            </dd>
            <dt>Avatar</dt>
            <dd>
              In online environments, a virtual digital image representing a person. In e-learning avatars
              usually represent the learners. The term comes from a Sanskrit word meaning an incarnation in human
              form.
            </dd>
          </dl>
        </details>
        <details>
          <summary> B </summary>
          <dl>
            <dt>Bandwidth</dt>
            <dd>
              The data transfer and reception rate/capacity of a computer communication channel. Generally
              expressed in bits per second. Bandwidth ranges from low bandwidth with slow transfer rates (modem)
              to high bandwidth with fast transfer rates (ethernet connection within LAN).
            </dd>
            <dt>Beta test</dt>
            <dd>
              Beta testing generally takes place after the initial Alpha test. Beta testing is more involved
              and requires the test group to document all errors including software coding, navigation, content,
              grammar, spelling etc. The Beta test may also bring up suggested changes to the flow of the program
              and the level of usability. The initial RFP will detail whether there will be any additional
              charges to new changes/improvements.
            </dd>
            <dt>Blended learning</dt>
            <dd>
              Blended learning is an instructional approach that includes a combination of online and
              in-person learning activities. For example, students can complete online self-paced assignments by
              a certain date and then meet on-site or online for additional learning activities.
            </dd>
            <dt>Bliki</dt>
            <dd>
              A blog that can be edited by readers or an agreed group of collaborators â€“ a combination of a
              blog and a wiki.
            </dd>
            <dt>Blog</dt>
            <dd>
              An informal website where the creator posts their personal thoughts, often in the form of a
              journal. Blog is a contraction of web log. Generally Blogs will include links to other websites.
              Blogs have become very popular and are viewed by many as an easily accessible site for information
              sharing and acquisition.
            </dd>
            <dt>Branching</dt>
            <dd>
              A non-linear navigation path that is dependent on the learner's response to questions.
              Originally widely used for online survey questionnaires and has been incorporated into some
              eLearning applications. The use of branching in eLearning applications avoids the user being tested
              on materials that they are already proficient in.
            </dd>
            <dt>Browser</dt>
            <dd>
              Software for interacting with, accessing, and viewing information on the Internet or created in
              HTML. Internet explorer and Netscape Navigator are the two most widely used browser
              applications.
            </dd>
          </dl>
        </details>
        <details>
          <summary> C </summary>
          <dl>
            <dt>Cascading style sheets (CSS)</dt>
            <dd>
              A CSS is an HTML feature that enables designers to define how different elements, such as font
              and hyperlink styles to appear. These style sheets can then be applied to a set of webpage or
              across a whole website.
            </dd>
            <dt>Chat</dt>
            <dd>
              Real-time text-based communication in a virtual environment. Chat can be used in e-learning for
              student questions, instructor feedback, or even group discussion.
            </dd>
            <dt>Chat Room</dt>
            <dd>
              A virtual meeting space on the Internet, an intranet, or other network, used for real-time text
              discussions. Unlike one-to-oneInstant Messenger applications, chat rooms enable conversations among
              multiple people at once.
            </dd>
            <dt>Chunking</dt>
            <dd>
              Separating learning materials into smaller and shorter subsections, thereby improving learner
              comprehension and retention.
            </dd>
            <dt>Cloud computing</dt>
            <dd>
              Capability to access data from anywhere rather than being tied to a particular machine. Also
              called "the cloud."
            </dd>
            <dt>Collaborative learning</dt>
            <dd>
              Learning that takes place in a peer-oriented environment. The development of collaborative
              tools such as web conferencing, instant messaging, email, weblogs etc. allow collaborative learning
              to take place between individuals/ groups that are geographically dispersed.
            </dd>
            <dt>Collaborative tools</dt>
            <dd>
              Tools that allow geographically dispersed groups and individuals to work in real time on
              learning assignments. Tools could include web conferencing, online discussion forums, instant
              messaging etc.
            </dd>
            <dt>Community of practice</dt>
            <dd>
              An informal group that shares knowledge in an informal and mutually respectful manner. The
              development of the internet has given rise to an increased number of communities of practice across
              disciplines and business industries.
            </dd>
            <dt>Compliance</dt>
            <dd>
              e-learning that meets established standards of, and has received official approval from, an
              accrediting organization
            </dd>
            <dt>Compliant</dt>
            <dd>
              eLearning content/programs that meets established standards. Generally official approval is
              given by an accrediting organization. An example of this would be ADL SCORM compliance. See also
              Conformant.
            </dd>
            <dt>Compressed file</dt>
            <dd>
              A computer file that has been reduced in size by a compression software program. See also ZIP
              file.
            </dd>
            <dt>Compression</dt>
            <dd>
              The process of making a file smaller to facilitate data transmission across a network or for
              storage purposes. Some file formats such as JPeG are already in a compressed file format.
              Compression results in a loss of quality.
            </dd>
            <dt>Computer-based training (CBT)</dt>
            <dd>
              CBT refers to any type of course that runs on a computer, either on a CD, on a person's hard
              drive or on the Internet. The distinguishing point is that computer-based training does not involve
              an instructor or facilitator who is physically present. Now that most computer-based training
              occurs via the Internet, the term is used infrequently. More common terms are&nbsp;online learning,
              eLearning&nbsp;and&nbsp;Web-based Training (WBT).
            </dd>
            <dt>Content gathering</dt>
            <dd>
              The process of gathering the necessary content to develop eLearning materials. Most often the
              design team will work very closely with subject matter experts (SMe's) to pull together the
              information, structure it and then create the required interactive and multimedia elements.
            </dd>
            <dt>Content management system (CMS)</dt>
            <dd>
              A software application that allows for the storage, indexing, retrieval and archiving of
              content. In addition, it may also allow for version control of content through the use of
              check-in/check out. A CMS allows for the reuse of expensive content assets such as brochures,
              photographs, video etc. See also LMS; LCMS.
            </dd>
            <dt>Cookie</dt>
            <dd>
              Information stored on a user's computer after he or she visits a website. The cookie tracks
              data about that user but can be disabled in the browser.
            </dd>
            <dt>Course map</dt>
            <dd>
              A graphic representation of the course details such as navigation. Course maps are specifically
              useful when a course involves extensive loop backs, remediation or branching.
            </dd>
            <dt>Creative Commons</dt>
            <dd>
              Is a not-for-profit organization and licensing system that offers creators the ability to
              fine-tune their copyright, spelling out the ways in which others may use their works
            </dd>
            <dt>Criterion</dt>
            <dd>
              The user must show a mastery of the eLearning criteria in order to achieve a sufficient score
              to pass the course. The eLearning criteria should be closely tied in with the overall objectives of
              the course.
            </dd>
            <dt>Crowdsourcing</dt>
            <dd>Outsourcing tasks to a distributed group of people.</dd>
          </dl>
        </details>
        <details>
          <summary> D </summary>
          <dl>
            <dt>Dashboard</dt>
            <dd>
              The administration area on your blog software that allows you to post, check traffic, upload
              files, manage comments, and so forth.
            </dd>
            <dt>Delicious (del.icio.us)</dt>
            <dd>
              A social bookmarking site that users to quickly store, organize (by tags), and share favorite
              webpages.
            </dd>
            <dt>Delivery method</dt>
            <dd>
              Term describing the way in which training is distributed to learners. Print-based workbooks,
              classroom, video, audio tapes, CD-ROM, and Internet are all sample delivery methods. See also the
              term "blended solution" which is the in vogue jargon describing a mix of media elements to deliver
              a course or curriculum.
            </dd>
            <dt>Design</dt>
            <dd>
              The second step in the classic ADDIe model of Instructional System Design. The design phase
              builds on the analysis information and includes the formulation of a detailed plan for the
              instruction, known as the Design Document. Sometimes Design is broken into "high level design" for
              the design doc and "low level design" which culminates in a script or storyboard. See also ADDIe
              model.
            </dd>
            <dt>Designer</dt>
            <dd>
              Used to describe any member of a training project team, usually referring to creators such as
              writers, graphic artists, and programmers. Technically, this term should refer only to
              instructional designers, but it is often used synonymously with the term developer.
            </dd>
            <dt>Developer</dt>
            <dd>
              Used to describe a member of a training project team involved in development activities or the
              project team as a whole. Could refer to an instructional designer, graphic designer, writer,
              etc.
            </dd>
            <dt>Development</dt>
            <dd>
              Learning or other types of activities that prepare a person for additional job responsibilities
              and/or enable him to gain knowledge or skills. The creation of training materials or courses, as in
              content development or e-learning development. The third step in the classic ADDIe model of
              Instructional System Design. The development phase follows the plans created in the design phase to
              create materials ready for several iterations of testing and refinement. See also ADDIe model.
            </dd>
            <dt>Digg</dt>
            <dd>
              A popular social news site that lets people discover and share content from anywhere on the
              web. Users submit links and stories and the community votes them up or down and comments on
              them.
            </dd>
            <dt>Discussion board</dt>
            <dd>
              A general term for any online "bulletin board" where you can leave and expect to see responses
              to messages you have left. On the Internet, Usenet provides thousands of discussion boards. Forums
              on the Internet or an intranet where users can post messages for others to read.
            </dd>
            <dt>Distance education</dt>
            <dd>
              educational situation in which the instructor and students are separated by time, location, or
              both. education or training courses are delivered to remote locations via synchronous or
              asynchronous means of instruction, including written correspondence, text, graphics, audio- and
              videotape, CD-ROM, online learning, audio- and videoconferencing, interactive TV, and facsimile.
              Distance learning does not preclude the use of the traditional classroom. The definition of
              distance education is broader than and entails the definition of eLearning.
            </dd>
            <dt>Distance learning</dt>
            <dd>
              The desired outcome of distance education. The two terms are often used interchangeably. Often
              used synonymously with Distance education; distinct as an outcome of Distance education.
              Instruction provided by a human separated by place.
            </dd>
            <dt>Distance learning</dt>
            <dd>
              Distance education/Learning occurs when students and their instructors are in different
              geographical locations and the instruction occurs on an electronic device, such as a computer or
              mobile phone. The learning can occur in a synchronous environment, in which all participants are
              connected at the same time or in an asynchronous environment, when participants are engaged in
              learning at different times.
            </dd>
            <dt>Drupal</dt>
            <dd>
              Is a free, open-source platform and content management system written in php. It is often used
              as a "back end" system that powers community features on many different types of sites, ranging
              from personal blogs to large corporate and political sites.
            </dd>
          </dl>
        </details>
        <details>
          <summary> E </summary>
          <dl>
            <dt>ecosystem</dt>
            <dd>
              A community and their environment functioning as a whole. The blogosphere can be viewed as an
              ecosystem
            </dd>
            <dt>elearning</dt>
            <dd>
              eLearning (short for electronic learning) is an umbrella term that refers to all types of
              training, education and instruction that occurs on a digital medium, like a computer or mobile
              phone.
            </dd>
            <dt>embedding</dt>
            <dd>
              The act of adding code to a website so that a video or photo can be displayed while it's being
              hosed at another site. Many users now watch embedded YouTube videos or see Flickr photos on blogs
              rather than on the original site.
            </dd>
            <dt>emoticons</dt>
            <dd>
              Without face-to-face communication, many meanings in words can be misunderstood or
              misinterpreted. People use body language and voice inflections to communicate emotions, mood, or
              intention. To avoid miscommunication and misunderstanding of a specific language usage, the need
              for transferring emotions via the digital way (emoticons) has evolved. For example a :-) (smiley)
              or ;-) (wink) shows that the author is making a joke. Most instant messenger programs now have
              pictorial versions of the emoticons.
            </dd>
            <dt>end user</dt>
            <dd>
              The person for whom a particular technology is designed; the individual who uses the technology
              for its designated purpose. In e-learning, the end user is usually the student.
            </dd>
            <dt>end user</dt>
            <dd>
              The person or persons who will be using a particular technology and for whom it is
              designed.
            </dd>
            <dt>enterprise-wide eLearning</dt>
            <dd>
              eLearning that is intended for all or most employees within a company. Often part of a
              strategic change of direction with a very short timeline. Also used to support a core
              processes.
            </dd>
            <dt>enterprise-wide e-learning</dt>
            <dd>
              e-learning that's intended for all or most employees within a company. It's often part of a
              strategic change of direction with a very short timeline, but is also used to support a core
              process such as sales.
            </dd>
            <dt>evaluation</dt>
            <dd>
              Any systematic method for gathering information about the impact and effectiveness of a
              learning offering. Results of the measurements can be used to improve the offering, determine
              whether the learning objectives have been achieved, and assess the value of the offering to the
              organization. The final step in the classic ADDIe model of Instructional System Design. The
              evaluation phase involves formative evaluations, evaluations of the product during development, and
              a summative evaluation, the final evaluation of the effectiveness of the training in solving the
              instructional problem. See also ADDIe model.
            </dd>
            <dt>explicit knowledge</dt>
            <dd>Knowledge that's easy to communicate. The opposite of tacit knowledge.</dd>
          </dl>
        </details>
        <details>
          <summary> F </summary>
          <dl>
            <dt>F2F (face-to-face)</dt>
            <dd>
              Term used to describe the traditional classroom environment, but now can be used in the virtual
              context.
            </dd>
            <dt>Facilitative tools</dt>
            <dd>
              electronic features used to deliver online courses. examples include mailing lists, chat
              programs, streaming audio, streaming video, and web pages.
            </dd>
            <dt>False-starter</dt>
            <dd>A person who registers for but does not complete an e-learning course.</dd>
            <dt>FAQ (frequently asked questions)</dt>
            <dd>
              An informational list, in question and answer format, of common inquiries from users about a
              topic or application and standard responses. FAQs appear on websites and discussion boards and
              within desktop applications.
            </dd>
            <dt>Feedback</dt>
            <dd>
              Communication between the instructor or system and the learner resulting from an action or
              process. Can be positive or negative, is used to shape behaviors, and should closely follow an
              action for maximum result. Information provided to learners about the correctness of their response
              (different from remediation).
            </dd>
            <dt>File transfer protocol (FTP)</dt>
            <dd>A method of transferring files over intranets or the Internet.</dd>
            <dt>Firewall</dt>
            <dd>
              Any of a number of security schemes that prevent unauthorized users from gaining access to a
              computer network or that monitor transfers of information to and from the network.
            </dd>
          </dl>
        </details>
        <details>
          <summary> G </summary>
          <dl>
            <dt>Gap analysis</dt>
            <dd>
              The process of figuring out what to do by assessing the gap between where you are and where you
              want to be. Most people begin building from the present into the future. We favor looking at the
              step right before the ultimate one and backing toward the present one step at a time.
            </dd>
            <dt>Granularity</dt>
            <dd>
              &nbsp;The degree of detail something can be broken down into, or the number of discrete
              components making up any type of system. In e-learning, granularity is defined by the number of
              content chunks.
            </dd>
            <dt>GUI (graphical user interface)</dt>
            <dd>
              A computer interface using windows, icons, menus and pointers (a WIMP interface), such as
              Windows.
            </dd>
          </dl>
        </details>
        <details>
          <summary> H </summary>
          <dl>
            <dt>Hashtag</dt>
            <dd>
              Community-driven convention for adding additional context and metadata to tweets. Users add
              them in-line to Twitter posts by prefixing a word with a hash symbol (or number sign).
            </dd>
            <dt>HTML (Hypertext Markup Language)</dt>
            <dd>The programming language used to create documents for display on the World Wide Web.</dd>
            <dt>HTML5</dt>
            <dd>
              The latest iteration of that markup language, and includes new features, improvements to
              existing features, and scripting-based APIs. It is designed to work on just about every platform
              and has been adopted by most mobile phone browsers.
            </dd>
            <dt>HTTP (Hypertext Transfer Protocol)</dt>
            <dd>
              The set of rules and standards that govern how information is transmitted on the World Wide
              Web.
            </dd>
            <dt>Hypertext</dt>
            <dd>
              A system for retrieving information from servers on the Internet using World Wide Web client
              software. Hypertext consists of key words or phrases in a WWW page that are linked electronically
              to other Web pages.
            </dd>
          </dl>
        </details>
        <details>
          <summary> I </summary>
          <dl>
            <dt>Icon</dt>
            <dd>
              A simple symbol representing a complex object, process, or function. Icon-based user interfaces
              have the user click on onscreen buttons instead of typing commands.
            </dd>
            <dt>Informal learning</dt>
            <dd>
              Informal learning occurs when people have a need to know something. They set their own learning
              objectives and acquire knowledge, skills and information in their own ways. This could be through
              asking questions, observing experts, practicing and conversing. It's the kind of natural learning
              humans do outside of a structured environment.
            </dd>
            <dt>Infrastructure</dt>
            <dd>
              The underlying mechanism or framework of a system. In eLearning, the infrastructure includes
              the means by which voice, video, and data can be transferred from one site to another and be
              processed.
            </dd>
            <dt>Instructional design</dt>
            <dd>
              Instructional design involves the identification of the knowledge, information, and skill gaps
              of a particular group of people and creating or selecting learning experiences that close this gap.
              Instructional designers base their learning decisions on cognitive psychology, instructional theory
              and best practices.
            </dd>
            <dt>Instructional designer</dt>
            <dd>
              An instructional designer practices the craft and science of instructional design. This person
              identifies the needs of a targeted audience and determines the best approaches for meeting the
              audience's needs. It could involve designing and writing online learning courses as well as writing
              the manuals needed for Instructor-Led Training. Some instructional designers also create graphics
              and use authoring systems to produce online courses.
            </dd>
            <dt>Instructional strategy</dt>
            <dd>
              Plan of activities (with or without an instructor) to teach content and sequence learning
              experience.
            </dd>
            <dt>Instructional systematic design (ISD) model</dt>
            <dd>
              "The ISD model was developed in the 1960's and it provides a methodical process for the design
              and development of instruction. The development stages of the ISD model generally include Analysis,
              Design, Development, Implementation, evaluation and Maintenance. Term describing the systematic use
              of principles of instruction to ensure that learners acquire the skills and knowledge essential for
              successful completion of overtly specified performance goals. "
            </dd>
            <dt>Instructor led training (ILT)</dt>
            <dd>
              ILT typically refers to providing instruction in a classroom environment where the instructor
              and learners are together at the same time and in the same physical location.
            </dd>
            <dt>Integration</dt>
            <dd>
              Combining hardware, software (and, in e-learning, content) components together to work as an
              interoperable system. The process of integration may also include front-end planning and
              strategy.
            </dd>
            <dt>Interactive multimedia</dt>
            <dd>
              Interactive multimedia allows learners to provide input to an online course and receive
              feedback as a result of the input. The input might consist of a mouse click or drag, gestures,
              voice commands, touching an input screen, text entry and live interactions with connected
              participants.
            </dd>
          </dl>
        </details>
        <details>
          <summary> J </summary>
          <dl>
            <dt>JPeG (Joint Photographic experts Group)</dt>
            <dd>
              A format for image compression that enables the user to weigh image quality against file size.
              JPeG is a lossy compression method, meaning that when the image is compressed, the file is made
              smaller by discarding some of its information. The more the file is compressed, the more
              information is discarded, and the more the image quality is degraded. 2) The subgroup of the
              responsible for setting the standards for the image file format that bears its name.
            </dd>
            <dt>Just-in-time</dt>
            <dd>
              Characteristic of e-learning in which learners are able to access the information they need
              exactly when they need it.
            </dd>
          </dl>
        </details>
        <details>
          <summary> L </summary>
          <dl>
            <dt>Learncasting</dt>
            <dd>
              Online educational or instructional content, which may be delivered via a podcast or a
              syndication feed.
            </dd>
            <dt>Learner centric</dt>
            <dd>
              The process of developing and presenting learning materials in the most effective and learner
              friendly manner for the student.
            </dd>
            <dt>Learning content management system (LCMS)</dt>
            <dd>
              A LCMS is a system that is used to create, store, use and reuse eLearning content in the form
              of learning objects. Although the terms should denote the combination of a learning management
              system (LMS) and a content management system (CMS), LCMS and LMS are often interchangeable
              used.
            </dd>
            <dt>Learning management system (LMS)</dt>
            <dd>
              A web based system that allows for the addition, deployment and tracking of learning content
              used for training purposes. Typically an LMS includes functionality for course catalogs
              (search/browse functionality), launching courses, registering new students, tracking
              current/completed student progress and assessments. Most of the learning management systems are
              developed to be independent of any content development/authoring packages. In addition, an LMS
              usually does not incorporate any authoring functionalities, but rather focuses on managing learning
              content.
            </dd>
            <dt>Learning object</dt>
            <dd>
              Learning objects (LO), also sometimes referred to a reusable learning object (RLO), is the
              smallest building block used in any eLearning program. The main value of using the learning object
