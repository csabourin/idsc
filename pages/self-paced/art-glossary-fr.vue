              d'apprentissage électronique sous forme d'objets d'apprentissage. Même si les termes devraient
              indiquer la combinaison d'un système de gestion de l'apprentissage (SGA) et d'un système de gestion
              du contenu (SGC), le SGCA et le SGA sont souvent utilisés de façon interchangeable.
            </dd>
