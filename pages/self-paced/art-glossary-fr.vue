        <details>
          <summary> A </summary>
          <dl>
            <dt>Accessibilité</dt>
            <dd>
              Caractéristique de la technologie qui permet aux personnes handicapées d'utiliser cette
              technologie. À titre d'exemple, les sites Web accessibles peuvent être consultés par des personnes
              ayant une déficience visuelle, auditive ou motrice, ou des troubles cognitifs. La conception
              accessible aide également les personnes travaillant avec des logiciels et du matériel plus vieux ou
              plus lents.
            </dd>
            <dt>Accord sur les niveaux de service (ANS)</dt>
            <dd>
              Contrat entre une entreprise offrant de l'apprentissage électronique et un client, lequel
              précise les services d'apprentissage électronique qui seront offerts.
            </dd>
            <dt>Adapter</dt>
            <dd>
              Réutiliser des éléments de contenu en les révisant, en les restructurant ou en les présentant
              selon une autre formule. Les objets d'apprentissage ou les éléments de contenu sur papier peuvent
              être adaptés en vue d'être offerts dans l'intranet d'une entreprise ou sur Internet.
            </dd>
            <dt>Analyse</dt>
            <dd>
              Première étape du modèle classique ACDMe. Même si l'étape de l'analyse est souvent négligée, il
              s'agit en fait de l'étape la plus importante dans l'élaboration de matériel d'apprentissage en
              ligne.
            </dd>
            <dt>Analyse des écarts</dt>
            <dd>
              Processus qui consiste à déterminer la marche à suivre en évaluant l'écart entre la situation
              actuelle et la situation visée. La plupart des gens commencent à bâtir l'avenir à partir du
              présent. Nous recommandons plutôt de regarder l'étape précédant immédiatement l'étape ultime, puis
              de reculer vers le présent une étape à la fois.
            </dd>
            <dt>Andragogie</dt>
            <dd>
              Processus d'élaboration de matériel d'apprentissage pour un auditoire adulte. Le terme a été
              inventé par Knowles, qui soutenait que les adultes sont différents des enfants, dans la mesure où
              leur expérience d'apprentissage n'est pas seulement façonnée par leur interaction avec le matériel,
              mais aussi par leur capacité de contrôler leurs interactions. en raison de leurs expériences
              passées, les adultes peuvent aussi avoir des idées préconçues au sujet de certains types
              d'apprentissage.
            </dd>
            <dt>Animation</dt>
            <dd>
              "Présenter des images d'une manière rapide afin de créer une illusion de mouvement. Les dessins
              animés télévisés ont essentiellement présenté et perfectionné l'art de l'animation. La présentation
              séquentielle rapide de graphiques légèrement différents permet de créer une illusion de mouvement.
              L'animation peut avoir une plus grande utilité qu'une représentation visuelle statique si elle
              illustre un processus, mais comme elle nécessite le traitement d'une plus grande quantité
              d'information par l'ordinateur, la largeur de bande requise est également plus grande
              (comparativement à l'audio, à la vidéo, au texte et aux graphiques)."
            </dd>
            <dt>Apprentissage à distance</dt>
            <dd>
              La formation et l'apprentissage à distance se produisent lorsque les étudiants et leurs
              enseignants se trouvent dans des lieux géographiques différents, et que l'enseignement s'effectue
              au moyen d'un appareil électronique, comme un ordinateur ou un téléphone mobile. L'apprentissage
              peut se produire dans un environnement synchrone, dans lequel tous les participants se connectent
              au même moment, ou dans un environnement asynchrone, où les participants effectuent leur
              apprentissage à des moments différents.
            </dd>
            <dt>Apprentissage asynchrone</dt>
            <dd>
              Lorsque les apprenants suivent un cours d'apprentissage en ligne à des moments différents, on
              parle d'«&nbsp;apprentissage asynchrone&nbsp;». On peut aussi parler d'«&nbsp;apprentissage en
              ligne&nbsp;» ou de «&nbsp;formation sur le Web&nbsp;». L'apprentissage asynchrone permet aux
              apprenants de suivre un cours à leur propre rythme et selon leur propre horaire.
            </dd>
            <dt>Apprentissage au rythme de l`apprenant</dt>
            <dd>
              Par «&nbsp;apprentissage au rythme de l'apprenant&nbsp;», on entend le type d'enseignement qui
              permet à une personne de contrôler la vitesse à laquelle elle souhaite assimiler le contenu des
              didacticiels. Il suppose la mise en place d'un environnement d'apprentissage asynchrone.
            </dd>
            <dt>Apprentissage axé sur l'apprenant</dt>
            <dd>
              Processus qui consiste à élaborer et à présenter à l'étudiant le matériel d'apprentissage de la
              façon la plus efficace et favorable possible.
            </dd>
            <dt>Apprentissage coopératif</dt>
            <dd>
              Apprentissage qui se déroule dans un environnement axé sur les pairs. Le développement d'outils
              de collaboration, comme la cyberconférence, la messagerie instantanée, le courriel et les blogues,
              permet un apprentissage coopératif entre des personnes ou des groupes qui sont géographiquement
              dispersés.
            </dd>
            <dt>Apprentissage électronique</dt>
            <dd>
              L'apprentissage électronique est un terme hyperonyme qui désigne tous les types de formation,
              d'éducation et d'enseignement qui se déroulent sur un support numérique, comme un ordinateur ou un
              téléphone mobile.
            </dd>
            <dt>Apprentissage électronique asynchrone</dt>
            <dd>
              Interaction qui n'a pas lieu en temps réel. Voici des exemples d'apprentissage en ligne
              asynchrone&nbsp;: apprentissage au moyen d'un cours d'apprentissage en ligne, visite d'un portail
              d'apprentissage en ligne, envoi de messages instantanés à un mentor. L'apprentissage asynchrone est
              abondamment utilisé parce qu'il permet à l'apprenant d'accéder à l'information à son propre rythme
              et au moment qui lui convient.
            </dd>
            <dt>Apprentissage électronique synchrone</dt>
            <dd>
              Communication et formation diffusées simultanément à un groupe de personnes. Des exemples
              d'apprentissage en ligne synchrone comprennent notamment les conférences vidéo ou audio, le partage
              d'applications ou de tableaux blancs, la messagerie instantanée et les bavardoirs en direct.
            </dd>
            <dt>Apprentissage en ligne</dt>
            <dd>
              Hyperonyme qui comprend tout type d'apprentissage accompli sur un ordinateur et habituellement
              sur Internet (virtuel, WebEx, au rythme des apprenants, etc.).
            </dd>
            <dt>Apprentissage en ligne à l'échelle de l'organisation</dt>
            <dd>
              Apprentissage électronique destiné à l'ensemble ou à la plupart des employés au sein d'une
              entreprise. Il s'inscrit souvent dans un changement d'orientation stratégique qui doit s'effectuer
              dans un très court délai. Cet apprentissage sert également à appuyer les processus de base.
            </dd>
            <dt>Apprentissage informel</dt>
            <dd>
              L'apprentissage informel se produit lorsque les gens ont besoin de savoir quelque chose. Les
              gens établissent leurs propres objectifs d'apprentissage et acquièrent des connaissances, des
              compétences et de l'information de la façon qui leur convient, que ce soit en posant des questions,
              en observant des experts, en s'exerçant ou en discutant. Il s'agit du type d'apprentissage naturel
              que les personnes font à l'extérieur d'un environnement structuré.
            </dd>
            <dt>Apprentissage mixte</dt>
            <dd>
              L'apprentissage mixte est une approche pédagogique qui comprend un agencement d'activités
              d'apprentissage en ligne et en personne. À titre d'exemple, les étudiants peuvent effectuer leurs
              travaux en ligne à leur propre rythme avant une certaine date, puis se rencontrer sur place ou en
              ligne pour réaliser d'autres activités d'apprentissage.
            </dd>
            <dt>Apprentissage mobile</dt>
            <dd>
              Apprentissage qui s'effectue sur un appareil portatif (p. ex. téléphone mobile), et qui peut
              avoir lieu n'importe quand et n'importe où.
            </dd>
            <dt>Apprentissage sur les médias sociaux</dt>
            <dd>
              L'apprentissage sur les médias sociaux désigne l'acquisition d'information et de compétences au
              moyen des technologies sociales qui permettent aux personnes de collaborer et de discuter ensemble,
              de fournir des commentaires ainsi que de créer du contenu et de le partager. L'apprentissage sur
              les médias sociaux peut notamment se faire sur des plates-formes en ligne de réseaux sociaux, des
              blogues et des microblogues (comme Twitter), des émissions de radio parlée en ligne et des
              wikis.
            </dd>
            <dt>Apprentissage synchrone</dt>
            <dd>
              Lorsque les apprenants participent simultanément à un cours d'apprentissage en ligne, mais à
              partir d'endroits différents, on parle d'apprentissage synchrone. L'apprentissage synchrone permet
              aux apprenants d'interagir avec l'enseignant et les autres participants. Cet apprentissage est
              réalisé au moyen d'un logiciel qui crée une salle de classe virtuelle.
            </dd>
            <dt>Article 508</dt>
            <dd>
              Article de la U.S. Rehabilitation Act de 1998 qui stipule que tous les documents électroniques
              et de technologie de l'information acquis, utilisés ou mis au point par le gouvernement fédéral
              après le 25&nbsp;juin&nbsp;2001, doivent être accessibles aux personnes handicapées. La technologie
              en question vise du matériel (p. ex. imprimantes, télécopieurs, téléphones et autres appareils
              électroniques) ainsi que des logiciels d'application et des sites Web.
            </dd>
            <dt>Assurance de la qualité</dt>
            <dd>
              Terme largement utilisé pour décrire les techniques, les processus et les procédures utilisés
              pour veiller à ce que les produits et services d'une entreprise répondent aux normes d'excellence.
              Les programmes d'assurance de la qualité comprennent habituellement des procédures d'examen par les
              pairs ou d'examen de l'utilisation permettant de remédier aux problèmes liés à la qualité.
            </dd>
            <dt>Audioconférence</dt>
            <dd>
              L'audioconférence renvoie à un lien entre au moins trois&nbsp;endroits ayant une connexion
              vocale seulement. Cela peut se faire par téléphone ou par ordinateur. Lorsque l'audioconférence
              s'effectue entre ordinateurs sur Internet, on utilise une technologie connue sous le nom de
              «&nbsp;voix sur le protocole Internet&nbsp;» (VoIP).
            </dd>
            <dt>Avatar</dt>
            <dd>
              Dans les environnements en ligne, il s'agit d'une image numérique virtuelle représentant une
              personne. Dans l'apprentissage en ligne, les avatars représentent habituellement les apprenants. Le
              terme provient d'un mot sanskrit signifiant une incarnation dans une forme humaine.
            </dd>
          </dl>
        </details>
        <details>
          <summary> B </summary>
          <dl>
            <dt>Babillard</dt>
            <dd>
              Terme général pour tout babillard en ligne où vous pouvez afficher des messages et vous
              attendre à voir des réponses aux messages que vous avez affichés. Sur Internet, Usenet offre des
              milliers de babillards. Forums sur Internet ou sur un intranet, où les utilisateurs peuvent
              afficher des messages pour que les autres les lisent.
            </dd>
            <dt>Baladoformation</dt>
            <dd>
              Contenu éducationnel ou pédagogique en ligne pouvant être transmis par baladodiffusion ou par
              un fil de nouvelles.
            </dd>
            <dt>Bande passante</dt>
            <dd>
              Capacité ou taux de transfert et de réception de données d'une voie de communication
              informatique. Habituellement exprimé en bits par seconde. La largeur de bande varie de faible avec
              taux de transfert lents (modem) à grande avec taux de transfert rapides (connexion ethernet à
              l'intérieur d'un réseau local [RL]).
            </dd>
            <dt>Bliki</dt>
            <dd>
              Blogue qui peut être modifié par les lecteurs ou un groupe établi de collaborateurs; il s'agit
              à la fois d'un blogue et d'un wiki.
            </dd>
            <dt>Blogue</dt>
            <dd>
              Site Web non&nbsp;officiel où le créateur affiche ses réflexions personnelles, souvent sous la
              forme d'un journal. Le terme «&nbsp;blogue&nbsp;» est une contraction du terme anglais «&nbsp;web
              log&nbsp;» (blog), qui signifie «&nbsp;carnet&nbsp;Web&nbsp;». en règle générale, les blogues
              comprennent des liens vers d'autres sites Web. Les blogues sont devenus très en vogue et sont
              considérés par de nombreuses personnes comme un site facilement accessible pour la mise en commun
              et l'acquisition d'information.
            </dd>
            <dt>Bus série universel (USB)</dt>
            <dd>
              Norme utilisée pour brancher des appareils périphériques comme des imprimantes et des
              dispositifs de stockage externes (disques durs, clés USB, etc.) sur l'ordinateur d'un
              utilisateur.
            </dd>
          </dl>
        </details>
        <details>
          <summary> C </summary>
          <dl>
            <dt>Capture d'écran</dt>
            <dd>
              Photo de ce qui est affiché à un écran d'ordinateur à un moment donné. Également appelée
              «&nbsp;copie d'écran&nbsp;». Les captures d'écran annotées sont souvent utilisées dans les manuels
              de logiciel et les programmes de formation.
            </dd>
            <dt>Capture vidéo de l'écran</dt>
            <dd>
              Vidéo, habituellement accompagnée d'une narration audio, qui illustre ce qui se passe sur un
              écran d'ordinateur.
            </dd>
            <dt>Carte de cours</dt>
            <dd>
              Représentation graphique des détails du cours, notamment en ce qui concerne la navigation. Les
              cartes de cours sont particulièrement utiles lorsqu'un cours fait souvent appel à l'embranchement,
              exige de nombreux retours en arrière ou nécessite de nombreuses corrections.
            </dd>
            <dt>Clavardage</dt>
            <dd>
              Communication en temps réel en mode texte dans un environnement virtuel. Le clavardage peut
              être utilisé dans l'apprentissage en ligne pour les questions des étudiants, la rétroaction des
              enseignants ou même les discussions en groupe.
            </dd>
            <dt>Clavardoir</dt>
            <dd>
              espace de rencontre virtuel sur Internet, sur un intranet ou sur un autre réseau, utilisé pour
              discuter en mode texte en temps réel. Contrairement aux applications de messagerie instantanée de
              personne à personne, les clavardoirs permettent la tenue de conversations entre plusieurs personnes
              à la fois.
            </dd>
            <dt>Code source</dt>
            <dd>
              Forme dans laquelle un programme informatique est rédigé par le programmeur, c'est-à-dire le
              langage de script. Le code source est ensuite compilé dans un code binaire (machine) ou interprété
              par un compilateur dans ce code.
            </dd>
            <dt>Collecte de contenu</dt>
            <dd>
              Processus qui consiste à recueillir le contenu nécessaire à l'élaboration de matériel
              d'apprentissage électronique. Le plus souvent, l'équipe de conception travaille en étroite
              collaboration avec des experts en la matière pour réunir l'information et la structurer, puis créer
              les éléments interactifs et multimédias requis.
            </dd>
            <dt>Communauté de pratique</dt>
            <dd>
              Groupe non officiel dont les membres s'échangent des connaissances de façon informelle et
              mutuellement respectueuse. Le développement d'Internet a donné naissance à un nombre accru de
              communautés de pratique dans les différentes disciplines et industries.
            </dd>
            <dt>Communauté en ligne</dt>
            <dd>
              Lieu de rencontre pour les gens sur Internet. Les communautés en ligne sont conçues pour
              faciliter l'interaction et la collaboration entre les gens qui partagent des intérêts et des
              besoins. elles peuvent être ouvertes à tous ou seulement aux membres; certaines offrent les outils
              de modérateur et d'autres ne les offrent pas.
            </dd>
            <dt>Communication en temps réel</dt>
            <dd>
              Communication dans le cadre de laquelle l'information est reçue au moment exact où elle est
              envoyée, ou presque. La communication en temps réel est une caractéristique de l'apprentissage
              synchrone.
            </dd>
            <dt>Compression</dt>
            <dd>
              Processus qui consiste à réduire la taille d'un fichier pour faciliter la transmission des
              données sur un réseau ou aux fins de stockage. Certains formats de fichier, comme JPeG, sont déjà
              compressés. La compression entraîne une perte de qualité.
            </dd>
            <dt>Concepteur</dt>
            <dd>
              Utilisé pour décrire un membre d'une équipe de projet de formation; fait habituellement
              allusion aux créateurs (p.&nbsp;ex. auteurs, graphistes et programmeurs). Théoriquement, ce terme
              devrait renvoyer uniquement aux concepteurs pédagogiques, mais il est souvent utilisé comme
              synonyme du terme «&nbsp;développeur&nbsp;».
            </dd>
            <dt>Concepteur pédagogique</dt>
            <dd>
              Le concepteur pédagogique pratique l'art et la science de la conception pédagogique. Il cerne
              les besoins d'un public cible et détermine les meilleures approches pour répondre aux besoins de ce
              public. Cela pourrait englober la conception et la préparation de cours d'apprentissage en ligne
              ainsi que la rédaction des manuels requis pour la formation en salle de classe. Certains
              concepteurs pédagogiques créent également des graphiques et utilisent des systèmes de création pour
              produire des cours en ligne.
            </dd>
            <dt>Conception</dt>
            <dd>
              La deuxième étape du modèle classique utilisé pour la conception de systèmes d'enseignement,
              c'est--à--dire le modèle ACDMe. L'étape de la conception s'appuie sur l'information servant à
              l'analyse et comprend la formulation d'un plan détaillé pour l'enseignement, appelé «&nbsp;document
              de conception&nbsp;». Parfois, la conception est divisée en conception «&nbsp;de haut niveau&nbsp;»
              (pour le document de conception) et «&nbsp;de bas niveau&nbsp;» (qui donne lieu à la création d'un
              scénario ou d'un scénarimage). Voir aussi «&nbsp;modèle ACDMe&nbsp;».
            </dd>
            <dt>Conception pédagogique</dt>
            <dd>
              La conception pédagogique suppose la désignation des écarts dans les connaissances, les
              renseignements et les compétences d'un groupe particulier de personnes, et la création ou la
              sélection d'expériences d'apprentissage visant à combler ces écarts. Les concepteurs pédagogiques
              fondent leurs décisions d'apprentissage sur la psychologie cognitive, la théorie de l'éducation et
              les pratiques exemplaires.
            </dd>
            <dt>Conforme</dt>
            <dd>
              Contenu et programmes d'apprentissage en ligne qui répondent aux normes établies.
              Habituellement, l'approbation officielle est donnée par un organisme d'accréditation. Un exemple
              serait la conformité à ADL SCORM.
            </dd>
            <dt>Conformité</dt>
            <dd>
              Apprentissage en ligne qui répond aux normes établies d'un organisme d'accréditation et qui a
              reçu son approbation officielle.
            </dd>
            <dt>Connaissances explicites</dt>
            <dd>Connaissances faciles à communiquer, contrairement aux connaissances tacites.</dd>
            <dt>Consortium World Wide Web (W3C)</dt>
            <dd>Le Consortium World Wide Web est l'organisme qui régit les normes Web.</dd>
            <dt>Contenu multimédia en continu</dt>
            <dd>
              Permet de lire des fichiers audio ou vidéo au fur et à mesure qu'ils sont téléchargés sur
              Internet plutôt que d'attendre que le fichier soit téléchargé en entier. Nécessite un programme de
              lecteur multimédia.
            </dd>
            <dt>Contenu multiméedia en continu</dt>
            <dd>
              Fichier audio ou vidéo téléchargé sur un ordinateur à partir d'Internet de façon continue et lu
              en temps réel au fur et à mesure qu'il est transféré vers l'ordinateur de destination.
            </dd>
            <dt>Convivialité</dt>
            <dd>
              Indicateur du degré de facilité d'utilisation d'une application d'apprentissage électronique.
              Les critères pour évaluer la convivialité peuvent comprendre la navigation, l'installation et
              l'accès, les fonctionnalités (impression, extraction de résultats), etc.
            </dd>
            <dt>Cours en ligne ouvert à tous</dt>
            <dd>
              Type de cours en ligne visant une participation à grande échelle et un libre accès par le Web.
              Les cours en ligne ouverts à tous sont une nouveauté dans le domaine de l'éducation à distance, et
              représentent un progrès vers l'atteinte des idéaux en matière d'éducation ouverte suggérés par les
              ressources pédagogiques ouvertes. Habituellement, les cours en ligne ouverts à tous n'offrent pas
              les crédits accordés aux étudiants qui paient pour suivre des cours dans des écoles, mais
              l'apprentissage peut être évalué aux fins de certification de l'apprenant.
            </dd>
            <dt>Création</dt>
            <dd>
              À l'instar de ce qui se produit dans le processus de «&nbsp;programmation&nbsp;», les
              développeurs assemblent des composants média discrets en utilisant un outil appelé «&nbsp;système
              de création&nbsp;».
            </dd>
            <dt>Creative Commons</dt>
            <dd>
              Il s'agit d'un organisme sans but lucratif et d'un système de délivrance de permis qui offre
              aux créateurs la capacité de mettre au point leurs droits d'auteur en énonçant les façons dont les
              autres peuvent utiliser leurs œuvres.
            </dd>
            <dt>Critère</dt>
            <dd>
              L'utilisateur doit démontrer une maîtrise des critères d'apprentissage électronique afin
              d'obtenir une note suffisante pour réussir le cours. Les critères devraient être étroitement liés
              aux objectifs globaux du cours.
            </dd>
            <dt>Cyberconférence</dt>
            <dd>
              Désigne une réunion en temps réel, en direct ou synchrone tenue au moyen d'une technologie Web
              comme le partage de fichiers audio et vidéo et d'applications.
            </dd>
          </dl>
        </details>
        <details>
          <summary> D </summary>
          <dl>
            <dt>Delicious (del.icio.us)</dt>
            <dd>
              Site d'échange de signets dont les utilisateurs se servent pour stocker, organiser (au moyen
              d'étiquettes) et partager rapidement leurs pages Web préférés.
            </dd>
            <dt>Demande de propositions (DP)</dt>
            <dd>
              Document écrit officiel transmettant les exigences détaillées à des fournisseurs potentiels. La
              DP est l'un des principaux moyens utilisés pour inviter les fournisseurs potentiels à présenter une
              soumission ou une proposition. La DP est un outil efficace, car elle oblige l'entreprise acheteuse
              à déterminer et à consigner ses exigences opérationnelles et techniques. en retour, elle permet à
              l'acheteur de comparer efficacement les offres et les solutions présentées par les fournisseurs
              potentiels.
            </dd>
            <dt>Développement</dt>
            <dd>
              Activités d'apprentissage ou autres types d'activités qui préparent une personne à assumer
              davantage de responsabilités professionnelles ou lui permettent d'acquérir des connaissances ou des
              compétences. Création de documents ou de cours de formation, comme dans l'élaboration de contenu ou
              d'outils d'apprentissage en ligne. Il s'agit de la troisième étape du modèle classique utilisé pour
              la conception de systèmes d'enseignement, c'est-à-dire le modèle ACDMe. À l'étape du développement,
              on suit les plans créés à l'étape de la conception pour créer des documents prêts pour plusieurs
              itérations de mise à l'essai et de mise au point. Voir aussi «&nbsp;modèle ACDMe&nbsp;».
            </dd>
            <dt>Développeur</dt>
            <dd>
              Utilisé pour décrire un membre d'une équipe de projet de formation participant à des activités
              de développement ou aux activités de l'équipe de projet dans son ensemble. Pourrait faire allusion
              à un concepteur pédagogique, à un concepteur graphique, à un rédacteur, etc.
