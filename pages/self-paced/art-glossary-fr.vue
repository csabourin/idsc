              peut être fondée sur un agencement des forces, des faiblesses et des antécédents culturels de
              l'apprenant. Même si la catégorisation suscite beaucoup de discussions, on trouve habituellement
              dans la documentation les quatre grands styles d'apprentissage suivants&nbsp;: apprendre en voyant
