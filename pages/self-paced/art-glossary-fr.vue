            <dd>
              Système Web qui permet l'ajout, le déploiement et le suivi des éléments de contenu
              d'apprentissage utilisés aux fins de formation. Le SGA comprend habituellement des fonctionnalités
              pour les catalogues de cours (rechercher/parcourir), le lancement de cours, l'inscription de
              nouveaux étudiants, et le suivi des progrès et des évaluations en cours et terminés des étudiants.
              La plupart des SGA sont développés de façon à être indépendants de tout progiciel de développement
              et de création de contenu. en outre, un SGA n'offre habituellement aucune fonctionnalité de
              création, mais est plutôt axé sur la gestion du contenu d'apprentissage.
            </dd>
