    <h2>Processus concernant le Formulaire d'information sur l'activité d'apprentissage (FIAA)</h2>
    <h3>But</h3>
    <p>Le processus concernant le FIAA a comme but ce qui suit :</p>
    <ul>
      <li>aviser les intervenants tels que le Service à la clientèle des ministères, le CCC, l'évaluation, les Services d'édition et les régions de la création prochaine d'un produit d'apprentissage (la modalité pédagogique peut varier);</li>
      <li>fournir toute l'information nécessaire pour créer correctement une entrée dans le catalogue du SHGA et en assurer la qualité;</li>
      <li>permettre à l'équipe chargée du marketing et des communications de collaborer avec les secteurs de programme afin de rédiger une description convenable du produit d'apprentissage qui facilitera la promotion et le marketing du produit.</li>
    </ul>
    <h3>Aperçu du processus</h3>
    <p>Un FIAA bien rempli comprend trois principales composantes :</p>
    <ol>
      <li>Le codage financier (les employés des Opérations d'affaires du SHGA ont besoin du code du domaine fonctionnel pour donner un code de cours);</li>
      <li>Un code de cours pour le produit d'apprentissage;</li>
      <li>Le titre et la description du cours et l'information de schématisation dans la langue de votre choix, qui auront été examinés au préalable par l'équipe chargée du marketing et des communications.</li>
    </ol>
    <p>Les trois composantes doivent être complètes pour que votre FIAA puisse être finalisé et que l'on puisse vous donner l'autorisation de créer votre cours/produit d'apprentissage (en fonction de la modalité pédagogique) dans le SHGA. Il est recommandé de commencer à remplir le FIAA au début du processus de création de votre produit d'apprentissage (quelle que soit la modalité pédagogique).</p>
    <h3>échéances prévues :</h3>
    <ol>
      <li>Codage financier : prévoir deux semaines;</li>
      <li>Code de cours : peut être obtenu dans un délai de 24 heures;</li>
