    <h2>Élaboration d’objectifs d’apprentissage pour des activités d’apprentissage en ligne</h2>
    <p>Les objectifs d’apprentissage sont essentiels pour la création de toute activité d’apprentissage. Tous les autres éléments (contenu de l’activité d’apprentissage, devoirs, évaluations) sont établis à partir de ces objectifs. Voici quelques conseils sur la façon d’élaborer des objectifs d’apprentissage pour des activités d’apprentissage en ligne.</p>
    <h3>Établissez d’abord un but général clair</h3>
    <p>Toutes les activités d’apprentissage commencent par un but général. Si le but général de l’activité d’apprentissage est clair, il est plus facile de créer des objectifs d’apprentissage précis afin d’atteindre ce but.</p>
