    <h2>Writing Learning Objectives for Online Learning Activities</h2>
    <p>Learning objectives are essential in the creation of any learning activity. They provide a foundation on which all the other elements (learning activity content, assignments, and assessments) are built. Below you will find a few tips on how to write learning objectives for online learning activities.</p>
    <h3>Start with a Clear Overarching Goal</h3>
    <p>All learning activities begin with an overarching goal (learning objective). If your overarching learning activity goal is clear, it is easier to build specific learning objectives to meet that overarching goal.</p>
    <p><em>ex.: Prepare several different common types of pie.</em></p>
    <h3>Create your Specific Learning Objectives</h3>
    <ul>
      <li>Learning objectives are more specific statements of learning related to the overarching goal.</li>
      <li>Once you have defined your overarching learning activity goal, you can create your specific learning activity objectives.</li>
      <li>A clearly defined learning objective should specify what is expected of the learner.</li>
      <li>Learning objectives are all about what the learners can demonstrate while taking the course and not what the facilitators can provide.</li>
      <li>Learning objectives describe what learners are able to demonstrate in terms of knowledge, skills, and values upon completion of a course or a program.</li>
      <li>You can also further breakdown your learning objectives and create sub-objectives.</li>
    </ul>
