    <h2>Apprentissage actif enrichi par les technologies</h2>
    <p>
      L’apprentissage actif enrichi par les technologies (AAET) met l’accent
      sur l’apprentissage actif, et NON sur la technologie. L’apprentissage actif
      exige que les apprenants développent leur compréhension du contenu par la
      construction active d’un produit, la résolution d’un problème ou les
      deux.
    </p>
    <p>
      Les caractéristiques uniques des technologies numériques peuvent
      améliorer – intensifier, accroître, amplifier – l’apprentissage actif. Les
      logiciels ouverts, les applications Web, les caméras numériques communs et
      les technologies mobiles presque omniprésentes favorisent la participation
      des apprenants et renforcent leur autonomie lorsqu’ils construisent,
      conçoivent, créent et distribuent des produits qui consolident et
      matérialisent leur apprentissage. Ces technologies offrent des possibilités
      de simulation, d’expérimentation, de recherche et d’expression libre. À
      leur tour, les médias sociaux améliorent l’accès de façon exponentielle :
      accès à l’information, à des possibilités de collaboration, à d’autres
      points de vue et à des auditoires pour les produits créés par les
      apprenants.
    </p>
    <p>
      Toutefois, aussi étonnantes que puissent être les technologies
      numériques, nous devons être clairs : ces technologies permettent
      d’amplifier les efforts, mais elles ne sont pas un fin en soi. S’il est
      impossible d’offrir des possibilités d’apprentissage actif en classe, les
      technologies numériques peuvent automatiser les processus, mais il est peu
      probable qu’elles améliorent l’apprentissage.
    </p>
