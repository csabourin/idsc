    <h2>Technology Enhanced Active Learning</h2>
    <p>
      The focus of Technology Enhanced Active Learning (TEAL) is on active
      learning, NOT on technology. Active learning requires learners to develop
      their understanding of content through the active construction of a
      product, the solving of a problem, or both.
    </p>
    <p>
      The unique characteristics of digital technologies can enhance –
      intensify, increase, amplify – active learning. Open source software, web
      apps, common digital cameras, and almost ubiquitous mobile technologies
      empower and engage learners as they construct, build, create, and
      distribute products that represent and reinforce their learning. These
      technologies provide opportunities for simulation, experimentation,
      research, and self-expression. In turn, social media exponentially improve
      access: access to information, to opportunities for collaboration, to
      alternative perspectives, and to audiences for student-generated
      products.
    </p>
    <p>
      But as amazing as we think digital technologies can be, we should be
      clear: these technologies are amplifiers, not endpoints. If opportunities
      for active learning are not present in the classroom, digital technologies
      may automate processes, but are unlikely to improve learning.
    </p>
