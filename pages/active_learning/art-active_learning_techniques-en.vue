    <h2>Active Learning Techniques</h2>
    <p>
      There are a multitude of active learning techniques – below are a few
      that you can use to keep learners active and engaged.
    </p>
    <p>Click on each of the techniques for a quick description.</p>
    <details>
      <summary><h4>3-2-1 format</h4></summary>
      <p>
        Have the learners write down 3 ideas/issues/reactions to the material,
        2 examples/uses/applications and 1 unresolved area/question then in pairs or small groups have them share what they wrote.
      </p>
    </details>
    <details>
      <summary><h4>Background knowledge probe</h4></summary>
      <p>
        Asks for basic, simple responses (short answer, true/false, multiple choices) from learners
        who are about to begin a session or study a new concept. E.g. During an introductory music theory
        course, ask the learners how to form a minor third chord.
      </p>
    </details>
    <details>
      <summary><h4>Celebrations</h4></summary>
      <p>
        A closing activity that end the training on a positive note, often with a burst of
        energy or enthusiasm. It should be the last thing they do before they leave the room.
      </p>
    </details>
    <details>
      <summary><h4>Connections</h4></summary>
      <p>
        An opening activity that helps learners focus on what they know or want to know while
        introducing themselves to other learners. The difference between this and an ice-breaker
        is the focus on the content – this is not just a social meeting exercise.
      </p>
    </details>
    <details>
      <summary><h4>Fishbowl</h4></summary>
      <p>
        A group of learners is selected to discuss a topic while the remaining learners observe,
        take notes, and later reflect upon the fishbowl discussion. In an open fishbowl, observers
        can become participants, swapping places with those within the fishbowl. In a closed fishbowl,
        observers can’t change their roles.
      </p>
    </details>
    <details>
      <summary><h4>Flipped classroom</h4></summary>
      <p>
        The flipped classroom is a pedagogical model in which the typical lecture and homework
        elements of a course are reversed. Short video lectures are viewed by learners at home before
        the class session, while in-class time is devoted to exercises, projects, or discussions.
      </p>
    </details>
    <details>
      <summary><h4>Focused listing</h4></summary>
      <p>
        Learners create a list of what they know about a particular subject/concept
        and share the lists with the class. This could be used to determine what learners
        already know about a concept.
      </p>
    </details>
    <details>
      <summary><h4>Focused reading</h4></summary>
      <p>
        Present the learners with a series of questions that they need to answer while
        reading some material. This gets the learners to focus on the important material,
        rather than trying to figure out what’s truly significant.
      </p>
    </details>
    <details>
      <summary><h4>Jigsaw teamwork</h4></summary>
      <p>
        In a larger project, divide up aspects of the project to each team member –
        it’s is their responsibility to become the expert in that area of the project.
        Project members then gather and each member presents what they’ve learned to create
        a collective understanding of all the material.
      </p>
    </details>
    <details>
      <summary><h4>Learning partners</h4></summary>
      <p>
        Ask learners to complete a task or discuss a key issue with the person sitting next to them.
        Use this technique when you want everyone to participate, but time is running short or facilities
        don’t allow for small group discussions.
      </p>
    </details>
    <details>
      <summary><h4>Mark-ups</h4></summary>
      <p>An activity in which you instruct learners to mark their written material in a certain way so that they will retain more of the information for a longer period of time. Provide the learners with different symbols or mark-up methods (highlight colours).</p>
    </details>
    <details>
      <summary><h4>One minute paper</h4></summary>
      <p>
        The learners are asked to spend 1 minute writing down the key ideas
        from the material that was just presented them. This is a good way to
        break up a traditional lecture format.
      </p>
    </details>
    <details>
      <summary><h4>Pass a problem</h4></summary>
      <p>
        Ask learners a complex question that requires higher order thinking.
        Groups get 10 minutes to think about the problem and write a paragraph
        about the problem. Pass the paragraph to the next group, who then gets
        5 minutes to re-think the question and write again. Once again this is
        put in a file and given to the next group along with the original
        entry. Repeat.
      </p>
    </details>
    <details>
      <summary><h4>Think-Pair-Share</h4></summary>
      <p>
        Learners are asked to think about some aspect of the material, then
        to pair up and share their thoughts with their partner. This can be
        varied by having the learners reflect on their notes, then pairing up
        with another student to compare notes – updating their own with any
        information that may have been missed.
      </p>
    </details>
    <details>
      <summary><h4>Tickets out/passports</h4></summary>
      <p>
        Post activities that needs to be completed and handed in during
        class, typically something relatively small. The idea is that the
        learner cannot leave until it’s completed. E.g. write down at least two
        ideas/concepts that you were intrigued by, or perhaps concepts that
        were particularly difficult and not adequately covered. As an
        instructor you can use this feedback to modify, your course
        inflight.
      </p>
    </details>
    <details>
      <summary><h4>Time sponges</h4></summary>
      <p>
        A short-topic related activity that soaks up time that would normally be spent on non-training things.
        These times would include: the few minutes before class starts, the few minutes after breaks or lunch,
        or perhaps when learners complete an activity in less than the allotted time.
      </p>
    </details>
    <details>
      <summary><h4>Signals</h4></summary>
      <p>
        Have the learners use some mechanism to signal their responses to a question or statement.
        Such things as noise makers, index cards, hand signals, standing-up, etc. can be used.
      </p>
    </details>
  </div>
