    <h3>Role change/facilitation considerations</h3>
    <ul>
      <li>
        To manage learners that are disengaged or otherwise engaged in
        distracting behaviour, move toward and stand near the offender – this
        will often discourage them from continuing the unwanted behaviour.
      </li>
      <li>
        Be a facilitator assisting/focusing/directing the learners rather
        than lecturing to them.
      </li>
      <li>Be prepared to relinquish <strong>some</strong> control.</li>
    </ul>
