<template>
  <div id="articleContent" class="ajaxed-in">
    <h2>Techniques d’apprentissage actif</h2>
    <p>
      Il existe une multitude de techniques d’apprentissage actif – vous en
      trouverez quelques unes ci dessous que vous pourrez utiliser pour garder
      les apprenants actifs et engagés.
    </p>
    <p>
      Cliquez sur chacune des techniques pour obtenir une courte
      description.
    </p>
    <details>
      <summary><h4>Activités éponges</h4></summary>
      <p>
        Une courte activité liée au sujet étudié, qui prend le temps
        normalement consacré à des activités non liées à la formation. Ces
        périodes de temps comprennent : les quelques minutes qui précèdent le
        début du cours ou qui suivent la pause ou le dîner ou peut-être le
        temps qui reste lorsque les apprenants terminent une activité avant la
        fin du délai alloué.
      </p>
    </details>
    <details>
      <summary><h4>Aquarium</h4></summary>
      <p>
        Un groupe d’apprenants est choisi pour discuter d’un sujet, tandis
        que le reste d’entre eux les observent, prennent des notes et
        présentent ultérieurement leurs observations au sujet de cette
        discussion. Dans un processus ouvert, les observateurs peuvent prendre
        la place des participants, en changeant de place avec ceux qui se
        trouvent dans le « bocal ». Dans un processus fermé, les observateurs
        ne peuvent pas changer de rôle.
      </p>
    </details>
    <details>
      <summary><h4>Balisage</h4></summary>
      <p>
        Dans le cadre de cette activité, vous demandez aux apprenants
        d’intégrer certains repères à leurs documents écrits afin qu’ils
        gardent en mémoire plus d’information pendant plus longtemps. Indiquez
        aux apprenants les différents symboles ou méthodes de balisage qu’ils
        peuvent utiliser (p. ex. surligner en couleur).
      </p>
    </details>
    <details>
      <summary><h4>Billets de congé/ passeports</h4></summary>
      <p>
        Activités subséquentes qui doivent être effectuées et remises au
        formateur pendant le cours; ces activités nécessitent généralement très
        peu de temps. L’idée est que les apprenants ne peuvent pas quitter les
        lieux tant qu’ils n’ont pas terminé. Ils doivent écrire, par exemple,
        au moins deux idées/concepts qui ont piqué leur curiosité ou peut-être
        des concepts qu’ils ont trouvés particulièrement difficiles et qui
        n’ont pas été abordés convenablement. En tant que formateur, vous
        pouvez utiliser cette rétroaction pour apporter des modifications à
        votre formation en cours de route.
      </p>
    </details>
    <details>
      <summary><h4>Casse-tête</h4></summary>
      <p>
        Dans un projet d’envergure, répartissez les différents aspects du
        projet entre chacun des membres de l’équipe – chacun aura la
        responsabilité de devenir expert de l’aspect du projet qui lui aura été
        assigné. Les participants se réunissent ensuite et chacun d’entre eux
        présente ce qu’il a appris afin de favoriser une compréhension commune
        de toute la matière étudiée.
      </p>
    </details>
    <details>
      <summary><h4>Célébrations</h4></summary>
      <p>
        Cette activité de conclusion permet de mettre fin à la formation sur
        une note positive, souvent en présence de participants débordant
        d’enthousiasme et d’énergie. Cette activité doit être la dernière chose
        qu’ils font avant de quitter la classe.
      </p>
    </details>
    <details>
      <summary><h4>Classe inversée</h4></summary>
      <p>
        La classe inversée est un modèle pédagogique dans lequel les
        éléments qui composent habituellement les cours magistraux et les
        devoirs sont inversés. Les apprenants doivent regarder de courtes
        présentations vidéo, avant de se présenter en classe, tandis que le
        temps passé en classe sert à faire des exercices, à réaliser des
        projets ou à discuter.
      </p>
    </details>
    <details>
      <summary><h4>Connexions</h4></summary>
      <p>
        Cette activité de départ aide les apprenants à mettre l’accent sur
        ce qu’ils savent ou veulent savoir, tout en se présentant aux autres
        participants. Cette activité est différente d’une activité brise-glace,
        en ce sens qu’elle met l’accent sur le contenu – il ne s’agit pas
        seulement d’un exercice de réseautage social.
      </p>
    </details>
    <details>
      <summary><h4>Exploration des connaissances préalables</h4></summary>
      <p>
        Posez des questions qui demandent des réponses simples et
        élémentaires (réponses courtes, vrai ou faux, choix multiples) aux
        apprenants qui s’apprêtent à commencer une session ou à étudier un
        nouveau concept.
      </p>
    </details>
    <details>
      <summary><h4>Format 3-2-1</h4></summary>
      <p>
        Demandez aux apprenants de mettre par écrit trois
        idées/questions/réactions par rapport à la matière enseignée, deux
        exemples/utilisations/applications et un point/question non résolu;
        ensuite, en équipes de deux ou en petits groupes, demandez-leur de
        faire part aux autres de ce qu’ils ont écrit.
      </p>
    </details>
    <details>
      <summary><h4>Lecture ciblée</h4></summary>
      <p>
        Présentez aux apprenants une série de questions auxquelles ils
        devront répondre pendant la lecture de certains documents. Cette façon
        de faire aide les apprenants à se concentrer sur la matière importante,
        au lieu de chercher à trouver eux mêmes les éléments qui ont réellement
        de l’importance.
      </p>
    </details>
    <details>
      <summary><h4>Liste ciblée</h4></summary>
      <p>
        Les apprenants dressent une liste de ce qu’ils savent sur un
        sujet/concept particulier et échangent leur liste avec le reste de la
        classe. Cette technique peut être utilisée pour déterminer ce que les
        apprenants savent déjà au sujet d’un concept donné.
      </p>
    </details>
    <details>
      <summary><h4>Partenaires d’apprentissage</h4></summary>
      <p>
        Demandez aux apprenants d’effectuer les tâches ou de discuter d’une
        question clé avec la personne assise à côté d’eux. Utilisez cette
        technique lorsque vous voulez faire participer tout le monde, mais que
        vous n’avez pas le temps ou les installations nécessaires pour
        organiser des discussions en petits groupes.
      </p>
    </details>
    <details>
      <summary><h4>S’échanger un problème</h4></summary>
      <p>
        Posez aux apprenants une question complexe qui nécessite une
        réflexion de haut niveau. Les groupes ont 10 minutes pour réfléchir au
        problème et rédiger un paragraphe sur ce dernier. Donnez ce paragraphe
        au groupe suivant, qui lui a 5 minutes pour revoir la question et
        rédiger un nouveau paragraphe. Encore une fois, ce que les participants
        ont écrit est placé dans un dossier et donné au groupe suivant,
        accompagné de la première réponse rédigée. Répétez l’exercice.
      </p>
    </details>
    <details>
      <summary><h4>Signaux</h4></summary>
      <p>
        Demandez aux apprenants d’utiliser certains mécanismes pour signaler
        leur réponse à une question ou leur réaction à une déclaration. Des
        mécanismes tels que des générateurs de bruit, des fiches, des signes de
        la main, un changement de position (se mettre debout), etc., peuvent
        être utilisés.
      </p>
    </details>
    <details>
      <summary><h4>Texte éclair</h4></summary>
      <p>
        Les apprenants sont invités à prendre une minute pour écrire les
        principales idées qu’ils ont retenues de la matière qui vient de leur
        être présentée. Il s’agit là d’une bonne façon de briser le format
        traditionnel des cours magistraux.
      </p>
    </details>
    <details>
      <summary><h4>Un-Deux-Tous(Réflexion-échange)</h4></summary>
      <p>
        Les apprenants sont invités à réfléchir à certains aspects de la
        matière enseignée, puis à former une équipe de deux et à échanger leurs
        idées avec leur partenaire. Vous pouvez modifier cette technique en
        demandant aux apprenants d’examiner les notes qu’ils ont prises, puis
        de s’associer avec un autre participant pour qu’ils comparent leurs
        notes – ils pourront mettre leurs propres notes à jour en y ajoutant
        toute information qu’ils pourraient ne pas avoir saisie.
      </p>
    </details>
  </div>
</template>
