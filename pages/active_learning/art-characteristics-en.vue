    </p>
    <ul>
      <li>Engages learners beyond reading, listening, and note-taking</li>
      <li>Increases student interest, motivation and attendance</li>
      <li>Increases mastery and retention of course material</li>
      <li>
        Promotes deep learning, not just acquisition of facts; helps
        learners retain, apply and transfer what they learn
      </li>
      <li>
        Helps learners build competencies (e.g. problem solving, critical
        thinking, communication skills – 21st century skills) as well as
        content knowledge
      </li>
      <li>
        Develops higher order thinking skills (analysis, synthesis and
        evaluation – Bloom)
      </li>
      <li>Provides assistance and real time feedback to the learners</li>
      <li>Often involves interaction among learners</li>
      <li>Often provides greater access to lessons, content and material</li>
    </ul>
    <p>
      Learners learn how to learn - active learning alters the focus from
      instructional delivery on the part of the lecturer to active knowledge and
      skills acquisition on the part of the learner. It puts the onus on the
      learner to succeed.
