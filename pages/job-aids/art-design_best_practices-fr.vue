    <h2>Pratiques exemplaires en conception d'outils de travail</h2>
    <p>
      Un outil de travail bien conçu peut être plus complexe qu'on le penserait. La lisibilité de
      l'information incluse dans un outil de travail est essentielle, car elle peut influencer l'efficacité de
      celui-ci.
    </p>
    <h3>Prenez en compte les pratiques exemplaires suivantes&nbsp;:</h3>
    <dl class="dl-horizontal">
      <dt>De l'information simple et concise</dt>
      <dd>
        <ul>
          <li>
            Les instructions, les questions et les lignes directrices que contient un outil de travail doivent être
            essentielles, concises et simples.
          </li>
          <li>éviter les acronymes</li>
          <li>S'il y a lieu, consulter les lignes directrices du Secrétariat du Conseil du Trésor.</li>
        </ul>
      </dd>
      <dt>Un langage approprié</dt>
      <dd>
        <ul>
          <li>
            Privilégier des verbes d'action, le mode impératif et la deuxième personne, afin
            d'assurer la clarté et la concision.
          </li>
        </ul>
      </dd>
      <dt>De courts paragraphes et de courtes phrases</dt>
      <dd>
        <ul>
          <li>
            Un excès de verbosité nuit à la capacité de l'employé de réaliser
            rapidement la tâche ou la procédure.
          </li>
        </ul>
      </dd>
      <dt>Des éléments visuels clairs et simples</dt>
      <dd>
        <ul>
          <li>
            Utiliser des aides visuelles et des captures d'écran pour appuyer la présentation de
            l'information et favoriser sa compréhension par l'employé.
          </li>
        </ul>
      </dd>
      <dt>Graphiques, plans de contenu et tableaux</dt>
      <dd>
        <ul>
          <li>
            Il est plus facile à parcourir et à comprendre des textes volumineux lorsque le contenu est
            présenté de façon visuelle. envisager la possibilité de transformer l'information
            volumineuse en graphiques ou en tableaux, le cas échéant.
          </li>
        </ul>
      </dd>
      <dt>Variété de la typographie</dt>
      <dd>
        <ul>
          <li>
            L'utilisation de polices de caractères variées, par exemple gras et italique, peut aider à
            mettre en évidence de l'information importante. S'assurer d'utiliser les polices de manière
            uniforme.
          </li>
        </ul>
      </dd>
      <dt>Uniformité des polices</dt>
      <dd>
        <ul>
          <li>éviter l'utilisation de plus de deux polices dans un outil de travail.</li>
        </ul>
      </dd>
      <dt>Uniformité des palettes de couleurs</dt>
      <dd>
        <ul>
          <li>
            Utiliser des palettes de couleurs approuvées par l'école pour la conception et la couleur de
            polices de l'ensemble de l'outil de travail.
          </li>
        </ul>
      </dd>
      <dt>Clarté et simplicité</dt>
      <dd>
        <ul>
          <li>
            éviter de surcharger l'outil de travail en y ajoutant trop d'éléments visuels, trop de
            texte ou trop de couleurs.
          </li>
        </ul>
      </dd>
      <dt>Titre significatif</dt>
      <dd>
        <ul>
          <li>
            en lisant le titre de l'outil de travail, les employés doivent être en mesure de déterminer
            rapidement s'il répond à leurs besoins.
          </li>
        </ul>
      </dd>
      <dt>Date ou numéro de version</dt>
      <dd>
        <ul>
          <li>
            L'information que contient un outil de travail doit être valide et actualisée. L'ajout
            d'une date ou d'un numéro de version favorise le suivi.
          </li>
        </ul>
      </dd>
      <dt>Numéroter clairement chaque étape</dt>
      <dd>
        <ul>
          <li>
            De nombreux outils de travail contiennent des directives sur la façon de réaliser une tâche.
            S'assurer que ces directives sont organisées en étapes claires et structurées pour aider
            l'employé.
          </li>
        </ul>
      </dd>
      <dt>Accessibilité</dt>
      <dd>
        <ul>
          <li>
            Toujours considérer l'accessibilité de votre contenu lors de la conception d'outils de
            travail. Un format alternatif peut être requis.
          </li>
        </ul>
      </dd>
    </dl>
  </div>
  </div>
