        <h2>Donner un appui avant, pendant et après la tâche</h2>
        <p>Un outil de travail ne fait pas qu'aider un employé à réaliser une tâche. Il peut également aider à préparer une personne avant qu'elle n'assume la responsabilité d'une tâche, ou l'aider à modifier son rendement ou à en faire un suivi lorsque la tâche est terminée. On peut classer les différents types d'outils de travail en trois catégories : les planificateurs, les compagnons et les vérificateurs éclair.</p>
        <dl class="dl-horizontal">
          <dt>Les planificateurs</dt>
          <dd>nous aident à nous préparer en vue de réaliser une tâche. Ils nous permettent d'acquérir les connaissances requises au préalable, de recueillir de l'information essentielle, d'organiser le travail ou d'orienter notre réflexion. Parmi les exemples de planificateurs, il y a notamment les guides de référence, les fiches de conseils, les vidéos et les communautés de pratique.</dd>
          <dt>Les compagnons</dt>
          <dd>donnent un appui pendant la réalisation d'une tâche. Ils nous permettent de nous assurer que la tâche est effectuée correctement et de manière efficace, ou nous aident à résoudre tout problème ou toute question qui peut survenir. Parmi les exemples de compagnons, il y a notamment les éléments infographiques, les foires aux questions, les guides de dépannage, les arbres décisionnels et les centres d'assistance.</dd>
          <dt>Les vérificateurs éclair</dt>
          <dd>nous aident une fois que la tâche est terminée. Ils nous donnent l'assurance d'avoir inclus toutes les étapes et tous les éléments essentiels, et nous permettent de conserver certains renseignements pour la prochaine fois que nous aurons à réaliser la même tâche. Parmi les exemples de vérificateurs éclair, il y a notamment les listes de vérification, les évaluations ou sondages, les outils de rétroaction et les formulaires.</dd>
        </dl>
        <p>Au moment de concevoir des outils de travail, ne vous limitez pas à donner un appui aux employés pendant la réalisation d'une tâche. Pensez également à des occasions de leur donner un appui avant et après la tâche.</p>
