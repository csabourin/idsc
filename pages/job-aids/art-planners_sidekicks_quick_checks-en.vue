    <h2>Providing Support Before, During and After the Task</h2>
    <p>A job-aid not only serves to help an employee do a task. It can also help prepare someone before taking on a task or help them adjust or track their performance after a task. These different types of job-aids can be categorized as Planners, Sidekicks and Quick Checks. </p>
    <dl class="dl-horizontal">
      <dt>Planners</dt>
      <dd>help us prepare for a task. They help us gain needed prior  knowledge, gather essential information, organize work or guide our thinking.  examples of planners include reference guides, tip sheets, videos, communities  of practice, and more.</dd>
      <dt>Sidekicks</dt>
      <dd>provide  support while a task is being done. They help us ensure we do the task  efficiently and correctly or help resolve issues or questions that may arise.  examples of sidekicks include infographics, FAQs, troubleshooting guides,  decision trees, helpdesk support, and more.</dd>
      <dt>Quick Checks</dt>
      <dd>assist  us once a task has been completed by helping us ensure we haven't forgotten any  crucial steps and items or by ensuring we retain certain information for the  next time we perform the same task. examples of quick checks include  checklists, evaluations or surveys, feedback tools, forms, and more.</dd>
    </dl>
    <p>When designing job-aids, avoid only aiming  to support employees during a task and consider opportunities to support them  prior to or after a task.</p>
