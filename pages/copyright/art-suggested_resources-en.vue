    <h2>Suggested resources for copyright-free works or licenced works</h2>
    <p>
      There are a number of resources which can provide School employees with access to works that are free of rights or whose rights of use have already been granted or negotiated. These resources can be very useful as they provide quick access to a wide variety of works. However, to avoid any violation of the <em>Copyright Act</em>, it is important to respect the specific terms and conditions associated with them.
    </p>
    <p>
      This section provides guidance on how to uses works available through the following resources:
    </p>
    <details>
      <summary>
        <h4>Public Domain</h4>
      </summary>
      <p>Works that are <em>not</em> protected by copyright are said to be in the <em><a title="This link will open in a new window" href="copyright_glossary-en.html#public" target="_blank">public domain</a></em>. That means: </p>
      <ul>
        <li>the work can be used without restrictions on copying and adapting;</li>
        <li>permission is not required to use the work; and</li>
        <li>there are no legal requirement to attribute the work to its author.</li>
      </ul>
      <p>
        A work typically enters the public domain when its copyright term expires. Works can also be in the public domain because the work was not eligible for copyright protection in the first place or because the copyright owner has forfeited any copyright in the work to the public.
      </p>
      <p>
        Determining whether or not a work is in the public domain can be complicated, however, as the terms of copyright often differ depending on a work’s authorship, format, date of publication, and country of origin. To identify if a work produced in Canada is in the public domain, please refer to the <em><a title="This link will open in a new window" href="~/assets/3. Public_domain_decision_tree_en.pdf" target="_blank">Public Domain Decision Tree</a></em> available in the Tools section of this website.
      </p>
    </details>
