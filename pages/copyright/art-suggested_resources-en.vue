    <details>
      <summary>
        <h4>Government of Canada Copying Licence</h4>
      </summary>
      <p>
        In April 2016, the Government of Canada (Public Services and Procurement Canada) ratified an agreement with <em><a title="This link will open in a new window" href="http://www.accesscopyright.ca/" target="_blank">Access Copyright</a></em> and <em><a title="This link will open in a new window" href="https://www.copibec.ca/" target="_blank">Copibec</a></em>. These non profit organizations were established to administer the reproduction and communication rights in published works. Their rich repertoires provide access to works by thousands of Canadian and foreign authors, visual artists and publishers in both of Canada’s official languages.
      </p>
      <p>
        The <em><a title="This link will open in a new window" href="https://www.tpsgc-pwgsc.gc.ca/app-acq/communications-licence-eng.html" target="_blank">Government of Canada Copying Licence</a></em> gives federal government employees (including consultants, temporary employees, subcontractors and students) authorization to copy copyrighted protected materials licensed by these organizations.
      </p>
      <h5>Licence parameters</h5>
      <p>
        The <em>Government of Canada Copying Licence</em> permits federal institutions to make and distribute copies of published works in the <em><a title="This link will open in a new window" href="http://www.accesscopyright.ca/" target="_blank">Access Copyright</a></em> and <em><a title="This link will open in a new window" href="https://www.copibec.ca/" target="_blank">Copibec</a></em> repertoires for the non profit purposes of conducting business within their mandates and delivering government programs and services.
      </p>
      <h5>Permitted uses</h5>
      <p>
        The licence permits users the following activities:
      </p>
      <ol>
        <li>Making multiple copies.</li>
        <li>
          Physically or digitally reproducing a published work made by any of the following processes:
          <ol style="list-style: lower-latin;">
            <li>photocopying;</li>
            <li>printing a copy from a digital copy; </li>
            <li>scanning a published work to make digital copy;</li>
            <li>saving a digital copy to a USB drive or a computer hard drive;</li>
            <li>posting a digital copy to a federal government shared drive or Intranet site; and</li>
            <li>posting a digital copy to an Internet site accessible only via secure authentication, such as GCcampus.</li>
          </ol>
        </li>
        <li>
          Copying up to ten per cent (10 %) of a published work (generally speaking); with the following exceptions which allow for more:
          <ol style="list-style: lower-latin;">
            <li>an entire newspaper article or page;</li>
            <li>an entire article;</li>
            <li>an entry from an encyclopedia, annotated bibliography, dictionary, or similar reference work;</li>
            <li>an entire reproduction of an artistic work; and</li>
            <li>one entire chapter, provided it is no more than twenty per cent (20%) of a book.</li>
          </ol>
        </li>
        <li>Using a copy for the purpose of projection, display, or interaction with an image for presentations.</li>
      </ol>
      <h5>Limitations</h5>
      <p>
        The licence parameters do not allow for:
      </p>
      <ol>
        <li>Intentional cumulative copying from the same published work beyond the limits set out.</li>
        <li>Making copies that are not faithful and accurate reproductions of the original published work without the express permission of the author. <span class="underlined"><strong>This includes any adaptations, modifications or translations.</strong></span></li>
      </ol>
      <h5>Attribution to the author</h5>
      <p>
        Users must, where reasonable under the circumstances, mention on at least one page:
      </p>
      <ol>
        <li>A credit to the author (including writer, artist, illustrator and photographer) and to the source.</li>
        <li>A notice stating, “Copied under licence from Access Copyright and Copibec. Further reproduction or distribution is prohibited, except otherwise permitted by law.”</li>
      </ol>
      <p>
        For more information, please refer to the full agreement: <a title="This link will open in a new window" href="https://www.tpsgc-pwgsc.gc.ca/app-acq/communications-licence-eng.html" target="_blank">https://www.tpsgc-pwgsc.gc.ca/app-acq/communications-licence-eng.html</a>
      </p>
