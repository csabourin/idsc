        <h4>Open Access</h4>
      </summary>
      <p>
        Open Access materials generally allow individuals to view, download and copy the material freely for any lawful purpose. The only constraint on reproduction and distribution, and the only role for copyright in this domain, is to give authors control over the integrity of their work and the right to be properly acknowledged and cited.
      </p>
