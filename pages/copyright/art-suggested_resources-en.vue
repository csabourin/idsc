    <details>
      <summary>
        <h4>Creative Commons Material</h4>
      </summary>
      <p>
        Creative Commons is a non-profit organization dedicated to making it easier for individuals to share and build on the work of others. Just as with Open Access, the Creative Commons status does <span class="underlined">NOT</span> make the material free of rights. It provides a number of standardized licences for copyrighted content that clearly specify what type of use the creator grants to users and what rights they wish to reserve.
      </p>
      <p>
        Creative Commons material can be used for in-class courses without requesting the written permission of the author, however users must respect the licence conditions chosen by the author. These conditions may, <em>or may not</em>, allow modifications (including translation) of the material. There are 6 types of licences which exist. To learn how to interpret these licences, please consult the <em><a title="This link will open in a new window" href="tools-en.html" target="_blank">How to interpret a Creative Commons licence</a></em> infographic available in the Tool Section of this website.
      </p>
      <p>
        Creative commons materials can be found in multiple ways. A good place to start is with the search portal on the <a title="This link will open in a new window" href="https://search.creativecommons.org/" target="_blank">Creative Commons website</a>, which allows users to return very specific results from a number of websites which host Creative Commons material. Websites, like <a title="This link will open in a new window" href="https://www.google.ca/advanced_search?q=gj&hl=en" target="_blank">Google</a> or YouTube, also offer advanced search options which provide customizable searches for Creative Commons material. Note that these search tools are based on a search term, the type of licence the material is available under, and/or based on the type of use to be made of the material.
      </p>
