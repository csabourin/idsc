          <tr id="author">
            <td>Author</td>
            <td>
              The creator of an artistic, literary, musical, or dramatic work. The author can be an individual, a group of individuals, a company, an organization, or government, etc. There can be several authors for a work.
            </td>
            <td>
              Robert is a graphic designer with the School’s Communication’s team and has created a poster for a new workplace initiative. In this case, Robert is the author of the poster, and the poster is an artistic work for which the copyright belongs to the Crown as he is a public servant.
            </td>
          </tr>
          <tr id="access">
            <td>Access Copyright and Copibec</td>
            <td>
              <p>
                Non-profit national organizations that represent Canadian writers, visual artists and publishers, as well as a number of foreign creators’ and publishers’ works.
              </p>
              <p>
                They provide advance permission for copying materials from an ever-growing repertoire of published works including books, journals, newspapers and magazines, while rewarding the creators and publishers of that material.
              </p>
              <p>
                PSPC has an agreement with Access Copyright and Copibec that allows employees of the Government of Canada to use copyrighted material, covered by the licence, within the limits set out in the licence, without fear of liability for copyright infringement.
              </p>
            </td>
            <td>
              Nathalie is a course designer who would like to use a textbook excerpt as part of her course material. She consulted the Access Copyright’s <em>Title Search and Permission Tool</em> to see if the title she wishes to use is there. Since it is, she uses the material within the parameters permitted by the <em>Government of Canada Copying Licence</em>.
            </td>
          </tr>
          <tr id="copyright">
            <td>Copyright</td>
            <td>
              <p>
                The sole right to produce, translate or reproduce a work, or a substantial part of it, in any form. It includes the right to perform the work or any substantial part of it or, in the case of a lecture, to deliver it.
              </p>
              <p>
                If the work is unpublished, copyright includes the right to publish the work or any substantial part of it.
              </p>
            </td>
            <td>
              All learning products at the School, including videos, job aids, infographics and online courses, are automatically protected as copyright material. This copyright applies regardless of whether or not there is a copyright symbol. However, since some of the School’s course content could be made available on public-facing platforms (i.e. Open Government Portal, GCcollab), a best practice is to systematically include the &copy;-symbol.
            </td>
          </tr>
          <tr id="act">
            <td>Copyright Act</td>
            <td>
              <p>
                The federal legislation governing copyright in Canada. It grants certain exclusive rights to the copyright owner to:
              </p>
              <ul>
                <li>Produce or reproduce the work or any substantial part thereof in any material form </li>
                <li>Perform the work or any substantial part thereof in public </li>
                <li>If the work is unpublished, to publish the work or any substantial part thereof </li>
                <li>Translate the work </li>
                <li>Adapt as a cinematographic work </li>
                <li>Communicate the work to the public</li>
              </ul>
            </td>
            <td>
              Diane is a course designer who has obtained copyright permission to use a chapter from a published book, but upon closer inspection of the <em>Copyright Act</em>, she now realizes that she will have to obtain permission for a second time to translate the chapter.
            </td>
          </tr>
          <tr id="crown">
            <td>Crown copyright</td>
            <td>
              <p>
                Crown copyright is attributed to any work that has been created or published by or under the direction or control of the Government of Canada (including the School).
              </p>
              <p>
                As a federal organization, Crown copyright exists for much of the <em>original</em> work at the School including courses, reports, publications, databases, videos, blogs and computer applications.  The <em>Copyright Act</em> provides copyright protection for all works of government employees for a period of 50 years (<em>Copyright Act</em>, s.12.).
              </p>
              <p style="word-break: break-word; word-break: break-all; -webkit-hyphens: auto; -moz-hyphens: auto; -ms-hyphens: auto; hyphens: auto;">
                Crown copyright also covers the work of contractors under contract to the Government of Canada (including the School), <em>except</em> where the contract specifically gives copyright to the contractor (For more detail see: <em>Policy on</em> Title to Intellectual Property Arising under Crown Procurement Contracts at <a title="This link will open in a new window" href="https://www.ic.gc.ca/eic/site/068.nsf/eng/00005.html" target="_blank">https://www.ic.gc.ca/eic/site/068.nsf/eng/00005.html</a>).
              </p>
            </td>
            <td>
              <p>
                As a video producer for the School, Sam has just made some final edits on a video he produced for the Management Development Program. The video incorporates footage that was filmed in the studio, as well as a short animation clip produced by an outside consultant under contract to the School. In this case, both the final video and animation clip are works that belong to the Crown (Government of Canada) and are protected under Crown Copyright for 50 years.
              </p>
            </td>
          </tr>
          <tr id="infringement">
            <td>Copyright Infringement</td>
            <td>
              A violation of copyright through unauthorized copying or use of a work or other subject matter under copyright.
            </td>
            <td>
              Marie is an independent artist who created an infographic about leadership that ellis, a School employee, really liked. The image had the copyright symbol and stated that it was the exclusive property of Marie. Disregarding this warning, ellis uses this infographic in a blog she writes for the School, without acquiring permission from the original author. In this case, ellis copied an image from someone else’s work, effectively committing a copyright infringement.
            </td>
          </tr>
          <tr id="cc">
            <td>Creative Commons</td>
            <td>
              A non-profit organization that enables the sharing and use of creativity and knowledge through free legal tools.
            </td>
            <td>
              Paul came across a graphic image on a website that he really liked and wanted to use in a presentation to his team. He looked at the license and found that it was a Creative Commons licence.  Paul proceeded to read the Creative Commons licence to learn how he could legally use the visual aid.
            </td>
          </tr>
          <tr id="cc-license">
            <td>Creative Commons license</td>
            <td>
              <p>
                Creative Commons (CC) licenses work alongside copyright and enable the content creator to modify their copyright terms to best suit their needs. CC provides free, easy-to-use copyright licenses in a simple, standardized way to give the public permission to share and use creative work, based on conditions chosen by the content creator. CC licenses let the content creator easily change their copyright terms from the default of "all rights reserved" to "some rights reserved."
              </p>
              <p style="word-break: break-word; word-break: break-all; -webkit-hyphens: auto; -moz-hyphens: auto; -ms-hyphens: auto; hyphens: auto;">
                There are six possible variations of Creative Commons licenses. each of these licenses has specific rights and limitations. To know the meaning of the pictograms used, please consult the following website: <a title="This link will open in a new window" href="https://creativecommons.org/licenses/" target="_blank">https://creativecommons.org/licenses/</a>
              </p>
            </td>
            <td>
              Julie wants to use a video protected by a Creative Common license. Before using the video, she compares the license symbols with those found on the Creative Common website to determine what the limits are for using the video.
            </td>
          </tr>
          <tr id="locks">
            <td>Digital locks</td>
            <td>
              <p>
                A digital lock is a form of access control or use restriction that controls or restricts access to a work, including passwords that prevent access to a work.
              </p>
              <p>
                It is an offense under the <em>Copyright Act</em> to "circumvent" an access control or digital lock.
              </p>
            </td>
            <td>
              Jean placed a password protection (i.e. digital lock) on the School’s registration system folder to protect privileged documents contained in the folder from illicit copying, reproduction, or translation.
            </td>
          </tr>
          <tr id="exemption">
            <td>exemption for educational institutions</td>
            <td>
              According to the <em>Copyright Act</em>, an educational institution (or a person acting under its authority) does not need to seek copyright permission when reproducing or translating materials for education or training purposes.
            </td>
            <td>
              Shortly after joining the School on secondment from the University of Ottawa, François translated an article he wanted to use in the course he was teaching at the School. He was subsequently informed that unlike the University of Ottawa, the School does have an exemption for education institutions due to its federal jurisdiction. Therefore, the School cannot avail itself of this exemption and course developers or faculty members wanting to use copyrighted material must first obtain copyright permission/licence from the author.
            </td>
          </tr>
          <tr id="fair">
            <td>Fair dealing</td>
            <td>
              <p>
                The fair dealing clauses of the <em>Copyright Act</em> allow users to engage in certain activities relating to research, private study, education, parody, satire, criticism, review, or news reporting without needing copyright permission.
              </p>
              <p>
                With respect to criticism, review, and news reporting, the user must mention the source of the material, along with the name of the author, performer, maker, or broadcaster for the dealing to be fair.
              </p>
              <p>
                The only way to evaluate if a dealing is fair, is to evaluate the following:
              </p>
              <ul>
                <li>The purpose, character, and amount of the dealing</li>
                <li>The nature of the work </li>
                <li>The alternatives to the dealing</li>
                <li>effects of the dealing on the work</li>
              </ul>
            </td>
            <td>
              Annie is doing personal research on leading practices in adult learning. To inform her research and to substantiate the conclusions she’s making, she consults and draws information from numerous industry reports. To avoid copyright violations, Annie cites the sources as well as the names of the corresponding authors.  In doing so, Annie respects the fair dealing clauses of the <em>Copyright Act</em> relating to research.
            </td>
          </tr>
          <tr id="ip">
            <td>Intellectual property</td>
            <td>
              A broad form of creative endeavor that can be protected through a trademark, patent, copyright, or industrial design.
            </td>
            <td>
              The Crown owns copyright on all learning products, which are part of the School’s overall intellectual property holdings
            </td>
          </tr>
          <tr id="license">
            <td>License</td>
            <td>
              <p>
                A legal agreement granting someone permission to use a work for certain purposes or under certain conditions.
              </p>
              <p>
                A license does not produce a change in ownership of the copyright.
              </p>
            </td>
            <td>
              As a course designer, Marcel wants to include a photo, produced by Company X in one of his videos. As such, he would negotiate and develop a “License Agreement” with Company X. Once the agreement is in place and the material is integrated into the video, the School would respect all the legal terms of the agreement as expected by the copyright owner (Company X).
            </td>
          </tr>
          <tr id="moral">
            <td>Moral Rights</td>
            <td>
              Rights an author retains over the integrity of a work and the right to be named as its author even after sale or transfer of the copyright. Moral rights are distinguished from economic rights since the former are derived from the reflection of the author’s personality in his or her work, whereas the latter grants an author the ability to benefit economically from their work.
            </td>
            <td>
              Through Publications Canada, Canadians have access to a variety of language tools developed by the School. By making those tools available to the public, the School chooses to not enforce its copyright for the benefit of Canadians. However, the School keeps the moral rights to the content even though the information may be used freely by others.
            </td>
          </tr>
          <tr id="public">
            <td>Public domain</td>
            <td>
              <p>
                Works that are <em>not</em> protected by copyright. A work typically enters the public domain when its term of copyright expires.
              </p>
              <p>
                In Canada, the copyright for a work usually expires 50 years after the death of the creator. After that delay the works fall into the public domain.
              </p>
            </td>
            <td>
              Helen is designing a course and would like to include a short excerpt from a Mordecai Richler novel. She cannot do so without asking permission, because Mordecai Richler died in 2001, so his novels will remain copyrighted until 2051, (50 years after his death).
            </td>
          </tr>
          <tr id="royalty">
            <td>Royalty</td>
            <td>
              The amount paid to copyright owners for the sale or use of their works.
            </td>
            <td>
              <p>
                The School receives yearly royalties from McGill Queen’s University Press for three books: Governance in a Changing environment, Protecting Canadian Democracy, and Governance in the Twenty-first Century. The School receives royalties on these books because they were co-authored and/or written on behalf of the School.
              </p>
              <p>
                On the other hand, the School pays royalties to authors, when required, for the use of their copyrighted material in its courses
              </p>
            </td>
          </tr>
          <tr id="royalty-free">
            <td>Royalty free</td>
            <td>
              Copyright material or intellectual property that is provided without the need for payment of royalties or licensing fees.
            </td>
            <td>
              Michelle wants to use a particular chart in the classroom material she’s preparing. She asks permission from the author, who has granted it without asking for monetary compensation in return. In this case, the material used is considered royalty free.
            </td>
          </tr>
          <tr id="trademark">
            <td>Trademark</td>
            <td>
              Trademarks may be one or a combination of words, sounds or designs used to distinguish the goods or services of one person or organization from those of others in the marketplace. Trademarks are a form of intellectual property.
            </td>
            <td>
              Like other federal departments, the School is <em>not</em> permitted to have its own trademark as stipulated by the Treasury Board <em><a title="This link will open in a new window" href="https://www.tbs-sct.gc.ca/pol/doc-fra.aspx?id=30683" target="_blank">Policy on Communication and Branding</a></em>.
            </td>
          </tr>
