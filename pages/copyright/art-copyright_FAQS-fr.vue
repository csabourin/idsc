              <h5><em>est-il possible de concevoir un cours entier à partir d’une théorie ou d’un modèle spécifique, si l’on mentionne la source et la référence? </em></h5>
            </summary>
            <p>
              Étant donné que ces modèles et théories sont protégées par le droit d’auteur et que les titulaires des droits gagnent leur vie en offrant de la formation et d’autres services liées à ceux-ci, il est peu probable que vous puissiez utiliser ces documents protégés pour concevoir un cours entier sans obtenir le consentement des titulaires en question, et ce même si le contenu est accessible au public sur Internet. Sans le consentement de l’auteur, leur utilisation est donc considérée comme une violation du droit d’auteur.
            </p>
