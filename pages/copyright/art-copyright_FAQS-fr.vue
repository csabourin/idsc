          <details>
            <summary>
              <h5><em>Qu’est-ce qui est considéré comme un usage personnel de contenu en ligne?</em></h5>
            </summary>
            <p>
              L’usage personnel signifie que le titulaire des droits d’auteur permet une utilisation libre de son travail, uniquement à des fins non-commerciales ou sans but lucratif.
            </p>
            <p>
              Par exemple, si les conditions d’utilisation d’un site Web précisent que vous pouvez utiliser son contenu pour usage personnel seulement, cela signifie que vous pouvez vous en faire des copies pour vous, mais que vous ne pouvez pas les distribuer (ce qui inclue la diffusion en salle de classe) ou les vendre.
            </p>
          </details>
