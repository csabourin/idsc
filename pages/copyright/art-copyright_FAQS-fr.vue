            <summary>
              <h5><em>Comment obtenir une licence pour utiliser une œuvre d’un titulaire de droit d’auteur introuvable? </em></h5>
            </summary>
            <p>
              Au Canada, la <a href="http://www.cb-cda.gc.ca/home-accueil-f.html" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Commission du droit d’auteur du Canada</a> est un organisme de réglementation économique investi du pouvoir, notamment, de délivrer des <a href="copyright_glossary-fr.html#license" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">licences</a> pour l’utilisation de l’œuvre lorsque le titulaire du droit d’auteur est introuvable. elle vous accordera une licence à condition que vous ayez déployé tous les efforts raisonnables pour trouver le titulaire du droit d’auteur. Vous devez donc faire une recherche approfondie avant de la contacter. Pour commencer votre recherche, vous pouvez consulter la <a href="https://www.ic.gc.ca/app/opic-cipo/cpyrghts/dsplySrch.do?lang=fra" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">base de données sur le droit d'auteur canadien</a> de l'Office de la propriété intellectuelle du Canada
            </p>
            <p>
              Les licences délivrées par la Commission ne sont valides qu’au Canada. À l’extérieur du Canada, l’utilisateur est assujetti aux lois de chaque pays dans lequel il utilisera l’œuvre, même si le titulaire du droit d’auteur est canadien.
