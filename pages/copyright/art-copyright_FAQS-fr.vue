          <details>
            <summary>
              <h5><em>Qu’est-ce que le droit d’auteur et en quoi est-il différent de la propriété intellectuelle? </em></h5>
            </summary>
            <p>
              <img alt="Propriété intellectuelle" class="pull-right" src="~/assets/Copyright_FAQ-fr.png" width="400">
              On confond souvent les notions de <span class="nowrap">« d</span>roit d’auteu<span class="nowrap">r »</span> et de <span class="nowrap">« p</span>ropriété intellectuell<span class="nowrap">e »</span>.
            </p>
            <p>
              Le droit d’auteur est le droit exclusif de produire ou de reproduire une œuvre ou une partie importante de cette œuvre sous une forme quelconque. Autrement dit, le droit d’auteur protège les créations originales artistiques, dramatiques, musicales et littéraires, afin qu’elles ne soient pas copiées, produites ou diffusées sans la permission de leur créateur. Au Canada, toutes les œuvres originales, y compris les livres, les articles de journaux, les enregistrements vidéo, les sites Web et les logiciels, sont protégées par le droit d’auteur.
            </p>
            <p>
              La propriété intellectuelle (PI), quant à elle, est un terme plus large qui renvoie aux droits juridiques concernant les idées, inventions et créations dans les domaines de l’industrie, des sciences, de la littérature et des arts. La PI touche également les symboles, noms, images, dessins et modèles utilisés dans les affaires, les brevets, marques de commerce, dessins industriels et plus, qui bénéficient tous d’une protection conférée par le régime de la PI du Canada.
            </p>
            <p>
              en d’autres mots, le droit d’auteur renvoie aux droits exclusifs d’un auteur sur son œuvre et la propriété intellectuelle est un terme plus large qui comprend le droit d’auteur et d’autres types de droits.
            </p>
          </details>
          <details>
            <summary>
              <h5><em>Quels sont les droits légaux protégés par le droit d'auteur?</em></h5>
            </summary>
            <p>
              La loi confère au titulaire du droit d’auteur des droits économiques et moraux. Le droit économique offre au titulaire des droits d’auteur le droit d’obtenir un paiement pour l’utilisation de son œuvre, alors que le droit moral permet au titulaire de protéger la façon dont elle est utilisée.
            </p>
          </details>
          <details>
            <summary>
              <h5><em>Pourquoi le droit d’auteur est-il important pour l’École?</em></h5>
            </summary>
            <p>
              L’École est à la fois un <em>créateur/producteur</em> et un <em>utilisateur</em> d’œuvres protégées par le droit d’auteur.
            </p>
            <p>
              Il est important de noter que les droits d’auteur restreignent l’utilisation <em>sans autorisation d</em>es œuvres intellectuelles, universitaires et littéraires comme des livres, articles, discours, essais, tableaux, cadres de travail, illustrations et traductions. Pour les employés de l’École utilisant des œuvres protégées par le droit d’auteur pour la création de vidéos, de cours, de modules de formation, d’outils de travail, d’évènements, de produits de communication et autres il est essentiel de comprendre ce qui est permis lorsque vient le moment d’utiliser les œuvres d’autrui ou de s’en inspirer.
            </p>
            <p>
              Sans le consentement du titulaire du droit d’auteur, la reproduction, la modification ou <span class="underlined">la traduction</span> du travail d’autrui constituent une violation. Les droits d’auteur donnent à l’auteur le droit de déterminer si son œuvre peut être utilisée par d’autres et, si c’est le cas, comment elle sera utilisée. Les employés de l’École qui utilisent des documents protégés par le droit d’auteur en salle de classe ou lors de la création de produits d’apprentissage sont encouragés à se familiariser avec la <em><a href="http://laws-lois.justice.gc.ca/fra/lois/c-42/" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Loi sur le droit d’auteur</a></em>.
            </p>
          </details>
          <details>
            <summary>
              <h5><em>Qui peut détenir des droits d’auteur?</em></h5>
            </summary>
            <p>
              en règle générale, c’est l’auteur de l’œuvre qui est le titulaire du droit d’auteur. Cependant, dans certains cas, il est possible que l’œuvre ait été produite dans le cadre de conditions de travail spécifiques. Dans la fonction publique fédérale, par exemple, le droit d’auteur appartient à l’employeur, c’est-à-dire la <a href="copyright_glossary-fr.html#crown" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Couronne</a>. La Couronne détient les droits d'auteurs lorsque lœuvre est préparée dans le cadre des tâches d’un employé, mais elle pourrait ne pas être la détentrice des droits si l’œuvre est préparée par un entrepreneur (pour de plus amples renseignements, communiquez avec <a href="mailto:csps.procurementapprovisionnements.efpc@canada.ca" title="Ce lien ouvrira une fenêtre de votre application de messagerie">l’équipe d’approvisionnement de l’École</a>).
            </p>
          </details>
          <details>
            <summary>
              <h5><em>Combien de temps dure le droit d’auteur au Canada?</em></h5>
            </summary>
            <p>
              Au Canada, les droits d’auteur demeurent valides pendant la durée de la vie du créateur, <em>plus 50 ans</em>.
            </p>
            <p>
              La durée des droits d’auteur varie toutefois selon le pays. Aux États Unis et au Royaume Uni par exemple, les droits d’auteur s’appliquent pendant la durée de la vie du créateur, <em>plus 70 ans</em>.
            </p>
          </details>
          <details>
            <summary>
              <h5><em>Qu’est-ce que le domaine public? </em></h5>
            </summary>
            <p>
              Le domaine public porte sur le contenu qui peut être librement copié, partagé, modifié et publié. Le contenu du domaine public inclut les œuvres dont le droit d’auteur a expiré, les œuvres inéligibles au droit d’auteur et les œuvres dont le créateur a renoncé à ses droits. Aucune permission n’est nécessaire pour copier ou utiliser les œuvres du domaine public.
            </p>
            <p>
              Veuillez noter que la majorité du contenu trouvé sur Internet n’est PAS du domaine public, même s’il est visible ou accessible publiquement. Les termes « du domaine public » et « accessible au public » ne sont pas interchangeables. Il revient à l’utilisateur de vérifier si une œuvre fait partie du domaine public avant de l’utiliser. Afin de déterminer si une œuvre produite au Canada fait partie du domaine public, veuillez utiliser le document <em><a href="~/assets/3. Public_domain_decision_tree_fr.pdf" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Arbre de décision : Domaine public</a></em> pouvant être consulté dans la section <span class="nowrap">« O</span>util<span class="nowrap">s »</span> de ce site Web.
            </p>
