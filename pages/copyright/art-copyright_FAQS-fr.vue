            <summary>
              <h5><em>Qu’est-ce que la violation des droits d’auteur?</em></h5>
            </summary>
            <p>
              La violation est le terme utilisé dans la <em><a href="http://laws-lois.justice.gc.ca/fra/lois/c-42/" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Loi sur le droit d’auteur</a></em> pour nommer une violation ou une infraction à la <em>Loi</em>. en termes simples, la violation du droit d’auteur correspond à l’utilisation d’œuvres protégées d’une façon interdite par la <em><a href="http://laws-lois.justice.gc.ca/fra/lois/c-42/" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Loi sur le droit d’auteur</a></em>.
            </p>
            <p>
              en utilisant une œuvre protégée d’une façon qui est strictement réservée au titulaire du droit d’auteur, on empêche ce dernier de l’utiliser pour en récolter des bénéfices financiers. L’utilisation non autorisée prive aussi le propriétaire de l’œuvre de ses <a href="copyright_glossary-fr.html#moral" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">droits moraux</a> (intégrité de l’œuvre) et pourrait constituer une violation du droit d’auteur. Cette utilisation peut donner lieu à des poursuites.
            </p>
            <p>
              Voici des exemples de violation de droits d’auteur :
            </p>
            <ul>
              <li>Photocopier des parties d’un livre</li>
              <li>Insérer dans du matériel didactique une bibliographie provenant d’un autre ouvrage (livre, revue spécialisée, article, etc.)</li>
              <li>Reproduire des tableaux ou des graphiques tirés d’une œuvre publiée</li>
              <li><strong>Traduire</strong> un texte tiré des travaux d’une autre personne</li>
              <li>Copier des didacticiels</li>
              <li>Ne pas respecter les conditions d’utilisation d’un site Web en montrant en classe du contenu ou une vidéo en ligne</li>
            </ul>
            <p>Voici des exemples de violation de droits moraux :</p>
            <ul>
              <li>Utiliser un texte ou une partie d’un texte hors contexte ou sans mentionner la source</li>
              <li>Reproduire une bande dessinée</li>
              <li>Utiliser un questionnaire de sondage à des fins autres que celles pour lesquelles il a été conçu</li>
              <li>Transcrire une entrevue télévisée ou enregistrée pour l’inclure dans le matériel d’un cours</li>
              <li>Réenregistrer des documents audio</li>
              <li>Modifier ou adapter un cours sans autorisation</li>
            </ul>
          </details>
          <details>
            <summary>
              <h5><em>Quels sont les risques éventuels encourus s'il y a violation du droit d’auteur? </em></h5>
            </summary>
            <p>
              Toute allégation de violation du droit d’auteur, réelle ou apparente, peut représenter un réel risque pour vous et pour l’École et pourrait :
            </p>
            <ul>
              <li>nuire à votre crédibilité professionnelle;</li>
              <li>nuire à la réputation de l’École;</li>
              <li>avoir des répercussions financières sur l’École si des dommages pécuniaires doivent être payés au titulaire du droit d’auteur;</li>
              <li>occasionner des poursuites contre l’École.</li>
            </ul>
          </details>
          <details>
            <summary>
              <h5><em>Qu’entend-on par « utilisation équitable »? </em></h5>
            </summary>
            <p>
              L’utilisation équitable figure depuis longtemps dans la Loi sur le droit d’auteur du Canada et permet l’utilisation de documents protégés à des fins qui ne menacent pas nécessairement les intérêts des titulaires de droits d’auteur et qui pourraient avoir des avantages sociaux notables – mais seulement si l’utilisation est équitable.
            </p>
            <p>
              Les dispositions de la <em><a href="http://laws-lois.justice.gc.ca/fra/lois/c-42/" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Loi sur le droit d’auteur</a></em> qui concernent l’utilisation équitable permettent aux utilisateurs de participer à certaines activités de recherche, d’étude privée, d’éducation, de parodie, de satire, de critique, de compte rendu ou de communication de nouvelles. en ce qui concerne la critique, le compte rendu et la communication de nouvelles, pour que l’utilisation soit équitable, l’utilisateur doit mentionner la source de l’œuvre ainsi que le nom de l’auteur, de l’artiste-interprète, du producteur ou du diffuseur.
            </p>
          </details>
          <details>
            <summary>
              <h5><em>L'École peut-elle se prévaloir de la clause d'utilisation équitable de la Loi sur le droit d'auteur à des fins éducatives? </em></h5>
            </summary>
            <p>
              Selon la <em><a href="http://laws-lois.justice.gc.ca/fra/lois/c-42/" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Loi sur le droit d’auteur</a></em>, l’École n’est <strong>PAS</strong> un établissement d’enseignement et ne peut se prévaloir des dispositions en matière d’enseignement.
            </p>
          </details>
          <details>
            <summary>
              <h5><em>Comment peut-on éviter d’enfreindre la Loi sur le droit d’auteur?</em></h5>
            </summary>
            <dl>
              <dt>Reformuler</dt>
              <dd>Les idées, concepts et théories ne sont pas protégés par le droit d’auteur; seule la forme dans laquelle ils sont exprimés l’est (langue, mots, représentation graphique, etc.).</dd>
              <dt>Synthèse</dt>
              <dd>Un effort créatif indépendant, même modeste, est suffisant pour rendre une œuvre originale.</dd>
              <dt>Utiliser plusieurs sources</dt>
              <dd>en vous appuyant sur plusieurs auteurs, vous réduisez la proportion d’« inspirations » tirées d’une seule œuvre.</dd>
              <dt>Utiliser des citations brèves</dt>
              <dd>La source (incluant l’auteur, le titre, l’éditeur, l’année de publication) doit toujours accompagner une citation reprise textuellement.</dd>
              <dt>Acheter</dt>
              <dd>Il est possible de négocier des prix réduits si de grandes quantités d’une œuvre sont achetées.</dd>
              <dd>Vous pouvez demander l’autorisation par téléphone, courriel, lettre, entre autres, mais il est toujours préférable d’obtenir une autorisation écrite.</dd>
              <dt>Demander une licence</dt>
              <dd>Une licence permet d’utiliser une œuvre protégée d’une manière spécifique, généralement en échange d’un montant forfaitaire ou de redevances versées au titulaire du droit d’auteur.</dd>
              <dt>Demander la cession des droits d’exploitation</dt>
              <dd>La cession de droits d’exploitation constitue un transfert de propriété par lequel le titulaire du droit d’auteur cède la propriété ou l’utilisation de son œuvre à une personne ou à une organisation.</dd>
            </dl>
