            <summary>
              <h5><em>Peut-on montrer aux apprenants une vidéo diffusée sur YouTube en salle de classe? </em></h5>
            </summary>
            <p>
              Il n’est habituellement PAS permis de montrer une vidéo YouTube dans les salles de classe. <a href="https://www.youtube.com/t/terms" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Les conditions d’utilisation de YouTube</a> n’offrent une permission que pour <em>usage personnel</em>; cela ne comprend donc pas le visionnement en salle de classe. De plus, les conditions d’utilisation rappellent aux utilisateurs que les vidéos sur YouTube sont protégées par les droits d’auteur; le consentement du titulaire est donc nécessaire pour en faire l’utilisation.
            </p>
            <p>
              Pour clarifier, les conditions d’utilisation de YouTube précisent :
            </p>
            <p /><blockquote>
              <em>« Vous pouvez accéder au Contenu pour vous informer et pour votre usage personnel comme prévu par la fonctionnalité fournie par le Service et comme autorisé par les Conditions d’utilisation. Il vous est interdit de télécharger du Contenu à moins qu’un lien de téléchargement ou un lien similaire ne soit fourni par YouTube sur le Service pour le Contenu en question. Il vous est interdit de copier, reproduire, distribuer, transmettre, <strong>diffuser</strong>, exposer, vendre, concéder sous licence ou exploiter de quelque autre façon que ce soit le Contenu à d’autres fins sans l’accord écrit préalable de YouTube ou des détenteurs de licence respectifs du Contenu. »</em>
            </blockquote>
            </p>
            <p>
              Toutefois, veuillez noter qu’il est possible de visionner en classe des vidéos se trouvant sur YouTube ou une autre plateforme de partage s'ils sont sous licence <a href="copyright_glossary-fr.html#cc" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Creative Commons</a>.
            </p>
            <p>
              Voici différentes options pour trouver des vidéos diffusées sous une licence de Creative Commons :
            </p>
            <ol>
              <li>
                en cliquant sur « PLUS » sous une vidéo sur YouTube pour voir le type de licence qui s’applique (voir la capture d’écran ci-dessous).
                <img src="~/assets/Copyright_FAQ-en_youtube.png" alt="Les options de Youtubes pour les licences" width="450">
                <ul>
                  <li>
                    YouTube comprend aussi une option qui permet d’afficher seulement les vidéos sous licence Creative Commons. Tapez votre demande et lancez la recherche. Dans <span class="nowrap">« F</span>iltre<span class="nowrap">s »</span>, au haut de l’écran de recherche, sélectionnez <span class="nowrap">« C</span>reative Common<span class="nowrap">s »</span>. On trouve également une liste de vidéos sous l’onglet <a href="https://www.youtube.com/user/creativecommons" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Creative Commons dans YouTube</a>, mais cette liste n’est pas exhaustive.
                  </li>
                </ul>
              </li>
              <li>Vimeo a dans son moteur de recherche une <a href="http://vimeo.com/creativecommons" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">section</a> dédiée aux vidéos sous licence dans Creative Commons. </li>
              <li>Le site de <a href="http://search.creativecommons.org/" target="_blank" title="Ce lien s'ouvrira dans une nouvelle fenêtre">Creative Commons</a> peut aussi vous aider à trouver des vidéos pouvant être diffusées publiquement. </li>
