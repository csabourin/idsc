    <h2>FAQ sur le droit d’auteur</h2>
    <p>Les enjeux entourant le droit d’auteur sont souvent très nuancés, complexes et propres à des circonstances particulières. Le présent document ne veut pas faire autorité, mais vise plutôt à fournir des renseignements généraux sur le droit d’auteur et ses répercussions sur le travail accompli quotidiennement à l’École. Pour des questions supplémentaires ou en cas de situation particulière, veuillez contacter la division des <a href="mailto:csps.copyrightdroitsdauteurs.efpc@canada.ca" title="Ce lien ouvrira une fenêtre de votre application de messagerie">Politiques et des relations stratégiques</a>.</p>
