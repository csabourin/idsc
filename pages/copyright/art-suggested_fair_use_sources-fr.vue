    <h3><strong>Domaine public, accès  libre et matériel Creative</strong> <sup id="fn1-rf"><a class="fn-lnk" href="#fn1"><span class="wb-inv">Note </span>1</a></sup></h3>
    <p>Lorsque la  durée du droit d'auteur arrive à échéance, l'œuvre est considérée comme faisant  dorénavant partie du <strong>domaine public</strong>.  Tout le monde peut alors l'utiliser sans la permission du propriétaire des  droits d'auteur.&nbsp;Une œuvre peut  également faire partie du domaine public parce qu'elle n'était pas admissible à  la protection des droits d'auteur dès le départ, ou parce que le propriétaire  des droits d'auteur a abandonné ses droits pour rendre l'œuvre au domaine public.  On peut abandonner les droits d'auteur en indiquant sur l'œuvre qu'elle peut  être copiée ou reproduite sans permission ni paiement de redevances. Des restrictions  peuvent s'appliquer à l'utilisation des œuvres dans un tel cas.</p>
    <p>.</p>
    <p>en règle  générale, le matériel à <strong>accès libre</strong><sup id="fn2-rf"><a class="fn-lnk" href="#fn2"><span class="wb-inv">Note </span>2</a></sup> permet aux  gens de consulter, de télécharger et de copier le matériel gratuitement à toute  fin légitime. Toutefois, avant d'utiliser du matériel à accès libre, veuillez  vérifier s'il existe des restrictions à l'utilisation du contenu.</p>
    <p><strong>Creative Commons (CC)</strong>&nbsp;est un organisme à but non lucratif ayant pour  but de faciliter l'échange de documents entre les personnes et de s'inspirer du  travail des autres. L'organisme émet un certain nombre de licences liées à du contenu  protégé par des droits d'auteur et communique clairement le type d'utilisation  que le créateur accorde aux utilisateurs, de même que les droits qu'il souhaite  se réserver. </p>
