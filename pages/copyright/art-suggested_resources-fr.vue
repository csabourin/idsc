        <h4>&OElig;uvres Creative Commons</h4>
      </summary>
      <p>
        Creative Commons est un organisme sans but lucratif qui se consacre à faciliter le partage et l’enrichissement de travaux d’une manière respectueuse des règles du droit d’auteur. Tout comme l’accès ouvert, le statut Creative Commons ne rend PAS l’œuvre libre de droits. Creative Commons octroie un certain nombre de licences standardisées pour du contenu protégé par des droits d’auteur, qui communiquent clairement le type d’utilisation que le créateur accorde aux utilisateurs, de même que les droits qu’il souhaite se réserver.
      </p>
      <p>
        On peut se servir de matériel Creative Commons pour des cours en classe sans demander la permission écrite de l’auteur, mais il faut respecter les conditions de licence choisies par l’auteur. Ces conditions peuvent ou non autoriser l’apport de modifications (y compris la traduction) à l’original. Il existe six types de licences. Pour savoir comment interpréter ces licences, veuillez consulter l’infographie <em><a title="Ce lien s'ouvrira dans une nouvelle fenêtre" href="tools-fr.html" target="_blank">Savoir interpréter une licence Creative Commons</a></em> dans la section Outils du présent site Web.
      </p>
      <p>
        On peut procéder de diverses façons pour trouver des œuvres Creative Commons. Le portail de recherche du <a title="Ce lien s'ouvrira dans une nouvelle fenêtre" href="https://search.creativecommons.org/" target="_blank">site Web de Creative Commons</a> constitue un bon point de départ, où les usagers peuvent obtenir des résultats très pointus à partir de divers sites hébergeant du matériel Creative Commons. Les sites tels que <a title="Ce lien s'ouvrira dans une nouvelle fenêtre" href="https://www.google.ca/advanced_search?hl=fr-CA&fg=1" target="_blank">Google</a> ou YouTube présentent également des options de recherche avancée qui permettent de faire des recherches personnalisées se limitant au matériel Creative Commons. Veuillez noter que ces outils de recherche reposent sur un terme de recherche, sur le type de licence sous lequel le matériel est publié et/ou sur le type d’utilisation qu’on souhaite faire de l’œuvre.
      </p>
