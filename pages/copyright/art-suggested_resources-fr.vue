    <h2>Ressources suggérées pour les œuvres libres de droit d’auteur ou sous licence</h2>
    <p>
      Le personnel de l’École dispose de diverses ressources qui lui permettent d’accéder à des œuvres libres de droit ou dont les droits d’utilisation ont déjà été accordés ou négociés. Ces ressources peuvent s’avérer d’une grande utilité, car elles donnent rapidement accès à une grande variété d’œuvres. Cependant, pour éviter d’enfreindre la <em>Loi sur le droit d’auteur</em>, il importe de respecter les conditions particulières qui se rattachent à chaque ressource.
