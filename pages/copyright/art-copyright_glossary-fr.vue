          <tr id="author">
            <td>Auteur</td>
            <td>
              Créateur d’une œuvre artistique, littéraire, musicale ou dramatique. Il peut s’agir d’une personne, d’un groupe de personnes, d’une entreprise, d’une organisation, d’un gouvernement, etc. Une œuvre peut être le fruit de plusieurs auteurs.
            </td>
            <td>
              Robert est un graphiste de l'équipe de Communication de l'École et a créé une affiche pour une nouvelle initiative en milieu de travail. Dans ce cas, Robert est l'auteur de l'affiche, et l'affiche est une œuvre artistique pour laquelle le droit d'auteur appartient à la Couronne puisqu'il est fonctionnaire
            </td>
          </tr>
          <tr id="access">
            <td>Access Copyright et Copibec</td>
            <td>
              <p>
                Organismes nationaux sans but lucratif qui représentent des auteurs, des concepteurs en arts visuels et des éditeurs canadiens, ainsi qu’un certain nombre d’œuvres d’éditeurs et de créateurs étrangers.
              </p>
              <p>
                Ils octroient une permission préalable pour reproduire des documents provenant d’un répertoire sans cesse croissant d’œuvres publiées, notamment des journaux, des revues professionnelles, des livres et des revues, tout en récompensant les créateurs et les éditeurs de ces documents.
              </p>
              <p>
                SPAC a conclu une entente avec Access Copyright et Copibec qui permet aux employés du gouvernement fédéral de copier du matériel protégé par un droit d’auteur visé par la licence, p. ex. des journaux, des revues professionnelles, des livres et des revues, dans les limites énoncées dans la licence et sans s’exposer aux risques de poursuite associés à la violation du droit d’auteur.
              </p>
            </td>
            <td>
              Nathalie est une conceptrice de cours qui aimerait utiliser un extrait d’un livre dans le contenu de son cours. elle a utilisé la fonction <em>outils de recherche de titres et d’autorisations</em> d’Access Copyright pour voir si le titre de l’ouvrage qu’elle désire utiliser s’y trouve. Puisqu'il s'y trouve, elle utilise le matériel en respectant les paramètres établis par la <em>Licence de reproduction du gouvernement du Canada</em>.
            </td>
          </tr>
          <tr id="copyright">
            <td>Droit d’auteur</td>
            <td>
              <p>
                Droit exclusif de produire, de traduire ou de reproduire la totalité ou une partie importante d’une œuvre sous une forme quelconque.
              </p>
              <p>
                Ce droit comporte le droit d’interpréter une œuvre ou toute partie importante de cette œuvre ou, dans le cas d’une conférence, le droit de la donner.
              </p>
              <p>
                Si l’œuvre n’est pas publiée, le droit d’auteur inclut le droit de publier l’œuvre ou d’en publier une partie importante.
              </p>
            </td>
            <td>
              Tous les produits d’apprentissage de l’École, qu’il s’agisse de vidéos, d’outils de travail, d’infographie ou de cours en ligne, sont automatiquement protégés par un droit d’auteur. Ce droit d’auteur s’applique, qu’il y ait présence ou non du symbole de droit d’auteur. Toutefois, étant donné que certains contenus de cours de l'École pourraient être disponibles sur des plateformes publiques (par exemple, Open Government Portal, GCcollab), les produits de l'École devraient, selon les meilleures pratiques, inclurent systématiquement le symbole &copy;.
            </td>
          </tr>
          <tr id="act">
            <td><em>Loi sur le droit d'auteur</em></td>
            <td>
              <p>
                Loi fédérale régissant le droit d’auteur au Canada. elle confère certains droits exclusifs au titulaire du droit d’auteur, notamment les suivants :
              </p>
              <ul>
                <li>produire ou reproduire la totalité ou une partie importante de l’œuvre, sous une forme matérielle quelconque;</li>
                <li>en exécuter ou en représenter la totalité ou une partie importante en public;</li>
                <li>si l’œuvre n’est pas publiée, en publier la totalité ou une partie importante;</li>
                <li>traduire l’œuvre;</li>
                <li>adapter l’œuvre en tant qu’œuvre cinématographique;</li>
                <li>communiquer l’œuvre au public, par télécommunication.</li>
              </ul>
            </td>
            <td>
              Diane est une conceptrice de cours qui a obtenu l’autorisation de reproduire un chapitre d’un ouvrage publié. Cependant, en examinant de plus près les dispositions de la <em>Loi sur le droit d’auteur</em>, elle constate maintenant qu’elle devra obtenir une deuxième autorisation pour traduire le chapitre.
            </td>
          </tr>
          <tr id="crown">
            <td>Droit d’auteur de la Couronne</td>
            <td>
              <p>
                Droit qui s’applique à toute œuvre créée ou publiée par l’intermédiaire, sous la direction et/ou sous la surveillance du gouvernement du Canada (y compris l’École).
              </p>
              <p>
                Le droit d’auteur de la Couronne s’applique à la majorité des produits originaux  développés à l’École (ex. les cours, les rapports, les publications, les bases de données, vidéos, blogues et les applications informatiques ), puisqu’il s’agit d’une organisation fédérale. La <em>Loi sur le droit d’auteur</em> protège les toutes les œuvres créées par les employés du gouvernement pendant une période de 50 ans (art. 12 de la <em>Loi sur le droit d’auteur</em>).
              </p>
              <p style="word-break: break-word; word-break: break-all; -webkit-hyphens: auto; -moz-hyphens: auto; -ms-hyphens: auto; hyphens: auto;">
                Le droit d’auteur de la Couronne s’applique également aux œuvres d’entrepreneurs travaillant sous contrat pour le gouvernement du Canada (y compris l’École), <em>sauf</em> lorsque le contrat accorde spécifiquement les droits d’auteur à l’entrepreneur. Pour de plus amples renseignements, consultez la Politique sur les droits de propriété intellectuelle issus de marchés conclus avec l’État à l’endroit suivant : <a title="Ce lien s'ouvrira dans une nouvelle fenêtre" href="https://www.ic.gc.ca/eic/site/068.nsf/fra/00005.html" target="_blank">https://www.ic.gc.ca/eic/site/068.nsf/fra/00005.html</a>.
              </p>
            </td>
            <td>
              <p>
                en tant que producteur vidéo pour l'École, Sam vient de faire quelques retouches sur une vidéo qu'il a produite pour le programme de développement de la gestion. La vidéo comprend des images filmées en studio, ainsi qu'un court clip d'animation produit par un consultant externe sous contrat avec l'École. Dans ce cas, la vidéo finale et le clip d'animation sont des œuvres appartenant à la Couronne (le gouvernement du Canada) qui sont protégées par le droit d'auteur de la Couronne pour une période de 50 ans.
              </p>
            </td>
          </tr>
          <tr id="infringement">
            <td>Violation du droit d’auteur</td>
            <td>
              Aux termes de la <em>Loi sur le droit d’auteur</em>, une atteinte au droit d’auteur est une reproduction ou l’utilisation non autorisée d’une œuvre ou d’un autre ouvrage auquel s’applique un droit d’auteur.
            </td>
            <td>
              Marie est une artiste indépendante qui a créé une infographie sur le leadership qu’ellis, une employée de l’École, aime beaucoup. L’image présente le symbole de droit d’auteur, ainsi qu’une mention indiquant qu’elle est la propriété exclusive de Marie. Ne tenant pas compte de cet avertissement, ellis utilise cette infographie dans un blogue qu’elle rédige pour l’École sans avoir obtenu la permission de son auteure. Dans ce cas ci, ellis a copié une image provenant d’une œuvre de quelqu’un d’autre, ce qui constitue une violation du droit d’auteur.
            </td>
          </tr>
          <tr id="cc">
            <td>Creative Commons</td>
            <td>
              Organisation à but non lucratif qui permet l’échange et l’utilisation de créations et de connaissances au moyen d’outils légaux et gratuits.
            </td>
            <td>
              Paul est tombé sur une représentation graphique sur un site Web qu’il aime beaucoup et désire l’utiliser dans une présentation qu'il prépare pour son équipe. Il examine la licence et constate qu’elle a été octroyée par Creative Commons. Paul prend connaissance de la licence de Creative Commons pour savoir comment il peut utiliser légalement l’aide visuelle.
            </td>
          </tr>
          <tr id="cc-license">
            <td>Licence de Creative Commons</td>
            <td>
              <p>
                Les licences de Creative Commons (CC) fonctionnent de concert avec le droit d’auteur et permettent au créateur de contenu de modifier les conditions de ses droits d’auteur en fonction de ses besoins. CC octroie d’une manière simple et uniformisée des licences de droits d’auteur gratuites et faciles à utiliser afin d’accorder au public la permission d’échanger et d’utiliser des œuvres créatives en respectant les conditions choisies par le créateur de contenu. Les licences de CC donnent la possibilité au créateur de contenu de changer facilement les conditions rattachées au droit d’auteur de la formulation par défaut « Tous droits réservés » à « Certains droits réservés ».
              </p>
              <p style="word-break: break-word; word-break: break-all; -webkit-hyphens: auto; -moz-hyphens: auto; -ms-hyphens: auto; hyphens: auto;">
                Il existe six types de licences Creative Commons. Chacune de ces licences comporte des limites et des droits particuliers. Pour connaître la signification des pictogrammes utilisés, veuillez consulter le site Web suivant : <a title="Ce lien s'ouvrira dans une nouvelle fenêtre" href="https://creativecommons.org/licenses/" target="_blank">https://creativecommons.org/licenses/</a>
              </p>
            </td>
            <td>
              Julie désire utiliser une vidéo protégée par une licence de Creative Commons. Avant d’utiliser la vidéo, elle compare les symboles de la licence avec ceux du site Web de Creative Commons pour déterminer les limites qui s’appliquent à l’utilisation de la vidéo.
            </td>
          </tr>
          <tr id="locks">
            <td>Serrure numérique</td>
            <td>
              <p>
                Mécanisme de contrôle de l’accès et de l’utilisation qui contrôle ou restreint l’accès à une œuvre, ce qui comprend les mots de passe.
              </p>
              <p>
                Le fait de contourner un mécanisme contrôlant l’accès à une œuvre constitue une infraction à la <em>Loi sur le droit d’auteur</em>.
              </p>
            </td>
            <td>
              Jean a protégé à l’aide d’un mot de passe (c. à d. une serrure numérique) le répertoire du système d’inscription de l’École afin de protéger contre toute copie, reproduction ou traduction illicites les documents privilégiés qu’il contient.
            </td>
          </tr>
          <tr id="exemption">
            <td style="word-break: break-word; word-break: break-all; -webkit-hyphens: auto; -moz-hyphens: auto; -ms-hyphens: auto; hyphens: auto;">
              exemption pour les établissements d’enseignement
            </td>
            <td>
              Aux termes de la <em>Loi sur le droit d’auteur</em>, un établissement d’enseignement (ou une personne agissant sous son autorité) n’est pas tenu de demander la permission de reproduire des documents lorsque lesdits documents sont reproduits ou traduits à des fins d’éducation ou de formation.
            </td>
            <td>
              Il y a peu de temps François, qui travaillait à l’Université d’Ottawa, a joint les rangs de l’École pour un détachement. Pour ses nouvelles fonctions à l’École, il a traduit un article qu’il souhaite utiliser dans le cours qu’il enseigne. Rapidement, il fut informé que contrairement à l’Université d’Ottawa, l’École ne peut se prévaloir de l’exemption accordée aux institutions d’enseignement puisque le statut juridique de l’École ne correspond pas à celui d’un établissement d’enseignement tel que décrit par la Loi sur le droit d'auteur, puisqu’elle relève de la compétence du gouvernement fédéral. Par conséquent, les employés de l’École qui désirent utiliser des documents protégés par des droits d’auteur doivent d’abord obtenir de l’auteur une licence ou la permission de les reproduire.
            </td>
          </tr>
          <tr id="fair">
            <td>Utilisation équitable</td>
            <td>
              <p>
                Les dispositions de la <em>Loi sur le droit d’auteur</em> relatives à l’utilisation équitable permettent aux utilisateurs de réaliser certaines activités se rapportant à la recherche, à l’étude privée, à l’éducation, à la parodie, à la satire, à la critique, aux comptes-rendus et aux reportages d’actualités, sans demander de permission d’utilisation.
              </p>
              <p>
                en ce qui concerne la critique, les comptes-rendus et les reportages d’actualités, l’utilisateur doit mentionner la source du document, ainsi que le nom de l’auteur, de l’artiste-interprète, du producteur ou du radiodiffuseur, pour que l’utilisation soit équitable.
              </p>
              <p>
                Le seul moyen de déterminer si une utilisation est équitable consiste à évaluer les critères suivants :
              </p>
              <ul>
                <li>le but, la nature et l’ampleur de l’utilisation;</li>
                <li>la nature du travail;</li>
                <li>les solutions de rechange à l’utilisation;</li>
                <li>l’effet de l’utilisation sur l’œuvre.</li>
              </ul>
            </td>
            <td>
              Annie effectue une recherche personnelle sur les pratiques de pointe en éducation des adultes. Pour appuyer sa recherche et étoffer ses conclusions, elle consulte de nombreux rapports de l’industrie et en tire des renseignements. Pour éviter toute violation du droit d’auteur, Annie cite les sources, ainsi que le nom de leurs auteurs. De cette façon, elle respecte les dispositions relatives à l’utilisation équitable de la <em>Loi sur le droit d’auteur</em> relatives à la recherche.
            </td>
          </tr>
          <tr id="ip">
            <td>Propriété intellectuelle</td>
            <td>
              Forme d’activité créatrice au sens large pouvant être protégée par une marque de commerce, un brevet, un droit d’auteur ou un dessin industriel.
            </td>
            <td>
              La Couronne possède les droits d’auteur de tous les produits d’apprentissage, qui font partie des actifs de propriété intellectuelle de l’École.
            </td>
          </tr>
          <tr id="license">
            <td>Licence</td>
            <td>
              <p>
                entente juridique octroyant à une personne la permission d’utiliser une œuvre à certaines fins ou sous certaines conditions.
              </p>
              <p>
                Une licence ne constitue pas un changement de propriété du droit d’auteur.
              </p>
            </td>
            <td>
              À titre de concepteur de cours, Marcel désire utiliser une photo produite par l’entreprise X dans l’une de ses vidéos. Ainsi, il doit négocier et élaborer un « contrat de licence » avec l'entreprise X. Après avoir conclu le contrat et intégré le matériel à la vidéo, l’École doit respecter toutes les conditions juridiques du contrat, selon les attentes du propriétaire du droit d’auteur, soit la compagnie X.
            </td>
          </tr>
          <tr id="moral">
            <td>Droits moraux</td>
            <td>
              Droits qu’un auteur conserve sur l’intégrité d’une œuvre et le droit d’être désigné comme son auteur même après la vente ou le transfert du droit d’auteur. Il convient de faire la distinction entre les droits moraux et les droits économiques. en effet, les droits moraux découlent de la personnalité de l’auteur qui se reflète dans son œuvre, tandis que les droits économiques donnent à l’auteur la possibilité de tirer un profit économique de son œuvre.
            </td>
            <td>
              Grâce à Publications Canada, les Canadiens ont accès à un éventail d’outils linguistiques élaborés par l’École. en mettant ces outils à la disposition du grand public, l’École choisit de ne pas faire appliquer ses droits d’auteur dans l’intérêt des Canadiens. Toutefois, l’École conserve les droits moraux à l’égard du contenu, même si l’information peut être utilisée librement par d’autres personnes.
            </td>
          </tr>
          <tr id="public">
            <td>Domaine public</td>
            <td>
              <p>
                œuvres qui ne sont <em>pas</em> protégées par le droit d’auteur. Généralement, une œuvre appartient au domaine public lors de l’expiration du droit d’auteur.
              </p>
              <p>
                Au Canada, le droit d’auteur s’appliquant à une œuvre vient habituellement à échéance 50 ans après le décès de son créateur. Après ce délai, les œuvres font partie du domaine public.
              </p>
            </td>
            <td>
              Helen conçoit un cours et aimerait y inclure un court extrait d’un roman de Mordecai Richler. elle ne peut pas le faire sans d’abord en obtenir l’autorisation, car Mordecai Richler est décédé le 3 juillet 2001, donc ses romans sont protégés par des droits d’auteur jusqu’au 31 décembre 2051. (50 ans après sa mort).
            </td>
          </tr>
          <tr id="royalty">
            <td>Redevance</td>
            <td>
              Somme versée aux propriétaires du droit d’auteur pour la vente ou l’utilisation de leurs œuvres.
            </td>
            <td>
              <p>
                L’École touche des redevances annuelles de McGill Queen’s University Press pour trois ouvrages : <em>Les nouveaux défis de la gouvernance, Protéger la démocratie, et Gouvernance au XXIe siècle</em>. L’École touche des redevances sur ces trois ouvrages parce qu’ils ont été rédigés en collaboration avec et/ou au nom de l’École.
              </p>
              <p>
                Par contre, l’École verse des redevances aux auteurs, s’il y a lieu, pour l’utilisation dans ses cours de leurs ouvrages protégés par des droits d’auteur.
              </p>
            </td>
          </tr>
          <tr id="royalty-free">
            <td>Libre de redevances</td>
            <td>
              Documents protégés par le droit d’auteur ou la propriété intellectuelle qui peuvent être utilisés sans que l’utilisateur doive verser des redevances ou des frais de licence.
            </td>
            <td>
              Michelle désire utiliser un tableau pour le document de cours qu’elle prépare. elle a demandé la permission à l’auteur, qui a accepté qu’elle utilise le matériel sans demander en retour une compensation monétaire. Dans un tel cas, le matériel utilisé est considéré comme un document libre de redevances.
            </td>
          </tr>
          <tr id="trademark">
            <td>Marque de commerce</td>
            <td>
              Une marque de commerce peut être constituée de mots, de sons ou de concepts, ou d’une combinaison de ceux-ci. elle vise à distinguer les produits ou les services d’une personne ou d’une organisation par rapport à d’autres produits ou services sur le marché. Les marques de commerce sont une forme de propriété intellectuelle.
            </td>
            <td>
              Tout comme les autres ministères fédéraux, l’École ne peut pas avoir sa propre marque de commerce, comme le stipule la <a title="Ce lien s'ouvrira dans une nouvelle fenêtre" href="https://www.tbs-sct.gc.ca/pol/doc-fra.aspx?id=30683" target="_blank">Politique sur les communications et l’image de marque</a> du Conseil du Trésor.
            </td>
          </tr>
