    <h3>Public Domain, Open Access and Creative Commons Material <sup id="fn1-rf"><a class="fn-lnk" href="#fn1"><span class="wb-inv">Footnote </span>1</a></sup></h3>
    <p>When the term of copyright expires, the work is said to come into the&nbsp;public domain&nbsp;and is then available for anyone to use and copy without seeking permission from the copyright owner.&nbsp;Works can also be in the public domain because the work was not eligible for copyright protection in the first place or because the copyright owner has forfeited copyright in the work to the public. This can be done by stating on the work that it may be copied or reproduced without permission or payment of royalties. Restrictions can be placed on the uses that can be made of works in this case.</p>
    <p>Open Access<sup id="fn2-rf"><a class="fn-lnk" href="#fn2"><span class="wb-inv">Footnote </span>2</a></sup>&nbsp;materials generally allow individuals to view, download and copy the material freely for any lawful purpose. However, before using any open access materials check to see if there are any limits to the use of the content.</p>
    <p>Creative Commons (CC)&nbsp;is a non-profit organization dedicated to making it easier for individuals to share and build on the work of others. It provides a number of licenses for copyrighted content that are standardized and communicate clearly what type of use the creator grants to users and what rights they wish to reserve.</p>
