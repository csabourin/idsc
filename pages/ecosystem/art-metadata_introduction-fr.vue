    <h2>Le th&eacute;saurus de l&rsquo;EFPC</h2>
    <p>Un th&eacute;saurus nous aide &agrave; comprendre les termes, par exemple des &eacute;l&eacute;ments de menu, de deux fa&ccedil;ons, &agrave; savoir&nbsp;:</p>
