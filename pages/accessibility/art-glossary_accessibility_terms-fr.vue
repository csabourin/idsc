    <h2>Glossaire d’accessibilité</h2>

    <dl>
      <dt>AQ (assurance de la qualité)</dt>

      <dd>Processus ou ensemble de processus servant à mesurer et à assurer la qualité d’un produit. On prévoit des activités systématiques dans le système d’assurance de la qualité afin de faire respecter les exigences en matière de qualité liées à un produit ou un service.</dd>

      <dt>BOEW (Boîte à outils de l’expérience Web)</dt>

      <dd>Elle rassemble différents outils et solutions prêts-à-utiliser pour la conception et la mise à jour de sites Web innovateurs qui sont à la fois accessibles, conviviaux et interopérables. Tous ces outils et solutions sont des logiciels gratuits mis à la disposition des ministères et des collectivités Web externes. La BOEW est dirigée par le Secrétariat du Conseil du Trésor (Ressources intellectuelles Canada). Elle est élaborée et tenue par les ministères fédéraux et les communautés Web externes.</dd>

      <dt>CDPI (Conseil des dirigeants principaux de l’information)</dt>

      <dd>Groupe de consultation et d’échange d’information sur des sujets liés à la gestion et à l’utilisation efficaces de l’information et de la technologie, en appui à la prestation de programmes et de services du gouvernement du Canada.</dd>

      <dt>CE (centres d’expertise)</dt>

      <dd>Les centres d’expertise sur la normalisation des sites Internet ont été créés au milieu de 2002 afin d’interpréter et de traiter les demandes portant sur les normes de normalisation des sites Internet provenant des employés de leur ministère respectif, de répondre ou de coordonner les demandes provenant des organismes et qui portent sur les instruments de politique, les pratiques exemplaires, les méthodes d’évaluation, les solutions et les outils relatifs à la normalisation des sites Internet et enfin de communiquer l’information pertinente dans l’ensemble de leur ministère respectif.</dd>

      <dt>CSS (feuilles de style en cascade)</dt>

      <dd>Langage de feuille de style utilisé pour décrire les éléments sémantiques de présentation (apparence et mise en page) d’un document écrit, au moyen de balises HTML ou XHTML. La version actuelle est CSS3.</dd>

      <dt>HTML (langage hypertexte)</dt>

      <dd>Il s’agit du principal langage utilisé pour les pages Web. Les éléments HTML sont le fondement des pages Web. La fonction d’un navigateur Web est de lire les documents HTML et de les transposer en pages visuelles ou sonores. Le navigateur n’affiche pas les balises HTML, mais les utilise pour interpréter le contenu de la page.</dd>

      <dt>JAWS (logiciel d’accès à l’emploi avec reconnaissance vocale)</dt>

      <dd>Programme de lecteur d’écran d’ordinateur de Microsoft Windows qui permet aux personnes aveugles ou ayant une déficience visuelle de lire l’écran, soit grâce à une fonction “ texte à discours », soit grâce à un afficheur braille dynamique. Le programme est l’œuvre de l’organisme Blind and Low Vision Group of Freedom Scientific (États-Unis).</dd>

      <dt>NSI (normalisation des sites Internet)</dt>

      <dd>Lignes directrices du gouvernement du Canada qui régissent les normes en matière de marque, de convivialité et d’accessibilité des sites Internet des ministères.</dd>

      <dt>PCIM (Programme de coordination de l’image de marque)</dt>

      <dd>Le PCIM du gouvernement du Canada contribue à conférer aux différents services de l’administration fédérale une image homogène, de façon que les Canadiens sachent au premier coup d’œil qu’ils ont affaire à l’un de leurs organismes gouvernementaux. Un autre objectif consiste à faciliter l’accès aux programmes et services gouvernementaux, toujours selon le principe de la clarté et de l’uniformisation des repères visuels.</dd>

      <dt>RPS (redondant, périmé et superflu)</dt>

      <dd>Campagne sur les pratiques exemplaires visant à réduire et à gérer le contenu, en mettant l’accent sur l’évaluation et l’amélioration du contenu en ligne afin de garder les coûts le plus bas possible.</dd>

      <dt>RSS (Really Simple Syndication)</dt>

      <dd>Famille de format de fils de syndication servant à publier des mises à jour fréquentes, comme les entrées de blogue, les actualisés, les fichiers audio et vidéo, dans un format normalisé. Les fils RSS procurent des avantages aux éditeurs en leur permettant de syndiquer automatiquement le contenu. Les fils RSS peuvent être lus par un logiciel appelé &laquo; lecteur RSS &raquo;, &laquo; lecteur de nouvelles &raquo; ou &laquo; agrégateur &raquo;.</dd>

      <dt>SEO (optimisation dans les moteurs de recherche)</dt>

      <dd>Processus visant à améliorer le classement dans les résultats d’un moteur de recherche. Le référencement prend en considération la façon dont les moteurs de recherche fonctionnent, ce que les gens cherchent, les termes de recherche ou les mots clés saisis dans le moteur de recherche et les moteurs de recherche préférés (selon les publics cibles).</dd>

      <dt>W3C (World Wide Web Consortium)</dt>

      <dd>Principal organisme international de normes sur le World Wide Web. Le Consortium est composé d’organisations membres, qui fournissent des employés à temps plein pour collaborer quant à l’élaboration de normes régissant le Web.</dd>

      <dt>WAI (Web Accessibility Initiative)</dt>

      <dd>Initiative du World Wide Web Consortium visant à améliorer l’accessibilité du Web aux personnes handicapées. Elle comprend plusieurs groupes de travail et groupes d’intérêt, qui collaborent pour l’élaboration de lignes directrices, de rapports techniques, de documents pédagogiques et de tout autre document lié aux différents volets de l’accessibilité du Web.</dd>

      <dt>WCAG (Règles pour l’accessibilité des contenus Web)</dt>

      <dd>Elles font partie des lignes directrices sur l’accessibilité du Web, publiées dans le cadre de la Web Accessibility Initiative. Elles portent sur l’accessibilité du contenu, principalement pour les personnes handicapées, mais aussi pour tous les agents (y compris les appareils très limités, notamment les téléphones cellulaires). La version actuelle est la 2.0 (11 décembre 2008).</dd>

      <dt>Web 2.0 (Web 2.0)</dt>

      <dd>Ensemble de nouvelles technologies, méthodes et philosophies pour l’échange d’opinions et d’idées, la collaboration et le commerce sur le Web. Les applications Web 2.0 comprennent les wikis (Wikipédia, GCPEDIA), les sites de réseaux sociaux (Facebook, Myspace), les sites de blogue et toute autre application Web collaborative.</dd>

      <dt>WMC (Conseil des gestionnaires de sites Web)</dt>

      <dd>Le Conseil traitera de questions se rapportant aux mesures destinées à assurer l’accessibilité et à d’autres instruments de politique du SCT (p. ex. Normalisation des sites Internet, etc.) concernant le Web, tout en favorisant l’innovation et l’évolution des sites Web du gouvernement du Canada. Des enjeux connexes pourraient comprendre l’infrastructure Web (p. ex. solutions, architecture, outils, etc.) et la gestion des sites Web (p. ex. gouvernance, processus et ressources).</dd>

      <dt>WWW (World Wide Web, Web, W3)</dt>

      <dd>Système de documents hypertextes interreliés accessibles par Internet.</dd>

      <dt>WYSIWYG (tel écran – tel écrit)</dt>

      <dd>Lorsque le contenu (texte et images) à l’écran au cours de la modification est affiché dans une forme très semblable à celle qu’il a une fois imprimé ou en tant que produit final (qui peut être un document imprimé, une page Web ou une diapositive de présentation).</dd>
    </dl>
