    <h3>Policy on the Duty to Accommodate</h3>

    <p>
      The policy backed by Treasury Board and the Public Service Commission.
      The goal of the policy is to "&hellip;build and retain a broad, barrier-free
      environment in the federal Public Service to ensure the full participation
      of persons with disabilities."
    </p>
    <p>This policy is to be implemented by:</p>

    <blockquote cite="http://www.tbs-sct.gc.ca/pol/doc-eng.aspx?id=12541&section=text">
      <ul>
        <li>
          identifying and removing barriers to employment, career development
          and promotion of persons with disabilities unless doing so would result
          in undue hardship
        </li>

        <li>
          designing all employment systems, processes and facilities to be
          accessible by building accommodation into workplace standards, systems,
          processes and facilities and
        </li>

        <li>
          accommodating individuals when such barriers cannot be removed.
          Such accommodation must be made to the point of undue hardship taking
          into consideration issues of health, safety and cost. Accommodation
          must also be based on the circumstances of each case and must respect
          an individual's right to privacy and confidentiality.
        </li>
      </ul>
