    <h2>Alternative (ALT) Text and Long Descriptions</h2>
    <p>
      Under the <a href="//laws-lois.justice.gc.ca/eng/charter/" target="_blank">Canadian Charter of Rights and Freedoms</a>, federal departments
      have the obligation to make all of their information and services
      accessible to all Canadians. This means that all users, whether they browse
      the Internet using traditional methods, or by using assistive devices such
      as screen readers, need to have the same access to all of our web content.
      For this and other reasons, all Government of Canada (GC) websites must
      comply with the <a href="http://www.tbs-sct.gc.ca/pol/doc-eng.aspx?id=23601&section=text" target="_blank">Government of Canada's Standard on Web Accessibility</a>.
    </p>
    <p>
      This standard requires that departments "provide text alternatives for
      any non-text element so that it can be changed into other forms people
      need, such as large print, braille, speech, symbols or simpler language.".
      This means that we must provide content that, when presented to the user,
      conveys essentially the same function or purpose as auditory or visual
      content.
    </p>
    <h3>What is "alt text"?</h3>
    <br>
    <p>
      "<strong>Alt text</strong>" or <strong>alternate text</strong> is
      included in web pages to describe a non-text element, such as a photo. When
      a screen reader happens upon an image, it will read the alt text aloud to
      the user. If there is no alt text, screen readers will either skip the
      image or read the filename out loud, which can be frustrating when the
      image filename is something like "DSC10982736a.jpg".
    </p>
    <h3>What are non-text elements?</h3>
    <p>
      Graphics that include text, images, animations, list bullets or spacers,
      graphical buttons, sounds, video, audio files or audio tracks of video.
    </p>
    <h3>What is a &quot;long description&quot;?</h3>
    <p>
      For complex images that require more than 100&nbsp;characters to convey
      textual information and where there is supplemental information available
      to enhance the understanding of the images a long description should be
      provided for users.
    </p>
    <h3>Why are alt text and long descriptions so important?</h3>
    <ul>
      <li>
        Canadians have the right to obtain information and services
        from GC websites regardless of the technologies they use.
      </li>
      <li>
        Images are not accessible to someone who is visually
        impaired.
      </li>
      <li>
        Alt text is used by search engines to index images. Proper use
        of alt text will help search engines find pages on the website, and
        display them higher in search results.
      </li>
    </ul>
