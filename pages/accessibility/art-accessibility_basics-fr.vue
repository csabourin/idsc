    <h2>Les notions de base de l’accessibilité du Web</h2>
    <h3>Qu’entendons par &laquo; accessibilité du Web &raquo;?</h3>
    <dl>
      <dt>Définition :</dt>
      <dd>Pratique inclusive visant à rendre les sites Web accessibles à toute personne, avec ou sans handicap.</dd>
      <dt>Principe :</dt>
      <dd>Le Web est fondamentalement conçu pour tous, peu importe le matériel informatique et les logiciels qu’ils utilisent, leur langue, leur culture, l’endroit où ils se trouvent ou encore leurs capacités physiques ou mentales.</dd>
      <dt>Pratique :</dt>
      <dd>Être flexible aux besoins, aux préférences et aux situations des utilisateurs.</dd>
    </dl>
    <details>
      <summary>
        <h3>Composantes de l’accessibilité Web</h3>
      </summary>
      L’accessibilité dépend de sept composantes :
      <ol start="1" type="1">
        <li>Contenu (texte, images, sons, code, balises, présentation)</li>
        <li>
          Agents utilisateurs (navigateurs Web, lecteurs de
          médias, etc.)
        </li>
        <li>Technologies d’assistance (lecteur d’écran, clavier, interrupteur)</li>
        <li>Connaissance et expérience des utilisateurs (comportements)</li>
        <li>Développeurs (concepteurs, codeurs, auteurs)</li>
        <li>Outils de rédaction (logiciels de création sites Web, contenu)</li>
        <li>Outils d’évaluation (valideurs HTML, valideurs CSS)</li>
      </ol>
    </details>
    <details>
      <summary>
        <h3>Pourquoi l’accessibilité Web est-elle importante?</h3>
      </summary>
      <ul>
        <li>Elle permet de répondre plus efficacement aux besoins des clients</li>
        <li>Elle permet d’atteindre un plus grand public</li>
        <li>Elle permet de s’ajuster aux différents types d’apprentissage</li>
        <li>Elle permet de démontrer la responsabilité des ministères</li>
        <li>Elle améliore la qualité des sites Web</li>
        <li>Elle permet de mieux répertorier le contenu pouvant faire l’objet d’une recherche</li>
        <li>Elle permet de respecter les exigences prévues par la loi</li>
        <li>Elle facilite l’innovation et soutient la multimodalité</li>
        <li>Elle crée un environnement commun pour les technologies Web</li>
        <li>Elle améliore l’expérience Web de tous les utilisateurs</li>
      </ul>
    </details>
    <details>
      <summary>
        <h3>Obstacles physique/psychologiques</h3>
      </summary>
      <ul>
        <li>Déficience visuelle</li>
        <li>Surdité et perte de l’ouïe</li>
        <li>Limitation des mouvements (fonction motrice/dextérité)</li>
        <li>Troubles de la parole</li>
        <li>Photosensibilité</li>
        <li>Troubles d’apprentissage</li>
        <li>Limitations cognitives</li>
        <li>Troubles sociaux</li>
        <li>Combinaison de handicaps</li>
      </ul>
    </details>
    <details>
      <summary>
        <h3>Obstacles : mauvaise conception Web</h3>
      </summary>
      <ul>
        <li>Dépendant du navigateur</li>
        <li>Manque du support complet du clavier</li>
        <li>Limites de temps pour accomplir les tâches, les formes</li>
        <li>Les contrôles qui n’ont pas un équivalent textuel</li>
        <li>Visuels et aides à la navigation manquantes</li>
        <li>Structure de page incorrecte</li>
        <li>Fonctions de navigation imprévisibles</li>
        <li>Le contenu vidéo qui n’a pas d’équivalent (audio ou texte)</li>
        <li>Contraste insuffisant</li>
        <li>Texte ne peut pas être redimensionné</li>
        <li>Expérience qui dépend de la disposition au lieu du contenu</li>
      </ul>
