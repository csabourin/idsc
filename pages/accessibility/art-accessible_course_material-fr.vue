    <h3>La validation de l’accessibilité pour les documents PDF</h3>
    <ol>
      <li>Avant de convertir votre document en format PDF, assurez-vous que le contenu de votre document est accessible.</li>
      <li>Dans Word, PowerPoint ou Excel, aller à <strong>Acrobat &gt; Préférences.</strong> Cochez les options suivantes: « Créer des signets à l’aide de titres &raquo;, &laquo; Ajouter des liens &raquo;, &laquo; Propriétés du document &raquo; et &laquo; Balise de structure de document pour l’accessibilité &raquo;.</li>
      <li>Cliquez sur <strong>OK</strong>, puis allez à <strong>Fichier &gt; Enregistrer au format Adobe PDF</strong>.</li>
      <li>Une fois que vous avez converti votre document en format PDF, dans Acrobat Standard, validez à nouveau son accessibilité en sélectionnant <strong>Affichage &gt; Outils &gt; Accessibilité</strong>. Cliquez sur <strong>Assistant de configuration</strong> pour affecter les paramètres d'accessibilité pour votre PDF. (Remarque: Les outils de validation d’accessibilité d’Adobe s’appuient sur les normes de conformité 508 qui sont inférieures aux normes WCAG 2.0. L’utilisation d’outils pour la validation WCAG 2.0 assure une conformité plus robuste.)</li>
