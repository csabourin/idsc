    <h3>La validation de l’accessibilité pour les documents Microsoft Office</h3>
    <p>Pour tester l’accessibilité de vos documents Word, PowerPoint et Excel :</p>
    <ol>
      <li>Ouvrez le document que vous souhaitez tester.</li>
      <li>Dans le menu <strong>Fichier</strong> sélectionnez <strong>Informations</strong></li>
      <li>Cliquez sur <strong>Vérifier la présence de problèmes</strong>, pour les questions, puis cliquez sur <strong>Vérifier l'accessibilité.</strong></li>
      <li>Consultez les erreurs, avertissements et conseils qui figurent dans le volet <strong>Résultats de l’inspection.</strong></li>
      <li>Cliquez sur chaque erreur ou avertissement pour afficher les détails et les étapes pour y remédier.</li>
      <li>Corrigez chaque item, puis répétez les étapes 1-3 jusqu'à ce qu'aucune erreur ou avertissement sont émis.</li>
    </ol>
