    <h2>Le matériel de cours accessible</h2>
    <p>La conformité à l’accessibilité ne s’applique pas uniquement au contenu Web. Elle s’applique également à tout matériel de cours que les employés de l’EFPC élaborent, développent et distribuent. Les documents Microsoft Office et PDF peuvent être accessibles, mais il y a des étapes à suivre afin de s’en assurer. Une fois que aurez développé la version finale de votre matériel de cours, il devrait être transmis aux <a class="nobr" href="mailto:csps.learningservicerequest-demandeserviceapprentissage.efpc@canada.ca">Services et technologies d’apprentissage</a> pour des tests de conformité plus robustes.</p>
    <h3>La conception de matériel accessible</h3>
    <p>Il y a certains principes de conception auxquels vous devez vous conformer pour le matériel de cours. Vous trouverez des pratiques exemplaires ci-dessous pour les documents Microsoft Office et PDF.</p>
    <h4>Pour les documents Microsoft Office :</h4>
    <ul>
      <li>Utilisez des styles de titre (H1, H2 , H3 , etc. ) .</li>
      <li>Utilisez les listes (à puces, numérotées et/ou multi-niveaux). Ne pas insérer des tirets et des numéros manuellement.</li>
      <li>Créez une table des matières si votre document est plus long que trois pages.</li>
      <li>Ne justifiez pas votre texte. Alignez vos paragraphes à la gauche.</li>
      <li>Utilisez des polices linéales (sans serif).</li>
      <li>N’utilisez pas de tailles de police de moins de 12 points.</li>
      <li>Soulignez seulement les liens.</li>
      <li>Assurez-vous qu'il y a un taux de contraste adéquat entre l’arrière-plan et le texte.</li>
      <li>Assurez-vous qu’aucune information ne dépende de couleurs ou de formes (ex : l’exemple en vert est exacte/ l’exemple en rouge est inexacte.)</li>
      <li>Utilisez des tableaux pour les données, mais pas pour des améliorations cosmétiques au contenu.</li>
      <li>Ajoutez des textes descriptifs à toutes les images d'importance. Pour ajouter des textes descriptifs, cliquez sur votre image avec le bouton de droite, sélectionnez <strong>Format,</strong> et sélectionnez <strong>Texte de remplacement.</strong></li>
    </ul>
    <h4>Pour les documents PDF :</h4>
    <ul>
      <li>Précisez le paramètre de la langue pour votre PDF. Allez à <strong>Fichier</strong> &gt; <strong>Propriétés</strong> &gt; <strong>Avancé</strong> afin de sélectionner une langue.</li>
      <li>Assurez-vous que le texte est consultable. En d’autres mots, votre lecteur devrait pouvoir rechercher un mot dans votre document s’il est consultable. Les documents numérisés ne sont pas accessibles puisque leur texte n’est pas consultable. Les documents numérisés produisent des images de texte seulement.</li>
      <li>Ajoutez des marqueurs interactifs pour tous les champs de formulaire PDF.</li>
      <li>Ajoutez un texte descriptif, s’il est manquant, pour toutes les images ou les champs du formulaire. Lors de la création d'un fichier PDF à partir d'un document Office accessible, les textes descriptifs sont généralement exportés vers le document PDF.</li>
      <li>Retirez ou modifiez des balises vides dans votre document PDF.</li>
    </ul>
