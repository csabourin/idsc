    <h2>Glossary of accessibility terms</h2>

    <dl>
      <dt>CLF (Common Look and Feel)</dt>

      <dd>
        The GC guidelines that formerly governed branding, usability and accessibility standards for government departments on the
        Internet. CLF has been replaced by the new Web Standards for the Government of Canada.
      </dd>

      <dt>CIO (Chief Information Officer)</dt>

      <dd>
        The departmental CIO (or equivalent) is responsible for ensuring that software and systems acquired by departments enable Web
        sites to meet the requirements of WCAG&nbsp;2.0 or are modifiable to enable Web sites to meet these requirements.
      </dd>

      <dt>CIOC (Chief Information Officer Council)</dt>

      <dd>
        A forum for consultation and information exchange on matters relating to the effective management and use of information and
        technology in support of program and service delivery in the Government of Canada
      </dd>

      <dt>COE (Centres of Expertise)</dt>

      <dd>
        COEs were established in mid‑2002 to Interpret and handle all inquiries about CLF Standards across their entire department,
        responding and/or coordinating all institution inquiries related to CLF policy instruments, best practices, assessment
        methodologies, solutions and tools, communicate relevant information across their entire department.
      </dd>

      <dt>CSS (Cascading Style Sheet)</dt>

      <dd>
        A style sheet language used to describe the presentation semantics (the look and formatting) of a document written in a markup
        language such as HTML and XHTML. The current version is CSS3.
      </dd>

      <dt>FIP (Federal Identity Program)</dt>

      <dd>
        The GC's corporate identity program that helps project the government as a coherent, unified administration and enables
        Canadians to recognize at a glance their government at work for them. It facilitates access to government programs and services
        through clear and consistent identification.
      </dd>

      <dt>GC, GoC (Government of Canada)</dt>

      <dd />

      <dt>HTML (HyperText Markup Language)</dt>

      <dd>
        The main markup language for web pages. HTML elements are the basic building-blocks of webpages. The purpose of a web browser
        is to read HTML documents and compose them into visible or audible web pages. The browser does not display the HTML tags, but uses
        the tags to interpret the content of the page.
      </dd>

      <dt>JAWS (Job Access with Speech)</dt>

      <dd>
        A computer screen reader program in Microsoft Windows that allows blind and visually impaired users to read the screen either
        with a text-to-speech output or by a Refreshable Braille display. Produced by the Blind and Low Vision Group of Freedom Scientific
        (USA).
      </dd>

      <dt>PDF (Portable Document Format)</dt>

      <dd>
        PDF is a file format used to represent documents in a manner independent of application software, hardware, and operating
        systems. Each PDF file encapsulates a complete description of a fixed-layout flat document, including the text, fonts, graphics,
        and other information needed to display it.
      </dd>

      <dt>QA (Quality Assurance)</dt>

      <dd>
        The process or set of processes used to measure and assure the quality of a product. The planned and systematic activities
        implemented in a quality system so that quality requirements for a product or service will be fulfilled.
      </dd>

      <dt>RSS (Really Simple Syndication)</dt>

      <dd>
        A family of web feed formats used to publish frequently updated works&nbsp;– such as blog entries, news headlines, audio, and
        video&nbsp;– in a standardized format. RSS feeds benefit publishers by letting them syndicate content automatically. RSS feeds can
        be read using software called an "RSS reader", "feed reader" or "aggregator".
      </dd>

      <dt>SEO (Search Engine Optimization)</dt>

      <dd>
        The process of improving ranking in search engine results. SEO considers how search engines work, what people search for, the
        actual search terms or keywords typed into search engines and which search engines are preferred by their targeted audience.
      </dd>

      <dt>W3C (World Wide Web Consortium)</dt>

      <dd>
        The main international standards organization for the World Wide Web. The consortium is made up of member organizations which
        maintain full-time staff for the purpose of working together in the development of standards for the WWW.
      </dd>

      <dt>WAI (Web Accessibility Initiative)</dt>

      <dd>
        An effort by the W3C to improve the accessibility of the WWW for people with disabilities. It has several working groups and
        interest groups that work on guidelines, technical reports, educational materials and other documents that relate to the several
        different components of web accessibility.
      </dd>

      <dt>WCAG (Web Content Accessibility Guidelines)</dt>

      <dd>
        Part of a series of Web accessibility guidelines published by the W3C's Web Accessibility Initiative. They consist of a set of
        guidelines for making content accessible, primarily for disabled users, but also for all user agents, including highly limited
        devices, such as mobile phones. The current version is&nbsp;2.0 (December&nbsp;11,&nbsp;2008).
      </dd>

      <dt>Web&nbsp;2.0 (Web&nbsp;2.0)</dt>

      <dd>
        Web&nbsp;2.0 refers to a set of new technologies, methods and philosophies for sharing opinions and ideas, working
        collaboratively, and doing business on the World Wide Web. Web&nbsp;2.0 applications include wikis (Wikipedia, GCPEDIA), social
        networking sites (Facebook, Myspace), blogging websites, and other collaborative web applications.
      </dd>

      <dt>WET (Web Experience Toolkit)</dt>

      <dd>
        Ready-made tools and solutions for building and maintaining innovative Web sites that are accessible, usable, and
        interoperable. These tools and solutions are open source software and free for use by departments and external Web communities. WET
        is led by TBS (Intellectual Resources Canada) and collaboratively developed and maintained by Government of Canada (GC) departments
        and external Web communities.
      </dd>

      <dt>WMC (Web Managers Council)</dt>

      <dd>
        The WMC deals with issues related to implementation of accessibility, and other TBS policy instruments (e.g.&nbsp;Common Look
        and Feel, etc.) on the Web while fostering innovation and evolution of GC Websites. The related issues could include Web
        infrastructure (e.g.&nbsp;solutions, architecture, tools, etc.), and Web management (e.g.&nbsp;governance, processes, and
        resources).
      </dd>

      <dt>WWW (World Wide Web; Web; W3)</dt>

      <dd>A system of interlinked hypertext documents accessed via the Internet.</dd>

      <dt>WYSIWYG (What You See Is What You Get)</dt>

      <dd>
        When content (text and graphics) displayed onscreen during editing appears in a form closely corresponding to its appearance
        when printed or displayed as a finished product, which might be a printed document, web page, or slide presentation.
      </dd>
    </dl>
