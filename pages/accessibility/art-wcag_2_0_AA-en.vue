        <dd>Provide alternatives for time-based media.</dd>
        <dt>1.3 Adaptable:</dt>
        <dd>
          Create content that can be presented in different ways (for
          example, simpler layout) without losing information or
          structure.
        </dd>
        <dt>1.4 Distinguishable:</dt>
        <dd>
          Make it easier for users to see and hear content, including
          separating foreground from background.
        </dd>
        <dt>2.1 Keyboard Accessible:</dt>
        <dd>Make all functionality available from a keyboard.</dd>
        <dt>2.2 Enough Time:</dt>
        <dd>Provide users enough time to read and use content.</dd>
        <dt>2.3 Seizures:</dt>
        <dd>
