      <summary>
        <h3>Success Criteria</h3>
      </summary>
      <p>
        Under each guideline, there are Success Criteria that describe
        specifically what must be achieved in order to conform to this
        standard. They are similar to the "checkpoints" in WCAG&nbsp;1.0. Each
        Success Criterion is written as a statement that will be either true or
        false when specific Web content is tested against it. The Success
        Criteria are written to be technology neutral.
      </p>
      <p>
        All WCAG&nbsp;2.0 Success Criteria are written as testable criteria
        for objectively determining if content satisfies the Success Criteria.
        While some of the testing can be automated using software evaluation
        programs, others require human testers for part or all of the test.<br>
        (<a href="http://www.w3.o../../UNDERSTANDING-WCAG20/intro.html#introduction-layers-head" target="_blank">http://www.w3.o../../UNDERSTANDING-WCAG20/intro.html#introduction-layers-head</a>)
      </p>
      <h3>What are levels A, AA and AAA?</h3>Levels A, AA and AAA correspond to
      WCAG success criteria levels. In simplified terms, these levels mean:
      <ul type="square">
        <li>Without level A: some people will find it impossible</li>
        <li>Without level AA: some people will find it difficult</li>
        <li>Without level AAA: some people will miss full impact</li>
      </ul>
      <p>
        For your content to be Level AA compliant, you must meet all Level A and
        Level AA standards.
      </p>
