    <h3>WCAG Guidelines and Principles</h3>
    <p>
      WCAG&nbsp;2.0 has 12&nbsp;guidelines that are organized under 4
      principles: perceivable, operable, understandable, and robust. For each
      guideline, there are testable success criteria, which are at three levels:
      25 at A, 13 at AA, and 23 at AAA. (<a href="http://www.w3.org/WAI/intro/wcag.php" target="_blank">http://www.w3.org/WAI/intro/wcag.php</a>)
    </p>
    <details>
      <summary>
        <h3>Principles</h3>
      </summary>
      <h4>Perceivable</h4>
      <p>
        Information and user interface components must be presentable to
        users in ways they can perceive.
      </p>
      <ul>
        <li>
          This means that users must be able to perceive the information
          being presented (it can't be invisible to all of their senses)
        </li>
      </ul>
      <p />
      <h4>Operable</h4>
      <p>User interface components and navigation must be operable.</p>
      <ul>
        <li>
          This means that users must be able to operate the interface
          (the interface cannot require interaction that a user cannot
          perform)
        </li>
      </ul>
      <h4>Understandable</h4>
      <p>
        Information and the operation of user interface must be
        understandable.
      </p>
      <ul>
        <li>
          This means that users must be able to understand the
          information as well as the operation of the user interface (the
          content or operation cannot be beyond their understanding)
        </li>
      </ul>
      <h4>Robust</h4>
      <p>
        Content must be robust enough that it can be interpreted reliably by
        a wide variety of user agents, including assistive technologies.
      </p>
      <ul>
        <li>
          This means that users must be able to access the content as
          technologies advance (as technologies and user agents evolve, the
          content should remain accessible)
        </li>
      </ul>
      <p>
        If any of these are not true, users with disabilities will not be
        able to use the Web.
      </p>
