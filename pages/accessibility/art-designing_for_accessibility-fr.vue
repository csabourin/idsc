    <h2>L’élaboration de contenus accessibles</h2>
    <p>Quoique votre rôle en tant que concepteur pédagogique ne requiert pas des compétences en programmation HTML, vous occupez tout de même un rôle important face à l’accessibilité lors de la conception de votre contenu et le développement de matériel de cours. Vous trouverez ci-dessous un aperçu simplifié de méthodes d’élaboration de contenu accessible.</p>
    <h3>Principes d’élaboration de contenus accessibles</h3>
    <ul type="square">
      <li>Fournir des équivalents textuels appropriés</li>
      <li>Mettre des entêtes aux tableaux de données</li>
      <li>Veiller à ce que les utilisateurs puissent remplir et soumettre tous les formulaires</li>
      <li>S’assurer que les liens sont appropriés selon le contexte</li>
      <li>Sous-titrer les médias ou fournir des transcriptions</li>
      <li>Assurer l’accessibilité du contenu non-HTML (PDF, Word, Flash)</li>
      <li>Permettre aux utilisateurs de passer outre les éléments répétitifs de la page</li>
      <li>Ne pas se fier uniquement à la couleur pour véhiculer de l’information</li>
      <li>S’assurer que le contenu est clairement écrit et facile à lire</li>
      <li>Rendre le contenu accessible à JavaScript</li>
      <li>Respecter les normes</li>
    </ul>
    <h4>Images</h4>
    <ul type="square">
      <li>Utiliser des équivalents textuels attribut (&lt;alt&gt;)</li>
      <li>Décrire les images</li>
      <li>Décrire la destination (si l’image est un lien)</li>
      <li>Laisser l’équivalent textuel vide pour les images décoratives (alt="")</li>
    </ul>
    <h4>Images en arrière-plan</h4>
    <ul type="square">
      <li>Aucun équivalent textuel</li>
      <li>Aucun élément sonore pour en transmettre une signification</li>
    </ul>
    <h4>Liens</h4>
    <ul type="square">
      <li>S’assurer que la destination est claire à partir du lien</li>
      <li>Si le lien ouvre un nouvel onglet ou une nouvelle fenêtre, il faut en avertir l’utilisateur</li>
      <li>Éviter la redondance des attributs &lt;title&gt;</li>
    </ul>
    <h4>Entêtes</h4>
    <ul type="square">
      <li>Elles sont souvent utilisées comme moyen de navigation par les utilisateurs de lecteur d’écran</li>
      <li>Elles permettent de répartir le contenu en parties plus faciles à gérer</li>
      <li>Il faut essayer de les intégrer correctement</li>
      <li>Utiles pour le référencement</li>
    </ul>
    <h4>Texte</h4>
    <ul type="square">
      <li>Ne pas utiliser une police trop petite</li>
      <li>Utiliser un bon contraste de couleurs</li>
      <li>Il faut qu’il puisse être redimensionné sans que la mise en page soit touchée (au départ)</li>
      <li>Éviter la justification</li>
      <li>Couleur et signification</li>
    </ul>
    <h4>Listes</h4>
    <ul type="square">
      <li>S’il s’agit d’une liste d’articles : il faut la baliser comme étant une liste</li>
    </ul>
    <h4>Vidéo</h4>
    <ul type="square">
      <li>Éviter les lectures automatiques</li>
      <li>Ajouter des sous-titres</li>
      <li>Fournir des équivalents textuels</li>
    </ul>
    <h4>Focus du clavier</h4>
    <ul type="square">
      <li>Bonne visibilité</li>
      <li>Ordre de tabulation approprié</li>
      <li>Veiller à ce que toutes les fonctionnalités soient accessibles par frappe (p. ex. menus déroulants)</li>
    </ul>
