    <h2>The basics of Web accessibility</h2>
    <h3>What Is Web Accessibility?</h3>
    <dl>
      <dt>Definition:</dt>
      <dd>The inclusive practice of making web sites usable by people of all abilities and disabilities</dd>
      <dt>Principle:</dt>
      <dd>
        The web is fundamentally designed to work for all people, whatever their hardware, software, language, culture, location or
        physical or mental ability
      </dd>
      <dt>Practice:</dt>
      <dd>Being flexible to the different user needs, preferences and situations</dd>
    </dl>
    <details>
      <summary><h3>Components of Web Accessibility</h3></summary>
      Web accessibility depends on the cooperation of 7 components:
      <ol start="1" type="1">
        <li>Content (text, images, sounds, code, markup, presentation)</li>
        <li>User agents (Web browsers, media players)</li>
        <li>Assistive technologies (screen readers, keyboards, switches)</li>
        <li>Users' knowledge and experiences (user behaviours)</li>
        <li>Developers (designers, coders, authors)</li>
        <li>Authoring tools (software that creates Web sites, content)</li>
        <li>Evaluation tools (HTML validators, CSS validators)</li>
      </ol>
    </details>
    <details>
      <summary>
        <h3>Why is Web accessibility important?</h3>
      </summary>
      <ul>
        <li>More efficient in meeting client needs</li>
        <li>Reaches a larger audience</li>
        <li>Addresses different learning styles</li>
        <li>Demonstrates corporate responsibility</li>
        <li>Improves quality of web sites</li>
        <li>Better indexing of searchable content</li>
        <li>Meets legal requirements</li>
        <li>Facilitates innovation and supports multi-modality</li>
        <li>Creates a common sandbox for web technologies</li>
        <li>Contributes to a better web experience for all users</li>
      </ul>
    </details>
    <details>
      <summary>
        <h3>Barriers: Physical / Psychological</h3>
      </summary>
      <ul>
        <li>Vision impairments</li>
        <li>Deafness and hearing loss</li>
        <li>Limited movement (motor function/dexterity)</li>
        <li>Speech disabilities</li>
        <li>Photosensitivity</li>
        <li>Learning disabilities</li>
        <li>Cognitive limitations</li>
        <li>Social disorders</li>
        <li>Any combination of the above</li>
      </ul>
    </details>
    <details>
      <summary>
        <h3>Barriers: Bad Web design</h3>
      </summary>
      <ul>
        <li>Browser-dependent</li>
        <li>No full keyboard support</li>
        <li>Insufficient time limits to complete tasks, forms</li>
        <li>Controls that do not have equivalent text alternatives</li>
        <li>Missing visual cues, navigational aids</li>
        <li>Improper page structure</li>
        <li>Unpredictable navigation functions</li>
        <li>Video content that does not have audio or text alternatives</li>
        <li>Insufficient contrast</li>
        <li>Text cannot be resized or loses info when resized</li>
        <li>Experience that is dependent on layout instead of content</li>
      </ul>
    </details>
