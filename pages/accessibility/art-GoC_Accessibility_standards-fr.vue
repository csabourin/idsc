    <h2>Les normes d’accessibilité du Gouvernement du Canada</h2>
    <p><img src="~/assets/Accessibility_image.jpg" width="216" height="150" class="float-left" alt="Close-up of a computer keyboard with one key showing the accessibility symbol, a person on a wheelchair">L’École de la fonction publique du Canada (EFPC) souscrit aux normes d’accessibilité du Gouvernement du Canada (GdC) pour tous ses produits et services, qu’ils soient en ligne ou hors ligne. Les employés de l’EFPC doivent donc toujours considérer l’accessibilité pour chacune des phases d’élaboration de contenu, que ce soit lors de la conception, le développement, l’implantation ou de l’évaluation.</p>
    <h3>Politique sur l’obligation de prendre des mesures d’adaptation</h3>
    <p>
      La Politique est soutenue par le Conseil du Trésor et la Commission de la fonction publique. L’objectif de la Politique est &laquo; …d’établir et de maintenir un milieu de travail ouvert et sans obstacles au sein de la fonction publique fédérale afin d’assurer la participation entière des personnes handicapées. &raquo;<br>
      La mise en œuvre de la présente politique sera réalisée :
    </p>
    <blockquote cite="http://www.tbs-sct.gc.ca/pol/doc-eng.aspx?id=12541&section=text">
      <ul>
        <li>en cernant et en éliminant les obstacles à l’emploi, au perfectionnement professionnel et à la promotion des personnes handicapées, à moins que les mesures à prendre n’imposent une contrainte excessive;</li>
        <li>en concevant tous les systèmes, les procédés et les installations de sorte qu’ils soient accessibles et en intégrant des mesures d’adaptation dans les normes, les systèmes, les procédés et les installations du milieu de travail;</li>
        <li>en fournissant des mesures d’adaptation lorsque les obstacles à l’emploi ne peuvent être éliminés. Ces mesures doivent être prises si elles n’imposent pas une contrainte excessive, compte tenu des questions de santé, de sécurité et de coût. Les mesures d’adaptation doivent par ailleurs être définies en fonction des circonstances propres à chaque cas et respecter le droit à la protection des renseignements personnels et à la confidentialité d’une personne.</li>
      </ul>
