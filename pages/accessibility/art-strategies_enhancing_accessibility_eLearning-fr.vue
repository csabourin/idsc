    <h3>Assurez l’accessibilité du contenu non HTML :</h3>
    <ul>
      <li>Le contenu HTML sera presque toujours plus accessible que le contenu dans tout autre format.</li>
      <li>Les fichiers PDF, Microsoft Word et PowerPoint, ainsi que ceux créés dans OpenOffice.org et Adobe Flash offrent des fonctions d’accessibilité élémentaires.</li>
      <li>Offrir des solutions de rechange sur le plan de l’accessibilité lorsqu’il n’est pas possible de rendre le contenu non HTML pleinement accessible.</li>
      <li>Mettre à l’essai l’accessibilité du contenu non HTML à l’aide de technologies fonctionnelles.</li>
      <li>Utilisez un langage clair et simple et soyez attentif à la charge cognitive (questions à choix multiple proposant un nombre trop élevé de réponses possibles). Évitez l’option « toutes ces réponses » – trop de détails à retenir.</li>
