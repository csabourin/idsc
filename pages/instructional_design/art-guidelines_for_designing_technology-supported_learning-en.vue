    <h2>Guidelines for Designing Technology-Supported Learning</h2>
    <p>Instructional Designers are faced with a variety of questions about incorporating learning technologies into learning approaches. examples include:  How do I go about converting a classroom course for distance only delivery? How do I design a blended learning course? How do I add a virtual community component to my classroom course? Which tools should I use?</p>
    <p>How does one decide which is the most appropriate learning approach or blend of approaches? It depends. Principles of adult learning and sound instructional design should always be considered, regardless of the approach or technologies used. There is no secret formula for designing successful blends of learning approaches but there are some considerations to keep in mind. Specifically, <em>the combination</em> of many factors will help to determine the best learning strategy.</p>
    <p>This document is not a complete requirements analysis or needs assessment questionnaire. Rather, it should be used as a guide when selecting learning technologies and approaches. Rationale for questions and examples are included in the document. They are intended to help you understand what factors you need to consider when planning on incorporating technologies for learning.</p>
    <h3>Important considerations when developing technology-supported learning</h3>
    <details>
      <summary>
        <h4>Learning Outcomes</h4>
      </summary>
      <p>It is important to understand the overall learning outcomes. What are your learning objectives? What kind of content do you currently have?</p>
      <p>If the goal is basic <em>knowledge transfer of standardized information</em> (e.g., definitions, procedures, compliance, new policies), the following are examples of appropriate learning approaches to consider incorporating.</p>
      <ul class="list-unstyled mrgn-lft-lg mrgn-lft-lg">
        <li>Text-based presentations (with supporting images, if appropriate)</li>
        <li>Support tools (e.g., documents outlining procedures for managing new client requests)</li>
        <li>Demonstrations</li>
        <li>Lectures - classroom or virtual classroom</li>
      </ul>
      <p>When the goal is to <em>work on problem solving/critical thinking skills, team work, communication skills, improvement of practice, to have a change in attitude or skills</em>, the following are examples of appropriate learning approaches to consider incorporating.</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Discussion and dialogue</li>
        <li>Problem-based or project-based learning</li>
        <li>Text or video-based scenarios</li>
        <li>Collaborative activities</li>
        <li>Demonstrations</li>
        <li>Mentoring and coaching</li>
        <li>Opportunities for practice</li>
        <li>Opportunities for assessment and feedback</li>
        <li>Opportunities for reflection</li>
        <li>Informal communities of practice</li>
      </ul>
      <p>It&rsquo;s also important to ask why incorporate technologies now? What&rsquo;s your goal?</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Reduce class time?</li>
        <li>eliminate class time?</li>
        <li>Offer a distance-only option? (Is classroom training still being offered?)</li>
        <li>What is currently working? What is not working?</li>
      </ul>
      <p>When training does not produce the desired or expected change in job performance, you can analyse the <em>Performance Gap</em> using this <a href="http://gcdocs.csps-efpc.gc.ca/otcs/llisapi.dll?func=ll&amp;objId=6032638&amp;objAction=download" target="_blank">Job Aid</a> or another of your choice.</p>
      <p>With a clear sense of where the gap is, what it is like and how big it is, you will design a much better learning solution.</p>
      <p>Understanding the parameters around your learners is also important. These questions will help guide you toward a suitable approach.</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Informal vs. structured learning? </li>
        <li>Cohort or community based?</li>
        <li>Is networking a major component?</li>
        <li>Sensitivity and/or security of information being shared?</li>
        <li>(Online  discussions, even in a secure environment, may not be successful)</li>
      </ul>
    </details>
    <details>
      <summary>
        <h4>Learners: identification, location and number</h4>
      </summary>
      <p>This <a href="http://gcdocs.csps-efpc.gc.ca/otcs/llisapi.dll?func=ll&amp;objId=6002951&amp;objAction=download" target="_blank">Learner Identification Analysis Job Aid</a> can help you gather necessary information about your learners.</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Are your learners geographically dispersed?</li>
        <li>How many learners will benefit from/ need this training?</li>
        <li>How many learners can you accommodate at one time?</li>
      </ul>
      <p>If learners are geographically dispersed and/or classroom space is an issue, online training may be the most cost-effective solution. Depending on the learning objectives, one would determine whether or not training should be conducted in a virtual classroom or via self-paced  e-learning (including mobile learning).</p>
      <p>If a large number of learners require training that is based on standardized &ldquo;information transfer&rdquo;, then a self-paced online learning product might be beneficial. It would offer flexibility for learners to set their own schedule for learning, reduce travel time and time spent away from the office.</p>
      <p>On the other hand, if the number of learners who require training is small, investing in the development of online material may not be financially sound.</p>
      <p>
        &ldquo;National offerings&rdquo;, using the virtual classroom, could be a good option for courses that would otherwise need to be cancelled due to low enrollment.<br>
        For example, english offerings in Quebec occur infrequently while courses offered in French in other regions get cancelled as well. One option is to pool participants from across the country to avoid cancellations.
      </p>
      <p>Another consideration is class size. For virtual classroom training, 12-15 students is ideal. If you need to train many people quickly, a traditional classroom scenario may be better. As always, the designer must consider other factors simultaneously, such as learning outcomes, geography, and scheduling challenges.</p>
    </details>
    <details>
      <summary>
        <h4>Stability of training material</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Will the material require many changes over time?</li>
        <li>Is the content customized?</li>
        <li>What is the shelf-life of the content?</li>
        <li>How much flexibility will be needed over time?</li>
      </ul>
      <p>If the content will need regular updating, an e-learning course is not likely to be the best option- especially if there is text, audio and video involved. In this case, modifications to content can be very time consuming and costly, particularly when managed in two languages.</p>
    </details>
    <details>
      <summary>
        <h4>Design / Development resources</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>How many resources do you have to devote to the project?</li>
        <li>What are their skills? (Design, develop, facilitate online, SMe)</li>
        <li>What are your timelines?</li>
      </ul>
      <p>If you need to develop quickly and do not have a skilled team (and Learning Technology and Services (LTS)productions team is not available), self-paced online learning may not be a good solution.</p>
    </details><details>
      <summary>
        <h4>Time &amp; scheduling</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>How long is your course?</li>
        <li>How is it currently scheduled (e.g., 5 full classroom days, 2 half-day sessions spread over 2 weeks)?</li>
        <li>Is it preferable that learners have time between sessions to work on projects and digest information?</li>
        <li>Are there any known time or scheduling constraints? (e.g., getting DMs in a classroom)</li>
      </ul>
      <p>Scheduling classroom training, particularly for several consecutive days, can be challenging for learners as well as for those coordinating the event.</p>
      <p>For the virtual classroom, there are additional time constraints because learners cannot typically spend more than 2 hours at a time online.</p>
      <p>When designing a blend of activities, one should think of real-time activities as a <em>precious opportunity</em>: </p>
      <ul>
        <li>In what ways can you take full advantage of students coming together with access to a subject matter expert?</li>
        <li>Which activities are best suited for this opportunity?</li>
      </ul>

      <p>In what other ways can you meet some of the &ldquo;knowledge transfer&rdquo; objectives in order to take advantage of classroom or virtual classroom time? Some examples include:</p>
      <ul>
        <li>Pre-reading course materials</li>
        <li>Pre-course activities</li>
        <li>Learner assessments in advance (to help focus real-time event on what is really needed and to get learners thinking about their learning needs which may be unknown to them)</li>
      </ul>
    </details>

