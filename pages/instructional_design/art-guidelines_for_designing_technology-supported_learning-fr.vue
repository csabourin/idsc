    <h2>Guide pour la conception d'un produit d'apprentissage assisté par la technologie</h2>
    <p>Les concepteurs pédagogiques se posent diverses questions sur l'utilisation des technologies dans les méthodes d'apprentissage. Par exemple&nbsp;: Comment convertir un cours en salle de classe en cours à distance? Comment concevoir un cours selon une méthode d'apprentissage mixte? Comment intégrer une communauté virtuelle à un cours en salle de classe? Quels outils utiliser?</p>
    <p>en somme, comment déterminer la ou les meilleures méthodes d'apprentissage? Cela dépend. Peu importe les méthodes et les technologies utilisées, il faut toujours suivre les principes de l'andragogie et les pratiques exemplaires de la conception pédagogique. Il n'y a pas de recette miracle pour en arriver à la combinaison parfaite. Cependant, certains facteurs doivent être pris en considération. Ce sont en fait de nombreux facteurs qui, examinés ensemble, aident à établir la stratégie gagnante.</p>
    <p>Le présent document ne se veut pas un questionnaire exhaustif d'analyse des exigences ou d'évaluation des besoins. Il s'agit plut&ocirc;t d'un guide pour choisir les méthodes et les technologies à utiliser. Il détaille les questions à se poser, exemples à l'appui, pour analyser les facteurs entrant en ligne de compte dans le recours à des technologies d'apprentissage.</p>
    <h3>Facteurs à prendre en considération pour concevoir un produit d'apprentissage assisté par la technologie</h3>
    <details>
      <summary>
        <h4>But de l'apprentissage</h4>
      </summary>
      <p>Il faut d'abord cerner le but général de l'apprentissage. Quels sont les objectifs d'apprentissage? &Agrave; quoi ressemble le contenu existant?</p>
      <p>Si le but est <em>d'acquérir</em> <em>une connaissance de base de notions standard</em> (p. ex. des définitions, des procédures, des normes, de nouvelles politiques), voici quelques méthodes d'apprentissage appropriées&nbsp;:</p>
      <ul class="list-unstyled mrgn-lft-lg mrgn-lft-lg">
        <li>Présentation sous forme de texte (avec des images, si possible)</li>
        <li>Outil de référence (p. ex. un document décrivant la procédure à suivre pour gérer une demande d'un nouveau client)</li>
        <li>Démonstration</li>
        <li>exposé &ndash; en salle de classe traditionnelle ou virtuelle</li>
      </ul>
      <p>Si le but est <em>d'acquérir des compétences en résolution de problème ou en communication, de développer l'esprit critique ou l'esprit d'équipe, d'améliorer une pratique ou de modifier une attitude ou une compétence,</em> voici quelques méthodes d'apprentissage appropriées<em>&nbsp;</em>:</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Discussion et dialogue</li>
        <li>Résolution d'un problème ou exécution d'un projet</li>
        <li>Scénarios présentés sous forme de texte ou de vidéo</li>
        <li>Activités de collaboration</li>
        <li>Démonstration</li>
        <li>Mentorat ou coaching</li>
        <li>Séance de pratique</li>
        <li>évaluation et rétroaction</li>
        <li>exercice de réflexion</li>
        <li>Communauté de pratique informelle</li>
      </ul>
      <p>Par ailleurs, il faut se demander pourquoi recourir aux technologies à ce moment-ci? Quel est le but?</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Réduire le temps passé en salle de classe?</li>
        <li>éliminer une séance donnée en salle de classe?</li>
        <li>Offrir un cours à distance seulement? (Un cours en salle de classe est-il toujours offert?)</li>
        <li>Actuellement, qu'est-ce qui fonctionne bien? Qu'est-ce qui fonctionne moins bien?</li>
      </ul>
      <p>Quand une formation ne produit pas le changement voulu sur le plan du rendement au travail, il peut être utile d'analyser l'écart de rendement au moyen de <a href="http://gcdocs.csps-efpc.gc.ca/otcs/llisapi.dll?func=ll&amp;objId=6138129&amp;objAction=download" target="_blank">cet outil de travail</a> ou d'un autre outil au choix.</p>
      <p>Un concepteur qui a une idée claire de la nature, de la portée et de l'ampleur du problème ne peut qu'élaborer une meilleure solution d'apprentissage.</p>
      <p>Il est aussi essentiel d'établir les paramètres propres au public cible. Voici quelques questions à se poser pour déterminer la méthode appropriée&nbsp;:</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Faut-il offrir une activité d'apprentissage informelle ou structurée?</li>
        <li>S'agit-il d'une cohorte ou d'une communauté?</li>
        <li>Le réseautage est-il un facteur important?</li>
        <li>Quel est le niveau de sensibilité ou de sécurité des renseignements transmis? (Il n'est peutâ€‘être pas idéal d'engager une discussion en ligne, même dans un environnement sécurisé.)</li>
      </ul>
    </details>
    <details class="grouped">
      <summary>
        <h4>Apprenants&nbsp;: identification, endroit et nombre</h4>
      </summary>
      <p><a href="http://gcdocs.csps-efpc.gc.ca/otcs/llisapi.dll?func=ll&amp;objId=6137224&amp;objAction=download" target="_blank">L'analyse pour identifier les besoins des apprenants</a> permet de compiler des renseignements utiles sur les apprenants.</p>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Les apprenants se trouvent-ils dans des lieux géographiques différents?</li>
        <li>Combien d'apprenants voudront ou devront suivre le cours?</li>
        <li>Combien d'apprenants pourront suivre le cours en même temps?</li>
      </ul>
      <p>Si les apprenants se trouvent dans des lieux géographiques différents ou que le nombre de places disponibles en salle de classe est limité, un cours en ligne pourrait être la solution la plus économique. Selon les objectifs d'apprentissage, il reste à choisir entre un cours en salle de classe virtuelle ou un cours en ligne à rythme libre (y compris sur appareil mobile).</p>
      <p>Si de nombreux apprenants doivent acquérir des notions standard, un cours en ligne à rythme libre pourrait être une bonne solution. Cette méthode permet aux apprenants de suivre le cours au moment qui leur convient, sans déplacement hors du bureau.</p>
      <p>Toutefois, si le public cible comporte peu d'apprenants, il n'est peut-être pas avantageux financièrement d'investir dans le développement d'un cours en ligne.</p>
      <p>Ainsi, il peut s'avérer intéressant d'offrir un cours &laquo;&nbsp;national&nbsp;&raquo; en salle de classe virtuelle si le taux d'inscription est trop faible pour qu'il se donne. &Agrave; titre d'exemple, peu de cours sont donnés en anglais au Québec ou en français, dans les autres provinces. Pour éviter d'annuler un cours, il est possible de &laquo;&nbsp;réunir&nbsp;&raquo; les apprenants à l'échelle du pays.</p>
      <p>Par ailleurs, il faut prendre en considération la taille de la classe. Dans le cas d'une salle de classe virtuelle, un groupe de douze à quinze apprenants est l'idéal. Pour ce qui est d'une salle de classe traditionnelle, elle convient davantage pour former un groupe nombreux en peu de temps. Bien entendu, le concepteur ne doit pas négliger les autres facteurs, comme le but de l'apprentissage, le lieu géographique et l'horaire.</p>
    </details>
    <details>
      <summary>
        <h4>Pérennité du matériel de formation</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Le cours devra-t-il être refait avec le temps?</li>
        <li>Le contenu est-il personnalisé?</li>
        <li>Quelle est sa durée utile?</li>
        <li>Quelle marge de man&oelig;uvre sera requise à long terme?</li>
      </ul>
      <p>Si le contenu doit être mis à jour régulièrement, un cours en ligne n'est pas la meilleure solution, surtout s'il contient du texte, et du contenu audio et vidéo. Modifier ce genre de contenu &ndash; éventuellement dans deux langues &ndash; peut nécessiter beaucoup de temps et d'argent.</p>
    </details>
    <details>
      <summary>
        <h4>Ressources de conception et d'élaboration</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Combien y a-t-il de ressources de disponibles pour travailler au projet?</li>
        <li>Quelles sont leurs compétences (p. ex. conception, élaboration, animation en ligne, expert en la matière)?</li>
        <li>Quelle est l'échéance?</li>
      </ul>
      <p>Si le délai d'élaboration est serré et que les membres de l'équipe ne possèdent pas les compétences requises (et que l'équipe de production des Services et technologies de l'apprentissage n'est pas disponible), un outil d'apprentissage en ligne à rythme libre n'est peutâ€‘être pas une bonne solution.</p>
    </details>
    <details>
      <summary>
        <h4>Durée et horaire</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Quelle est la durée du cours?</li>
        <li>Actuellement, quelle plage horaire occupe-t-il (p. ex. cinq&nbsp;jours en salle de classe, deux&nbsp;séances de une demi-journée réparties sur deux&nbsp;semaines)?</li>
        <li>est-il préférable d'espacer les séances pour permettre aux apprenants d'exécuter des projets ou d'assimiler les notions abordées?</li>
        <li>Y a-t-il des contraintes de temps ou d'horaire (p. ex. pour inviter des sous-ministres en salle de classe)?</li>
        <li>Que ce soit pour l'apprenant ou le coordonnateur, il peut s'avérer difficile de mettre à l'horaire un cours en salle de classe, surtout s'il dure plusieurs jours consécutifs.</li>
        <li>Or, organiser un cours en salle de classe virtuelle est aussi contraignant, car normalement, l'apprenant ne peut pas passer plus de deux&nbsp;heures de suite en ligne.</li>
        <li>Ainsi, dans la conception d'une activité d'apprentissage mixte, il faut considérer l'événement en temps réel comme une occasion privilégiée&nbsp;:</li>
        <li>Comment tirer pleinement avantage de la réunion des apprenants avec un spécialiste?</li>
        <li>Quelles seraient les meilleures activités à organiser?</li>
      </ul>
      <p>Quelles autres méthodes permettraient d'atteindre les objectifs d'apprentissage tout en profitant pleinement du temps en salle de classe &ndash; traditionnelle ou virtuelle? Voici quelques exemples&nbsp;:</p>
      <p>Lecture préparatoire</p>
      <p>Activité préparatoire</p>
      <p>évaluation préalable (de façon à inciter les apprenants à prendre conscience des besoins en apprentissage qu'ils pourraient ignorer et à y consacrer pleinement l'événement en temps réel).</p>
    </details>
    <details>
      <summary>
        <h4>Suivi et évaluation</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>S'agit-il d'une formation indispensable?</li>
        <li>Faut-il évaluer les apprenants ou assurer un suivi?</li>
        <li>Faut-il faire passer un test de connaissances?</li>
        <li>Faut-il s'assurer que les apprenants ont lu tout le contenu?</li>
        <li>Les réponses à ces questions aident à déterminer la plateforme à utiliser, la façon de respecter les normes du modèle informatique SCORM et les activités administratives à effectuer dans le Système harmonisé de gestion de l'apprentissage.</li>
      </ul>
    </details>
    <details>
      <summary>
        <h4>Accessibilité et langues officielles</h4>
      </summary>
      <ul class="list-unstyled mrgn-lft-lg">
        <li>Y a-t-il des exigences particulières quant à l'accessibilité?</li>
        <li>Le contenu de cours est-il disponible dans les deux langues officielles?</li>
      </ul>
      <h5>Accessibilité</h5>
      <p>Les concepteurs de cours jouent un r&ocirc;le de premier plan pour rendre accessibles le contenu et le matériel pédagogique. Ils doivent tenir compte de l'accessibilité à chaque étape&nbsp;: la conception, l'élaboration, la mise en &oelig;uvre et l'évaluation. Au chapitre de l'accessibilité Web, le gouvernement du Canada a adopté les Règles pour l'accessibilité des contenus Web&nbsp;2.0 <a href="http://www.tbs-sct.gc.ca/pol/doc-fra.aspx?id=23601" target="_blank">(WCAG&nbsp;2.0)</a> afin d'honorer ses engagements. La section sur <a href="//sb-es-demo.csps-efpc.com/idsc/content_sections/accessibility/GoC_Accessibility_standards-fr.html" target="_blank">l'<strong>accessibilité</strong></a> du Centre de soutien pour les concepteurs pédagogiques présente des lignes directrices et des précisions sur la conception et l'élaboration de produits d'apprentissage accessibles.</p>
      <h5>Langues officielles</h5>
      <p>Les concepteurs de cours doivent connaître les obligations de l'école en matière de langues officielles et les garder à l'esprit quand ils créent des produits d'apprentissage. La section sur <a href="//sb-es-demo.csps-efpc.com/idsc/content_sections/official_languages/official_language_introduction-fr.html" target="_blank">les <strong>langues officielles</strong></a> du Centre de soutien pour les concepteurs pédagogiques présente des lignes directrices et des recommandations.</p>
