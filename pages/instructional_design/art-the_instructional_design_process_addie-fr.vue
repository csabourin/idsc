      <b-tab title="D">
        <h3>étape de la conception</h3>
        <p>
          L'étape de la conception est le moment où vous utilisez l'information de votre analyse pour créer l'esquisse d'un
          produit d'apprentissage qui répond aux besoins de votre client.
        </p>
        <p>
          Pendant l'étape de la conception, il faut déterminer les stratégies pour atteindre les objectifs
          d'apprentissage, établir une méthode d'évaluation, créer des exercices et du contenu et choisir le mode de
          prestation approprié. Si vous utilisez des éléments multimédias, vous aurez également à
          préparer des scénarios et des scénarimages. L'étape de la conception est le moment où vous donnez une
          structure logique à votre contenu d'apprentissage.
        </p>
        <p>Vous pouvez diviser l'étape en quatre volets</p>
        <ol>
          <li>
            <details>
              <summary>
                <h4>Planifier une stratégie pédagogique</h4>
              </summary>
              <ul>
                <li>Comment faut-il organiser le contenu?</li>
                <li>Comment faut-il présenter les idées aux apprenants?</li>
                <li>Quel mode de prestation faut-il utiliser?</li>
                <li>Comment est-ce que le matériel pédagogique sera regroupé et organisé?</li>
                <li>Quels types d'activités et d'exercices seront les plus susceptibles d'aider les apprenants?</li>
                <li>Comment le cours peut-il évaluer les réalisations des apprenants?</li>
              </ul>
            </details>
          </li>
          <li>
            <details>
              <summary>
                <h4>Choisir la structure du cours</h4>
              </summary>
              <p>
                Pour en savoir plus sur les <em>modes de prestation</em> dans l'écosystème d'apprentissage de
                l'école, cliquez <a href="http://gcdocs.csps-efpc.gc.ca/otcs/llisapi.dll?func=ll&amp;objId=6070373&amp;objAction=download" target="_blank">ici</a>.
              </p>
            </details>
          </li>
          <li>
            <details>
              <summary>
                <h4>élaborer le Plan de conception de l'apprentissage</h4>
              </summary>
              <p>Le rôle du plan de conception</p>
              <ul>
                <li>Décrire l'approche globale à l'égard de l'apprentissage</li>
                <li>Déterminer les choix des supports pédagogiques</li>
                <li>Cerner le public cible</li>
                <li>Analyser le contenu</li>
                <li>
                  énumérer les objectifs d'apprentissage et harmoniser ceux-ci avec la stratégie
                  d'enseignement
                </li>
                <li>Déterminer une stratégie pour la mise à l'essai du produit.</li>
              </ul>
              <p>
                <em>(Remarque&nbsp;: La <strong>fiche signalétique du produit</strong> sera diffusée au cours du prochain
                  exercice. Il s'agit d'un document de référence</em> <em>qui contient toute l'information au sujet
                  d'un produit et qui servira tout au long de son cycle de vie &ndash; de l'analyse à la mise en œuvre. elle
                  remplacera un grand nombre de processus et de modèles).</em>
              </p>
            </details>
          </li>
          <li>
            <details>
              <summary>
                <h4>Autres facteurs</h4>
              </summary>
              <ul>
                <li>Assurez-vous que les concepts sont cohérents et complets</li>
                <li>
                  Présentez la solution proposée et encouragez les gens à formuler des commentaires au sujet de la
                  conception
                </li>
                <li>Demandez à la direction d'approuver/d'autoriser le Plan de conception de l'apprentissage</li>
                <li>Créez des scénarimages et demandez qu'ils soient approuvés</li>
                <li>Donnez des instructions aux concepteurs</li>
                <li>Adaptez le plan de projet qui comprend des mesures, des échéanciers et des personnes responsables</li>
                <li>
                  Demandez à un rédacteur technique et à des réviseurs (équipe d'éditique) de
                  vérifier tout le contenu
                </li>
              </ul>
