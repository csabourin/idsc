      <b-tab title="E">
        <h3>étape de l'évaluation</h3>
        <p>
          L'étape de l'évaluation englobe l'ensemble du processus, de l'analyse au développement
          (et plus loin encore), même si elle se trouve à la fin de la structure énoncée. L'évaluation
          doit comprendre des mesures formatives et sommatives. Par exemple, vous aurez créé les outils pour recueillir les
          commentaires des apprenants à l'étape de la conception. &Agrave; l'étape du développement,
          vous avez créé des outils de test pour pouvoir déterminer si les objectifs d'apprentissage ont
          été atteints. L'équipe <a href="mailto:csps.evaluation.efpc@canada.ca">d'évaluation</a> peut vous aider à cet effet. Vous saurez donc si le cours atteint ses objectifs énoncés..
        </p>
        <ul>
          <li>
            élaboration de questionnaires
            <ul>
              <li>est-ce que le cours plaît aux apprenants?</li>
              <li>
                est-ce que les apprenants réussissent à atteindre les objectifs d'apprentissage à la fin du
                cours?
              </li>
              <li>est-ce que les apprenants modifient leurs comportements dans le milieu de travail?</li>
              <li>est-ce que le cours aide le client/l'organisation à atteindre ses objectifs opérationnels?</li>
            </ul>
          </li>
          <li>Recueillir les commentaires des apprenants.</li>
          <li>évaluer l'amélioration et déterminer les lacunes.</li>
          <li>Rédiger un rapport sur les constatations &ndash; les résultats du cours.</li>
          <li>Transmettre le rapport aux clients et les membres indiqués de la direction.</li>
          <li>Au besoin, élaborer un plan de révision.</li>
        </ul>
