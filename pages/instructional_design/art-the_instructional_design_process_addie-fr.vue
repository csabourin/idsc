      <b-tab title="I">
        <h3>étape de la mise en œuvre</h3>
        <p>
          L'étape de la mise en œuvre, c'est le moment où le cours est prêt à être
          lancé. Il est question de logistique et de lancement.
        </p>
        <ul>
          <li>
            Logistique du cours
            <ul>
              <li>établir l'horaire/le programme pour le lancement du cours</li>
              <li>
                Mettre au point un calendrier des cours, ajouter les cours au répertoire de l'école, réserver les
                salles (s'il y a lieu)
              </li>
              <li>Choisir des formateurs et les préparer au moyen d'une séance sur mesure de formation des formateurs</li>
              <li>Préparer tout le matériel de formation</li>
              <li>
                S'assurer que tous les sites sont prêts &ndash; s'assurer que les salles ont le matériel et le soutien
                technique nécessaires
              </li>
            </ul>
          </li>
          <li>Lancer le cours</li>
          <li>Annoncer le produit et en faire la promotion</li>
        </ul>
        <section>
          <h5>Voici les principaux produits livrables</h5>
          <ul>
            <li><strong>inscription des apprenants dans le SHGA</strong></li>
            <li>
              <strong>formulaires de commentaires</strong> (instruments d'évaluation à donner aux apprenants à la
              fin du cours)
            </li>
            <li><strong>guides</strong> et <strong>outils de travail</strong></li>
            <li><strong>documents pour la séance de formation des formateurs</strong></li>
            <li>peut-être des <strong>formulaires de présence</strong>, en fonction du mode de prestation pour la formation</li>
          </ul>
          <p>L'équipe des Services d'apprentissage peut vous aider. Pour obtenir des gabarits et des formulaires sur les produits livrables, consultez notre <a href="//intranet.myschool-monecole.gc.ca/cdd/ProductDevelopment_f.asp" target="_blank">page de développement des produits</a> sur l'Intranet.</p>
        </section>
