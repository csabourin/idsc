      <b-tab title="D">
        <h3>étape de développement</h3>
        <p>
          L'étape de développement est axée sur la création de prototypes et l'élaboration de
          matériel pédagogique. Il faut faire des mises à l'essai et les évaluer pendant cette étape.
          Voici quelques-uns des principaux produits livrables de cette étape:
        </p>
        <ul>
          <li>Matériel pédagogique</li>
          <li>Manuels de l'enseignant</li>
          <li>Outils de travail</li>
          <li>Instruments d'évaluation</li>
          <li>Calendriers de cours.</li>
        </ul>
        <p>
          <a href="mailto:csps.evaluation.efpc@canada.ca">L'équipe d'évaluation</a> vous aidera à élaborer votre stratégie sur l'évaluation;<br>
          <a href="mailto:csps.communications.efpc@canada.ca">L'équipe des communications</a> vous aidera à élaborer des plans de communication
        </p>
        <section>
          <h5>Voici un aperçu du processus</h5>
          <ul>
            <li>Les concepteurs rassemblent tous les éléments de contenu</li>
            <li>
              Création d'un prototype (en collaboration avec l'équipe des technologies de l'apprentissage)
              <ul>
                <li>Modèles de cours</li>
              </ul>
            </li>
            <li>
              examen du prototype et tenue de consultations à son sujet
              <ul>
                <li>est-ce que le contenu est exact?</li>
                <li>est-ce que le contenu est complet?</li>
              </ul>
            </li>
            <li>élaboration du matériel pédagogique</li>
            <li>
              Processus d'assurance de la qualité et d'assurance de la qualité intégrée qu'effectuent
              les évaluateurs
            </li>
            <li>
              Vérification que toutes les exigences sont satisfaites &ndash; séance avec un groupe de discussion
              <ul>
                <li>Déterminer le temps qu'il faut aux apprenants pour réaliser chaque module et chaque activité</li>
                <li>évaluer le niveau d'engagement des apprenants à l'égard du matériel</li>
                <li>Repérer les endroits où le niveau de difficulté du matériel est trop bas ou trop élevé</li>
                <li>Confirmer que les apprenants comprennent les consignes des exercices et des activités</li>
                <li>évaluer le déroulement et l'équilibre du cours</li>
                <li>
                  Vérifier dans quelle mesure les apprenants réussissent à atteindre les objectifs d'apprentissage
                  énoncés du cours avant la fin de celui-ci
                </li>
                <li>Valider les outils d'évaluation du cours</li>
                <li>Recueillir des commentaires sur le cours de la part du groupe de discussion (communiquer avec <a href="mailto:csps.evaluation.efpc@canada.ca">l'équipe d'évaluation</a>)</li>
                <li>Déterminer les sections du cours qui ont besoin d'être modifiées</li>
              </ul>
            </li>
            <li>Révision du contenu du cours</li>
            <li>Organisation d'une mise à l'essai</li>
            <li>Commentaires recueillis des apprenants à la suite de la mise à l'essai</li>
            <li>Modifications nécessaires apportées au matériel de cours</li>
          </ul>
        </section>
