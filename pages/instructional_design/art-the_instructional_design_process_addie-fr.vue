    <h2>Le processus de conception de mat&eacute;riel p&eacute;dagogique : ADDIE</h2>
    <p>
      Au moment de concevoir du mat&eacute;riel p&eacute;dagogique, vous prenez une activit&eacute; complexe et vous la divisez en
      &eacute;tapes r&eacute;alisables. Le mod&egrave;le &laquo;&nbsp;ADDIE&nbsp;&raquo; (un acronyme qui signifie les cinq principales
      &eacute;tapes de la conception&nbsp;: Analyse, Conception, D&eacute;veloppement, Mise en &oelig;uvre et &eacute;valuation) fournit un
      cadre pour la conception p&eacute;dagogique. Pour en savoir plus, consultez le r&eacute;sum&eacute; graphique dans le <a href="//blogs.articulate.com/les-essentiels-du-elearning/une-introduction-au-modele-addie-pour-les-concepteurs-pedagogiques/" target="_blank">ce blogue</a>
    </p>
    <p>
      N&rsquo;oubliez pas que m&ecirc;me si les &eacute;tapes sont pr&eacute;sent&eacute;es de mani&egrave;re lin&eacute;aire, il peut y
      avoir de la souplesse dans la fa&ccedil;on dont on effectue la conception. Soulignons notamment que m&ecirc;me si
      l&rsquo;&eacute;valuation se trouve &agrave; la fin du processus, elle doit se faire tout au long de celui-ci. La s&eacute;quence ADDIE
      permet n&eacute;anmoins d&rsquo;organiser les t&acirc;ches &agrave; r&eacute;aliser au moment de cr&eacute;er un produit
      d&rsquo;apprentissage.
    </p>
    <p>
      Naviguez les onglets identifi&eacute;s par des lettres ci-dessous pour avoir de l&rsquo;information et des outils de travail que vous
      pouvez utiliser pendant votre processus de conception &laquo;&nbsp;ADDIE&nbsp;&raquo;.
    </p>
