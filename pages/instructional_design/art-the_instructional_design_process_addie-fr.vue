    <h2>Le processus de conception de matériel pédagogique : ADDIE</h2>
    <p>
      Au moment de concevoir du matériel pédagogique, vous prenez une activité complexe et vous la divisez en
      étapes réalisables. Le modèle &laquo;&nbsp;ADDIE&nbsp;&raquo; (un acronyme qui signifie les cinq principales
      étapes de la conception&nbsp;: Analyse, Conception, Développement, Mise en œuvre et évaluation) fournit un
      cadre pour la conception pédagogique. Pour en savoir plus, consultez le résumé graphique dans le <a href="//blogs.articulate.com/les-essentiels-du-elearning/une-introduction-au-modele-addie-pour-les-concepteurs-pedagogiques/" target="_blank">ce blogue</a>
    </p>
    <p>
      N'oubliez pas que même si les étapes sont présentées de manière linéaire, il peut y
      avoir de la souplesse dans la façon dont on effectue la conception. Soulignons notamment que même si
      l'évaluation se trouve à la fin du processus, elle doit se faire tout au long de celui-ci. La séquence ADDIE
      permet néanmoins d'organiser les tâches à réaliser au moment de créer un produit
      d'apprentissage.
    </p>
    <p>
      Naviguez les onglets identifiés par des lettres ci-dessous pour avoir de l'information et des outils de travail que vous
      pouvez utiliser pendant votre processus de conception &laquo;&nbsp;ADDIE&nbsp;&raquo;.
    </p>
    <b-tabs content-class="mt-3">
      <b-tab title="A">
        <h3>étape d'analyse</h3>
        <p>
          Pendant l'étape d'analyse, vous faites le point sur ce qui existe et sur ce que l'on souhaite comme
          produit d'apprentissage. Vous faites l'analyse des besoins et des objectifs. Vous déterminez les lacunes en
          matière de connaissances, de compétences et de rendement. Vous décomposez les tâches en
          éléments constitutifs. Vous créez des objectifs d'apprentissage <a href="http://www.tbs-sct.gc.ca/psm-fpfm/learning-apprentissage/ptm-grt/pmc-dgr/smart-fra.asp" target="_blank"><abbr title="S-Spécifique M-Measurable A-Atteignable R-Réaliste T-Temporellement défini ">SMART</abbr></a>. Vous
          énumérez les ressources et les documents. L'information qui découle de l'analyse sert à
          réaliser les produits livrables de cette étape :
        </p>
        <ul>
          <li>
            La <a href="learning_needs_analysis_checklist-fr.html" target="_blank">liste de vérification pour l'analyse des
              besoins d'apprentissage</a>
          </li>
          <li>La <a href="learning_design_plan-fr.html" target="_blank">Plan de conception de l'apprentissage</a> </li>
          <li>Le <a href="sample_project_plans-fr.html" target="_blank">Plan de projet</a> </li>
          <li>
            Si vous prévoyez inclure une nouvelle fonctionnalité, un <strong>document sur les exigences
              opérationnelles</strong> est requis. Communiquez avec <a href="mailto:csps.iitsolutionsintake-receptionsolutionsiit.efpc@canada.ca">Réception Solutions IIT (EFPC)</a>.
          </li>
        </ul>
        <p>
          Vous pouvez recueillir de l'information pour l'analyse à l'aide des <em>Outils de travail</em> avant de
          l'ajouter à votre Plan de conception de l'apprentissage. Vous pouvez également demander l'aide des
          Services d'apprentissage pour l'analyse et les produits livrables en envoyant un message à l'adresse <a href="mailto:CSPS.LearningServiceRequest-DemandeServiceApprentissage.EFPC@csps-efpc.gc.ca">Demande de Service d'Apprentissage</a>.
        </p>
        <p>
          L'étape d'analyse comporte trois rondes essentielles de questionnement :<br>
          (Cliquez sur les titres ci-dessous pour voir de l'information au sujet de chacune des sous-étapes de l'étape
          d'analyse).
        </p>
        <ol>
          <li>
            <details>
              <summary>
                <h4>Analyse des besoins d'apprentissage</h4>
              </summary>
              <p>
                Au cours de l'analyse, vous déterminerez si la formation est la bonne solution pour le problème. Vous
                examinerez en profondeur les besoins en matière d'apprentissage en répondant à des questions qui
                cherchent à expliquer POURQUOI, QUI, COMMeNT, QUOI et COMMeNT
              </p>
              <ul>
                <li>Pourquoi a-t-on besoin de ce produit?</li>
                <li>Qui souhaite le développement du produit ou de la formation?</li>
                <li>Qui est en cause dans le manque de rendement, de connaissances ou de compétences?</li>
                <li>Comment peut-on corriger les lacunes? (Comment peut-on combler l'écart?)</li>
                <li>Comment est-ce que la formation ou le produit d'apprentissage peut aider?</li>
                <li>De quelle autre façon peut-on répondre aux besoins? (Soutien au rendement, mentorat, outils de travail&hellip;)</li>
                <li>Comment mesurer les améliorations sur les plans du rendement, des connaissances ou des compétences?</li>
                <li>Quelles fonctions exercent les employés dans le cadre de l'emploi?</li>
                <li>Quels sont les meilleurs moyens pour eux d'exercer leurs fonctions?</li>
                <li>Quelle information les gens qui occupent les postes peuvent-ils vous donner au sujet des fonctions qu'ils exercent?</li>
                <li>Quelles sont les normes de rendement?</li>
                <li>Qu'est-ce qui empêche de satisfaire aux normes de rendement? (Quel est l'écart?)</li>
                <li>
                  Quel rendement insatisfaisant dans le milieu de travail le client essaie-t-il de corriger?<br>
                  Quelles améliorations du rendement sont attendues à la suite de la formation?
                </li>
                <li>Quelles sont les options de prestation?</li>
                <li>Quand est-ce que la formation sera donnée?</li>
                <li>Quand est-ce que nous serons conscients des résultats?</li>
                <li>
                  Quel est le délai accordé pour terminer la formation en tant que produit, et quel est le délai
                  accordé à l'apprenant pour la suivre?
                </li>
              </ul>
              <p>
                Voici le lien vers la <a href="learning_needs_analysis_checklist-fr.html" target="_blank">liste de vérification</a> pour
                l'analyse des besoins d'apprentissage.<br>
                et le lien vers l'analyse de l'écart de rendement - outil de travail
              </p>
              <p>&Agrave; la suite de ces analyses, vous:</p>
              <ol>
                <li>saurez si la formation est véritablement nécessaire,</li>
                <li>commencerez à conceptualiser le mode de prestation qui permettra de répondre au besoin.</li>
              </ol>
            </details>
          </li>
          <li>
            <details>
              <summary>
                <h4>Analyse du public</h4>
              </summary>
              <p>Dès que vous en savez plus sur la solution que vous souhaitez créer, vous devrez en savoir plus sur les apprenants</p>
              <ul>
                <li>Qui sont les apprenants?</li>
                <li>Quel est leur niveau actuel de compétences ou de connaissances?</li>
              </ul>
              <p>Vous pouvez consulter cet outil de travail pour faire l'analyse des apprenants : <a href="learner_identification_analysis-fr.html" target="_blank">Analyse pour identifier les besoins des apprenants</a></p>
            </details>
          </li>
          <li>
            <details>
              <summary>
                <h4>Analyse de la tâche</h4>
              </summary>
              Lorsque vous savez en quoi consiste le cours ou le produit et savez qui sont les apprenants, il
              est temps de décomposer le sujet en segments ou en tâches. Décomposez par la suite ces segments ou tâches en
              sous-tâches et déterminez les étapes de chaque sous-tâche.
              <ul>
                <li>Que vise le programme? (Définissez les buts et objectifs d'apprentissage du programme)</li>
                <li>Assurez-vous que les objectifs correspondent aux critères <a href="http://www.tbs-sct.gc.ca/pmc-dgr/smart-fra.asp" target="_blank" title="Cliquez ici pour en savoir plus sur « SMART »">&ldquo;SMART&rdquo;</a> </li>
                <li>est-ce que les clients et intervenants sont d'accord avec les objectifs du programme?</li>
              </ul>
            </details>
          </li>
        </ol>
