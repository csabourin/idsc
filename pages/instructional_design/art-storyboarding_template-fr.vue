    <h2>Modèle de scénarimage</h2>
    <p>
      Un scénarimage est un document de présentation visuel
      qui indique l'ensemble des éléments nécessaires
      pour un component multimédia d'un cours, notamment les
      éléments graphiques, audio, vidéo, le texte à
      l'écran et toute caractéristique interactive. Le
      principal objectif est de présenter ce que vous prévoyez
      comme conception, déroulement et structure de
      l'élément et de s'assurer que tous sont sur la
      même longueur d'onde avant d'amorcer la programmation. Le
      scénarimage aide également les programmeurs à
      comprendre la structure et la logique de votre élément ou
      cours.
