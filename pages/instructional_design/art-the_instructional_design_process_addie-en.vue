          <h3>Development Phase</h3>
          <p>The development phase focuses on creating prototypes and developing course materials. Pilots should be run and evaluated during this phase. The key deliverables of the Development phase can include:</p>
          <ul>
            <li>Course materials</li>
            <li>Instructor&rsquo;s guides</li>
            <li>Job aids</li>
            <li>Assessment instruments</li>
            <li>Course schedules</li>
            <li>
              For multi-media &ndash; there are other deliverables IT and learning technology specialists will prepare them with you, such as:<br>
              alpha versions for online products or media and a Web Content Accessibility Guidelines (WCAG) compliance report.
            </li>
          </ul>
          <p>
            The <a href="mailto:csps.evaluation.efpc@canada.ca">evaluation team</a> will help you develop your evaluation strategy;<br>
            The <a href="mailto:csps.communications.efpc@canada.ca">communications team</a> will help you with communication plans
          </p>
          <p>Here is a process overview:</p>
          <ul>
            <li>Developers assemble all the content assets</li>
            <li>
              Create a prototype (with learning tech team)
              <ul>
                <li>Course Templates</li>
              </ul>
            </li>
            <li>
              Review and consult about the prototype
              <ul>
                <li>Is the content accurate?</li>
                <li>Is the content complete?</li>
              </ul>
            </li>
            <li>Develop the course materials</li>
            <li>Testers run a QA and IQA process</li>
            <li>
              Conduct a tabletop review &ndash; focus group session
              <ul>
                <li>Measure the amount of time learners need for each module and activity</li>
                <li>Check learners' engagement with the material(s)</li>
                <li>Detect points where material(s) may be too easy/too difficult</li>
                <li>Confirm that learners understand the instructions for activities and exercises</li>
                <li>evaluate the flow and balance of the course</li>
                <li>Test how well learners achieve the course's stated learning objectives by the end of the course</li>
                <li>Validate the course assessment tools</li>
                <li>Collect feedback from focus group about the course (contact <a href="mailto:csps.evaluation.efpc@canada.ca">evaluation team</a>) </li>
                <li>Locate points where the course should be revised</li>
              </ul>
            </li>
            <li>Revise course content</li>
            <li>Run a pilot session</li>
            <li>Get learners feedback from pilot</li>
            <li>Make the necessary changes to materials</li>
          </ul>
