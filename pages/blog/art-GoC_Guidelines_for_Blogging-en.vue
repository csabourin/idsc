    <h2>Guidelines to Acceptable Use of Internal Wikis and Blogs Within the Government of Canada</h2>

    <p>
      The following are important excerpts from TBS's <a
        href="http://www.tbs-sct.gc.ca/pol/doc-eng.aspx?id=17555"
        target="_blank"
      >Guidelines to Acceptable Use of Internal Wikis and Blogs Within the Government of Canada</a> which
      CSPS bloggers and Programs should be aware of.
    </p>

    <details>
      <summary><h4>Context</h4></summary>

      <p>
        &quot;Wikis and blogs are IT-enabled collaboration software tools that are part of what is called &quot;Web
        2.0.&quot; Web 2.0 refers to the next generation of World Wide Web technology characterized by software that
        enables users to easily create, modify, link, share, and post content through the use of a computer's Web
        browser in real time. This collaborative environment comes in the form of blogs, wikis (such as Wikipedia), and
        many new types of online social networking websites, such as Facebook, MySpace, and LinkedIn.
      </p>

      <p>
        Today, many people use these tools extensively to rapidly build communities of interest-sharing information,
        experiences, ideas, and designs. The multiple-author capability of wikis makes them effective tools for mass
        collaborative authoring. These tools are typically relatively simple to learn and use.&quot;
      </p>
    </details>

    <details>
      <summary><h4>Copyright</h4></summary>

      <p>
        Departments are advised that copyright considerations need to be respected not only with respect to the
        content produced on blogs and wikis but also to the software that enables it. As detailed in the Communications
        Policy of the Government of Canada, Policy Requirements, under Copyright and Licensing, &quot;Institutions must
        comply with the Copyright Act and ensure that the ownership rights associated with works subject to copyright
        are fully respected in all media applications.&quot;
      </p>
    </details>

    <details>
      <summary><h4>Access to Privacy and Information</h4></summary>

      <p>
        All information posted to blogs and wikis is subject to the provisions of the <a
          href="//laws-lois.justice.gc.ca/eng/acts/A-1/"
          target="_blank"
        >Access to Information Act</a> and the <a
          href="//laws-lois.justice.gc.ca/eng/acts/P-21/"
          target="_blank"
        >Privacy Act</a>. It is recommended that
        departments ensure that their wikis and blogs contain appropriate notices that advise contributors in that
        regard. The department's ATIP office can be consulted for assistance. The business leader (or owner) of the
        wiki or blog would normally work with the departmental ATIP coordinator to respond to any access to information
        requests.
      </p>
    </details>

    <details>
      <summary> <h4>Official Languages</h4></summary>

      <p>
        In regions designated bilingual for language-of-work purposes, blogs and wikis are considered bilingual work
        instruments. Departments would need to provide blog and wiki interfaces in both official languages and permit
        any user to choose their preferred official language to be compliant with official languages requirements.
        Users are encouraged to add information in the official language of their choice.
      </p>
    </details>

    <details>
      <summary> <h4>Bilingual blogs</h4></summary>

      <ul>
        <li>The department posts the initial blog in both official languages.</li>

        <li>
          The department posts a notice on the blog indicating the terms of reference regarding the use of
          official languages.
        </li>

        <li>Users can respond to any post, or any reply to any post, in the official language of their choice.</li>

        <li>
          The department ensures that its response to a post is in the official language in which the post was
          received.
        </li>

        <li>
          The department advises users that the responses or replies displayed are in the language in which they
          were received.
        </li>

        <li>
          If a department decides to provide summaries of the blog, it ensures that the summaries are posted in
          both official languages simultaneously and are of equal quality.
        </li>
      </ul>
    </details>

    <details>
      <summary> <h4>Personal opinions</h4></summary>

      <p>
        Personal opinions that relate to the position and functions of the individual as an employee or of the
        individual in the performance of services under contract with the federal government and that would have
        normally been expressed in the course of their duties may be expressed on wikis and blogs.
      </p>

      <p>
        The <a href="http://www.tbs-sct.gc.ca/pol/doc-eng.aspx?id=12419&amp;section=text" target="_blank">Policy on
          the Use of electronic Networks</a> states that the posting of personal Web pages on Government of Canada
        resources is not permitted. It adds, however, that authorized users may use specifically designated Government
        of Canada resources to post personal information, provided such information is relevant to the business of the
        department and is in compliance with all other aspects of the policy.
      </p>
    </details>

    <details>
      <summary><h4>Accessibility</h4></summary>

      <p>
        The content posted to a wiki or a blog should not introduce accessibility issues. Although posting plain
        unformatted text will rarely introduce accessibility issues, posting specialized content, such as data tables,
        images, links, or multimedia, significantly increases the risk of introducing accessibility issues. For
        additional information see the <a
          href="http://www.tbs-sct.gc.ca/clf2-nsi2/clfs-nnsi/clfs-nnsi-2-eng.asp"
          target="_blank"
        >Common Look and Feel Standards for the Internet, Part 2: Standard on the Accessibility,
          Interoperability and Usability of Web Sites.</a>
      </p>
    </details>

    <details>
      <summary><h4>Communications</h4></summary>

      <p>
        Departmental heads of communications are responsible for internal communications and have an oversight role
        on Web content. In this regard it is recommended that departments inform their head of communications of
        projects involving wikis and blogs. The departmental communications strategist(s) or advisor(s) can provide
        advice and guidance when there is any uncertainty on the nature or appropriateness of intended contributions or
        discussions on wikis and blogs. For further information, see the <a
          href="http://www.tbs-sct.gc.ca/pol/doc-eng.aspx?id=12316"
          target="_blank"
        >Communications Policy of the Government of
          Canada.</a>
      </p>
