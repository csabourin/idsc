    </p>
    <h4>Les blogues d'instruction</h4>
    <p>
      Le terme &laquo;blogue d'instruction&raquo; parle de lui même. Ce type de blogue indique au lecteur comment il
      doit s'y prendre pour exécuter une tâche donnée, en présentant une série d'étapes à suivre. Les billets d'un «
      blogue d'instruction » intéressent les lecteurs parce que chacun d'entre nous souhaite apprendre à faire de
      nouvelles choses. Le billet d'un blogue d'instruction pourrait notamment viser à enseigner au lecteur
    </p>
    <h4>Les blogues sous forme de liste</h4>
    <p>
      Les billets de blogue présentés sous forme de liste font partie des articles les plus appréciés et faciles à
      lire. Les lecteurs adorent les listes parce qu'ils peuvent y jeter un coup d'œil rapide et y trouver facilement
      l'information qu'ils cherchent. Les billets de blogue sous forme de liste présentent également l'avantage d'être
      courts. Pour qu'un billet rédigé sous cette forme soit apprécié, il doit être organisé logiquement; c'est donc dire
      que les éléments doivent y être présentés soit selon l'intérêt qu'ils suscitent (celui qui suscite le plus
      d'intérêt doit figurer en tête de liste), soit par ordre alphabétique (si vous établissez une liste de ressources
      ou un glossaire, par exemple), soit étape par étape. Un billet de blogue sous forme de liste pourrait par exemple
      porter sur les dix façons de faire participer les apprenants à des forums de discussion ou sur les cinq outils les
      plus efficaces pour le montage vidéo.
    </p>
    <h4>Les blogues à contenu épuré</h4>
    <p>
      Il n'est pas nécessaire qu'un billet de blogue ne contienne que du matériel original. Les blogueurs peuvent
      recueillir sur le Web des renseignements de façon sélective et faire uniquement état des points de vue et des
      ressources qu'ils jugent les plus pertinents et complets. C'est ce qu'on appelle la « conservation du contenu
      ».
    </p>
    <p>
      La conservation du contenu a le même objectif que la création de contenu : éduquer, mobiliser et attirer le plus
      de lecteurs possible. Les blogues à contenu épuré sont très utiles pour présenter aux lecteurs des points de vue
      nouveaux et différents. Les billets à contenu épuré sont habituellement rédigés sous forme de liste, si bien qu'il
      est très facile pour les lecteurs d'en prendre connaissance. Un billet de blogue à contenu épuré pourrait par
      exemple porter sur les meilleures façons d'utiliser les médias sociaux à des fins d'apprentissage. On y
      retrouverait probablement un résumé et des liens possibles vers d'autre contenu en ligne présentant des façons
