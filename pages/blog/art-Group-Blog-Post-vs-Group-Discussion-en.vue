    <h2>Differences Between a Group Blog Post and a Group Discussion</h2>

    <p>
      A <cite>Group Blog</cite> is meant to be a one-sided communications feature. Your group members may respond with
      long or short comments, and that is a good thing but it is not necessarily the objective of the blog. Blogs also
      have a "like" button and are available individually, as well as within groups. Blogs are often more of an "opinion
      piece" from the writer and can also include a call to action.
    </p>

    <p>
      A <em>Group Discussion</em>, on the other hand, is meant to be a two-sided communications feature. Here, you
      want to receive answers! The creator posts a text with information and questions which are then answered in depth
      by the members. It then becomes a continuous back and forth discussion of the group members' ideas, opinions,
      questions and interpretations of the topic at hand. Like Blogs, Discussions have "like" buttons. However,
      <strong>discussions are only available within groups on GCconnex.</strong>
    </p>

