    <h2>Différences entre un blogue de groupe et une discussion de groupe</h2>

    <p>
      La fonction <em>Blogue de groupe</em> sert habituellement à communiquer un message dans un sens. Les membres de
      votre groupe peuvent répondre aux billets de blogue en formulant de courts ou de longs commentaires, et il s'agit
      d'une bonne chose, mais ce n'est pas nécessairement le but de l'exercice. Contrairement aux discussions, les
      blogues sont disponibles dans l'ensemble de GCconnex. Les billets de blogue sont souvent des textes où l'auteur
      émet une opinion et peuvent servir à lancer des appels à l'action.
    </p>

    <p>
      La fonction <em>Discussion de groupe</em>, au contraire, est conçue pour les communications bidirectionnelles.
      Ici, vous voulez recevoir des réponses! Le créateur publie un texte qui contient de l'information et des questions
      auxquelles les membres sont appelés à répondre de manière détaillée, ce qui donne lieu à un échange continu
      d'idées, d'opinions, de questions et d'interprétations à propos du sujet abordé. Comme les blogues, les discussions
      ont des boutons &laquo; J'aime &raquo;. Cependant, <strong>la fonction de discussion de GCconnex est disponible
        seulement dans les groupes.</strong>
    </p>

