    <h2>Pratiques exemplaires : Poser les bonnes questions</h2>
    <p>L'utilisation efficace d'un babillard de discussion ne se limite pas à demander aux étudiants de réagir à des lectures dans un manuel ou à donner suite à des exercices. Pour partir du bon pied, commencez par poser les bonnes questions.</p>

    <h3>Questions à prendre en considération lors de la planification d'activités pour un babillard de discussion</h3>
    <p>Voici les trois grandes questions que vous devez vous poser lors de l'élaboration d'activités pour un babillard de discussion :</p>
    <ul>
      <li>Comment les idées et le contenu à aborder s'insèrent ils dans l'objectif global du cours ou du programme?</li>
      <li>Quelles compétences, connaissances, perspectives ou impressions vos étudiants doivent ils retirer des discussions?</li>
      <li>Quelle mesure ou quel résultat la participation au forum de discussion devrait elle entraîner?</li>
    </ul>

    <p>Vous devez poser des questions efficaces pour que la discussion qui s'ensuit le soit tout autant. D'ordinaire, les questions d'analyse, de synthèse et d'évaluation se prêtent à des discussions plus approfondies d'un sujet donné. Cela dit, il est judicieux de poser quelques questions aux étudiants sur leurs connaissances comme exercice d'échauffement. Vous pouvez ensuite passer à des questions liées à la compréhension et à l'application des connaissances pour vous assurer qu'ils comprennent bien le sujet avant de vous engager dans une discussion approfondie.</p>

