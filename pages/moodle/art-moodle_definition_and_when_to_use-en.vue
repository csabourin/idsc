    <h3>How Can Moodle be Used for Teaching?</h3>

    <p>
      Moodle offers a range of functionalities designed around a core structure of a customizable home page with learning activities
      organized into "courses." Learners are given log-in access to the Moodle site&nbsp;and then enrolled in courses relevant to them. The
      home page can be customized to meet the needs of individual&nbsp;training programs.
    </p>

    <p>
      Each course can be designed to include resources and activities. Activities available are those included in the core Moodle
      and each course can use any combination of activities and resources. Typical activities include: assignments, quizzes, forums, live
      chat sessions, wikis, glossaries or imported SCORM content. Typical resources include files such as PDFs, videos, links, and HTML
      pages.
    </p>
    <p>
      Instructors&nbsp;can mark and review assignments, view and download answers and results, control timing of events, as well as block and
      monitor access. Instructors can also interact with learners via discussion forums and live chats. They can facilitate discussions to
      get learners to exchange ideas on a particular topic.
    </p>

    <h3>When to Use Moodle?</h3>

    <p>
      Moodle is better suited to courses that span long periods of time as it allows users to learn at their own pace. You have the option
      to run a course based on cohorts (i.e. fixed dates) or to leave it open so people can freely enroll in and start a course at any given
      time.
