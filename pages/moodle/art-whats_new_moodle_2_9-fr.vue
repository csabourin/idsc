    <h2>Les nouveautés dans Moodle 2.9</h2>
    <p>
      Dans la version de GCcampus de janvier 2016, la plateforme Moodle sera mise à niveau,
      c'est-à-dire qu'elle passera de la version 2.7 à la version 2.9. Voici quelques-unes des principales
      améliorations que comportent les versions 2.8 et 2.9.
    </p>
    <section>
      <h3>Version 2.8</h3>
      <dl class="dl-horizontal">
        <dt>Enregistrement automatique du texte </dt>
        <dd>L'information saisie dans l'éditeur de texte (WYSIWYG) est enregistrée automatiquement.</dd>
        <dt>Amélioration du carnet de notes </dt>
        <dd>
          Il est maintenant plus facile de noter l'étudiant gr&acirc;ce aux nouvelles améliorations apportées au
          carnet de notes.
        </dd>
        <dt>Fonction &laquo; J'aime &raquo; dans les forums </dt>
        <dd>
          Les utilisateurs peuvent maintenant &laquo; aimer &raquo; les messages dans les forums, comme il est déjà
          possible de le faire dans les médias sociaux.
        </dd>
        <dt>Navigation dans les forums</dt>
        <dd>Des liens permettent dorénavant de consulter les fils de discussion précédents et suivants.</dd>
        <dt>Amélioration de la fonction de correction des questionnaires </dt>
        <dd>
          Gr&acirc;ce à l'amélioration de la page de correction des questionnaires, il est maintenant plus facile
          d'ajouter des questions ou de gérer les questions dans un questionnaire. Des paramètres relatifs aux
          activités conditionnelles ont aussi été ajoutés.
        </dd>
        <dt>Choix multiples</dt>
        <dd>
          L'utilisateur peut être autorisé à choisir plus d'une réponse gr&acirc;ce à
          l'activité Choix.
        </dd>
        <dt>Joindre un fichier à une t&acirc;che </dt>
        <dd>Le professeur peut joindre des fichiers à une t&acirc;che à confier à un étudiant.</dd>
      </dl>
      <p>Pour une liste complète des nouveautés dans la version 2.8, visitez le site de <a href="//docs.moodle.org/2x/fr/Mise_%C3%A0_jour_%C3%A0_Moodle_2.8" target="_blank">Moodle</a>.</p>
    </section>
    <section>
      <h3>Version 2.9</h3>
      <dl class="dl-horizontal">
        <dt>Temps consacré à une leçon </dt>
        <dd>Vous pouvez maintenant préciser de combien de temps l'utilisateur dispose pour terminer une leçon.</dd>
        <dt>Note de passage</dt>
        <dd>
          Vous pouvez maintenant préciser la note de passage dans les paramètres de l'activité plut&ocirc;t
          qu'à partir du bulletin de notes.
        </dd>
        <dt>Bloc des résultats aux activités</dt>
        <dd>Un nouveau bloc permet d'afficher le résultat de l'utilisateur pour chaque activité du cours.</dd>
        <dt>Fonction glisser-déplacer dans l'éditeur de texte</dt>
        <dd>
          Vous pouvez maintenant utiliser la fonction glisser-déplacer pour insérer une image dans l'éditeur de
          texte; il n'est plus nécessaire de commencer par télécharger l'image.
        </dd>
        <dt>Recherche de fichiers sur le serveur</dt>
        <dd>Les gestionnaires peuvent maintenant effectuer une recherche parmi les fichiers qui se trouvent sur le serveur.</dd>
        <dt>Titres de rubriques dans les questionnaires</dt>
        <dd>Il vous est possible de créer des titres de rubriques dans le bloc de navigation des questionnaires.</dd>
      </dl>
      <p>Pour une liste complète des nouveautés dans la version 2.9, visitez le site de <a href="//docs.moodle.org/2x/fr/Mise_à_jour_à_Moodle_2.9" target="_blank">Moodle</a>.</p>
