    <h2>Qu'est-ce que Moodle?</h2>

    <p>
      <img alt="Logo de Moodle, la lettre M portant un chapeau de graduation" class="pull-right col-md-5" height="auto" src="~/assets/moodle/moodle_logo.jpg">Moodle est un système de gestion de l'apprentissage (SGA) sous licence libre, ou encore un environnement d'apprentissage virtuel. Il
      permet aux éducateurs de créer des cours en ligne en mettant l'accent sur l'interaction et la création de contenu en collaboration. Moodle
      propose un vaste éventail de fonctions pour créer des cours interactifs et diffuser des ressources d'apprentissage.
    </p>
    <p>
      Moodle repose sur une approche de l'éducation axée sur le constructivisme et le constructivisme social. Selon cette approche, les
      apprenants (et pas uniquement les enseignants) peuvent contribuer à l'expérience d'apprentissage. Le concept de Moodle permet aux étudiants
      de commenter le contenu (ou d'y contribuer), de participer à des groupes de discussion, à des blogues et à des activités de groupe ou de
      travailler en équipe dans le cadre d'un wiki.
    </p>

    <p>
      L'École de la fonction publique du Canada utilise actuellement la version 2.7 de Moodle. Les utilisateurs peuvent ouvrir une session en
      utilisant leurs données d'identification de MonDossier du SHGA.
    </p>

    <h3>Comment utiliser Moodle pour enseigner?</h3>

    <p>
      Moodle propose diverses fonctions reposant sur une structure de base à partir de laquelle on peut adapter la page d'accueil selon
      différentes activités d'apprentissage, organisées en « cours ». Les apprenants obtiennent l'accès au site Moodle et s'inscrivent ensuite
      aux cours qui les intéressent. Il est possible de personnaliser la page d'accueil afin de répondre aux besoins de chaque programme de
      formation.<br>
    </p>

    <p>
      Chaque cours peut être adapté pour qu'on y intègre des ressources et des activités. Les activités offertes sont celles de la structure
      de base de Moodle. En outre, chaque cours peut faire appel à diverses activités et ressources. Voici des activités types : devoirs, tests,
      forums, séances de clavardage, wikis, glossaires ou un contenu importé SCORM. Les ressources types comprennent des fichiers, comme des PDF,
      des vidéos, des hyperliens et des pages HTML.<br>
      Les enseignants peuvent corriger et noter les travaux, voir et télécharger les réponses et les résultats, contrôler le moment où ont lieu
      les événements et bloquer l'accès ou le contrôler. Les enseignants peuvent également interagir avec les apprenants dans le cadre de groupes
      de discussion ou de séances de clavardage. Ils peuvent animer des discussions pour inciter les apprenants à échanger des idées sur un sujet
      donné.
    </p>

    <h3>Quand utiliser Moodle?</h3>

    <p>
      Moodle est mieux adapté aux cours qui se déroulent sur une longue période, puisqu'il permet aux utilisateurs d'apprendre à leur rythme.
      Il est possible d'offrir un cours en fonction de cohortes (à dates fixes) ou de laisser aux utilisateurs le choix de s'inscrire librement
      et de commencer le cours au moment qui leur convient le mieux.
    </p>

