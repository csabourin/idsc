    <h2>Apprentissage à distance : Concevoir des activités pour de petits groupes</h2>

    <p>Il est possible d'adapter un grand nombre d'activités et de projets conçus pour les petits groupes en classe à un environnement en ligne. La plupart des étudiants aiment travailler en groupe et apprécient l'aspect social du travail de groupe.</p>
    <p>Lorsque vous concevez des activités à réaliser dans un babillard de discussion pour vos étudiants, réfléchissez à des manières de stimuler les compétences en résolution de problèmes. Tous les types d'activités se prêtent à un environnement en ligne : qu'il s'agisse d'activités élémentaires visant à évaluer les connaissances des étudiants sur un sujet donné, ou encore, d'activités poussées faisant appel à la pensée critique des apprenants.</p>

    <p>Le tableau ci dessous présente un éventail d'idées d'activités à réaliser au moyen de babillards de discussion. </p>

