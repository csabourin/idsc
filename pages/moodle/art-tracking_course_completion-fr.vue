    <h2>Suivi de l'achèvement des cours dans Moodle</h2>
    <p>Pour assurer le suivi de l'achèvement de cours dans Moodle, vous devez d'abord activer la fonction correspondante dans les paramètres de cours. Ainsi, il vous sera possible de vérifier l'achèvement des cours au moyen de différentes conditions.</p>
    <h3>Activer la fonction de suivi</h3>
    <p>Rendez-vous d'abord dans les <strong>paramètres</strong> du cours.</p>
    <p><img id="Picture 2" src="~/assets/moodle/01.png" alt=""></p>
    <p>Faites défiler le menu jusqu'à la section <strong>Suivi d'achèvement</strong>, puis cliquez sur OUI.</p>
