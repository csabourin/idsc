    <h3>Foire aux questions</h3>
    <details>
      <summary>
        <h4>Le suivi de mon cours Moodle devrait-il se faire au moyen du SHGA?</h4>
      </summary>
      <p>Nous recommandons que le suivi de tous les cours s'effectue au moyen du SHGA, car une telle façon de faire comporte plusieurs avantages :</p>
      <ul>
        <li>Le cours figure dans l'historique de formation de l'apprenant, d'où il est possible d'y accéder de nouveau.</li>
        <li>Un rapport peut être produit sur le nombre d'apprenants qui se sont inscrits au cours et qui l'ont terminé.</li>
        <li>L'apprenant peut s'inscrire par lui-même à la séance de cours.</li>
        <li>Le cours peut faire partie d'une certification dans le SHGA.</li>
      </ul>
    </details>
    <details>
      <summary>
        <h4>Où puis-je obtenir une liste de tous les apprenants qui ont suivi le même cours?</h4>
      </summary>
      <p>Vous pouvez utiliser le rapport d'achèvement de cours afin de savoir qui a terminé le cours. Vous verrez notamment si les apprenants ont rempli chacune des conditions à respecter pour qu'il soit indiqué que le cours est terminé.</p>
    </details>
    <details>
      <summary>
        <h4>Comment le SHGA et Moodle permettent-ils, ensemble, d'assurer le suivi de l'achèvement?</h4>
      </summary>
      <p>Lorsque l'apprenant a terminé le cours, Moodle envoie un message au SHGA. Il est ensuite indiqué dans l'historique de formation de l'apprenant que le cours est terminé. Pour utiliser cette fonction,  il faudra associer votre cours Moodle à une séance de cours du SHGA. Avant le lancement du cours, envoyez l'adresse URL du cours Moodle et le numéro de séance du SHGA à l'adresse Service des opérations SGA et demandez qu'ils soient associés.</p>
      <p>Il est à noter qu'il peut y avoir un certain délai avant que l'historique de formation n'indique que le cours est terminé.</p>
    </details>
    <details>
      <summary>
        <h4>Une fois le cours terminé, l'apprenant y a-t-il toujours accès?</h4>
      </summary>
      <p>Ce sont vos besoins qui déterminent si l'utilisateur demeure inscrit au cours Moodle et peut donc encore consulter le contenu, ou s'il n'a plus accès à ce contenu à la fin du cours (par exemple, dans le cadre d'un programme multiphase). Pour vous assurer que tout est en règle et répond à vos besoins, discutez des options avec l'analyste des activités et l'équipe des opérations du SHGA.</p>
    </details>
