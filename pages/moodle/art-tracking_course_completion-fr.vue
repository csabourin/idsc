    <h3>Établir les conditions relatives à l'achèvement du cours</h3>
    <p>La page <strong>Achèvement de cours</strong> vous permet d'établir les <strong>conditions à respecter</strong> pour qu'il soit indiqué que le cours est terminé. Vous trouverez ci-dessous un court paragraphe expliquant la façon dont vous pouvez vous servir de chacune des conditions pour déterminer si le cours est terminé. Vous pouvez combiner plusieurs conditions, ou n'en appliquer qu'une seule. </p>
    <h3>Achèvement d'activité</h3>
    <p>Pour utiliser la fonction à partir de l'achèvement d'une ou de plusieurs autres activités, vous devez activer la fonction de suivi de l'achèvement dans les paramètres de l'activité. Une fois que c'est fait, l'activité s'affiche dans la liste. </p>
