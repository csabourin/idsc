    <h2>Caractéristiques de Moodle</h2>

    <p>
      Lorsque vous créez votre cours, vous avez accès à un vaste éventail de ressources et d'activités. La liste ci-dessous décrit brièvement
      chacune de ces caractéristiques.
    </p>

    <h3>Ressources:</h3>

    <dl>
      <dt>Dossier</dt>

      <dd>Élément permettant d'organiser ensemble de multiples fichiers.</dd>

      <dt>Étiquette</dt>

      <dd>
        Élément servant de séparateur sur la page d'un cours Moodle. On peut afficher divers types de contenus (texte, image, vidéo) pour
        organiser la page de cours.
      </dd>

      <dt>Fichier</dt>

      <dd>Élément permettant de partager un fichier avec les apprenants (PDF, Word, vidéo, etc.)</dd>

      <dt>Livre</dt>

      <dd>
        Ressources multipages présentées en format linéaire. Un livre peut contenir plusieurs chapitres et sous chapitres. L'apprenant peut
        imprimer le livre dans sa totalité ou par chapitre.
      </dd>

      <dt>Page</dt>

      <dd>Page unique de contenu. On peut y afficher divers types de contenus, comme du texte, des images, des fichiers audio ou vidéo.</dd>

      <dt>Paquetage IMS Content</dt>

      <dd>Élément permettant d'ajouter du matériel statique provenant d'autres sources en paquetage IMS content.</dd>

      <dt>URL</dt>

      <dd>Lien vers une page Web ou un fichier.</dd>

      <dd>
        <h3>Activités:</h3>
      </dd>

      <dt>Atelier</dt>

      <dd>
        Activité d'évaluation par les pairs comportant de nombreuses options. Les apprenants soumettent leur travail grâce à un outil de
        production de texte en ligne et de pièces jointes. Deux notes sont associées à un apprenant : une note pour son propre travail et la
        note accordée au travail d'autres apprenants dans le cadre de l'évaluation par les pairs.
      </dd>

      <dt>Base de données</dt>

      <dd>
        Élément permettant à l'enseignant ou à l'apprenant de créer, d'afficher et de consulter une banque d'entrées sur un sujet donné. Le
        format et la structure de ces entrées sont presque illimités et peuvent comprendre des images, des fichiers, des hyperliens, etc.
      </dd>

      <dt>Chat</dt>

      <dd>Élément permettant aux participants de discuter entre eux en temps réel.</dd>

      <dt>Choix</dt>

      <dd>Élément permettant à l'enseignant de poser une question et de proposer un choix de réponses.</dd>

      <dt>Devoir</dt>

      <dd>Élément permettant aux enseignants de noter et de commenter les fichiers téléversés par les apprenants.</dd>

      <dt>Feedback</dt>

      <dd>
        Élément permettant à l'enseignant de créer et de mener des sondages pour recueillir des commentaires. Contrairement à l'outil
        Sondage, cette option vous permet de rédiger vos propres questions plutôt que de choisir parmi une liste de questions préétablies.
        Également, contrairement à l'outil Questionnaire, vous pouvez créer des questions qui ne sont pas notées.
      </dd>

      <dt>Forum</dt>

      <dd>Élément permettant aux apprenants et aux enseignants de participer à une discussion en différé. Idéal pour échanger des idées.</dd>

      <dt>Glossaire</dt>

      <dd>
        Élément permettant aux apprenants ou aux enseignants de créer et de tenir à jour une liste de définitions, comme un
        dictionnaire.
      </dd>

      <dt>Journal</dt>

      <dd>
        Élément permettant aux enseignants de recueillir en ligne les textes des apprenants, de les réviser et de formuler des
        commentaires, ainsi que de noter les travaux. Les travaux remis par les apprenants ne sont accessibles qu'aux enseignants.
      </dd>

      <dt>Leçon</dt>

      <dd>
        o Série de pages que l'on peut relier ensemble selon les choix que font les apprenants. On peut également s'en servir pour
        présenter l'information de façon linéaire.
      </dd>

      <dt>Outil externe</dt>

      <dd>
        Élément permettant aux participants d'interagir avec d'autres ressources et activités d'apprentissage conformes sur d'autres sites
        Web.
      </dd>

      <dt>Paquetage SCORM</dt>

      <dd>Élément permettant à l'enseignant de télécharger des dossiers d'apprentissage en ligne SCORM ou AICC dans le cours Moodle.</dd>

      <dt>Questionnaire</dt>

      <dd>
        Élément permettant à l'enseignant de créer des questionnaires comportant différents types de questions (choix multiples, vrai ou
        faux, réponse courte, etc.). Les questions sont conservées dans une banque de questions et peuvent être sélectionnées au hasard ou
        réutilisées dans d'autres questionnaires.
      </dd>

      <dt>Sondage</dt>

      <dd>Élément permettant de recueillir des données au moyen d'outils de sondage vérifiés.</dd>

      <dt>Wiki</dt>

      <dd>Recueil de documents créés collectivement sur Internet. Tout le monde peut contribuer à un wiki.</dd>
    </dl>

    <p>
      Pour en apprendre davantage sur chaque type d'activité, visitez le site Web <a
