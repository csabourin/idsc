    <h2>Assessing Learner Participation in Discussion Forums</h2>
    <p>
      The discussion forums in most cases are a means to an end ”better learning and thinking” than an end in themselves. They should have
      intrinsic value, and help students in meeting the overall course objectives or learning outcomes. Thus, it is better to focus the
      evaluation on the desired learning outcomes for the course, recognizing that individual students will choose different routes to get to
      those outcomes.<br>
      <br>
      Assessing discussion board activities can take many forms. Regardless of the type of assessment strategy or tool that you use, there
      are some key points to remember:
    </p>
    <ul>
      <li>Assessments must be matched to learning outcomes;</li>
      <li>Assessments must be aligned with instruction; and</li>
      <li>
        Students must be given clear guidelines regarding how their work and/or participation will be assessed.<br>
      </li>
    </ul>
    <p>
      Many distance-learning programs assess student engagement and the quality of that engagement. There are times when using a simple
      system such as awarding points for participation is appropriate. At other times, you may give whole-class feedback incorporating
      individual student posts. However, there are also times when a more detailed assessment may be desired.<br>
      <br>
      Teacher Assessment is probably the most commonly used assessment. However, there are many advantages to using peer assessment and self
      assessments as well. Essays or projects may be a better means to assess students in the long run than is measuring their contribution
      to discussion forums. Instructors can also use their discretion and take into account a student's contribution to the online discussion
      forums when marking essay-type assignments.<br>
      <br>
      Peer Assessment actively involves students in the assessment process and encourages critical thinking skills. The use of peer review on
      the discussion board serves a variety of purposes. It allows students to learn from one another as they view each other's work. It can
      also encourage quality by making the students' work public and allowing them to practice adhering to assessment guidelines when
      providing feedback to others. Peer review can be used to assess writing assignments as well as student-produced products.
    </p>
    <p>
      Self Assessment encourages students to think about their own learning and to reflect upon areas of strength and weakness. Students
      can be asked to assess the frequency and quality of their posts and to assess or measure their role within the learning community. How
      do they rate the frequency/quality of their posts? Did they support the learning of other learners? Did they support the overall
      learning growth of the community as a whole?<br>
