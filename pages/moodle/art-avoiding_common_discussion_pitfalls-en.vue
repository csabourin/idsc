    <h2>Avoiding  common discussion forum pitfalls </h2>
    <h3>Common Discussion  Board Pitfalls</h3>
    <p>
      While Discussion  Boards are a great tool for building community and providing a  means for students to  share their ideas with the group, there are some common  problems and pitfalls:<br>
    </p>
    <ul>
      <li>Students may misunderstand directions or may be unsure of what is expected of them.</li>
      <li>Student comments  can become off track or  go in a direction that is not supported in the lesson.</li>
      <li>Students may stall or put off participating in  the discussion board until the last minute.</li>
      <li>Students may not feel  a sense of connection with their  classmates.</li>
      <li>Students  may react in an inappropriate way by flaming other students or making disinterested or disrespectful comments  to their peers or in response to assignments.</li>
    </ul>
    <h3>Tips for Avoiding Common Discussion Board Pitfalls</h3>
    <ul>
      <li>Structure  online discussion board activities carefully  - provide clear guidelines  for posting material, how often to comment, length of comment, what  information to include in the comments. Ask students to respond using specifics from their textbook  readings or other sources.</li>
      <li>Encourage  students to  contact you if they don't understand  the assignment or are having technical difficulties.</li>
      <li>Demonstrate ways  to support arguments,  cite textbook references/online resources to support a point of  view in your  own postings. Be actively present. </li>
      <li>Establish guidelines  for giving students credit for discussion board participation - give  points for posting regularly,  responding to classmates' posts, staying on topic and responding in a thoughtful manner.</li>
      <li>Use small group  activities  to help build community and establish peer  communication and connection.</li>
      <li>Encourage  students to  interact informally. Create  discussion threads or areas for personal introductions and  social interaction.</li>
      <li>Establish clear Netiquette guidelines and expectations  re: writing style at the very beginning. </li>
    </ul>
