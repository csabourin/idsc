    <h2>Distance Learning: Key Instructional Principles</h2>

    <p>
      Over 20 years of research have gone into the development of best practices in distance learning. Coming out of that research is the
      understanding that effective distance-learning programs demonstrate five key principles in program design and facilitation, as
      follows:
    </p>

    <ol>
      <li>
        <h4>All of us are potential teachers as well as learners; in a true collaborative environment we are <em>both</em>.</h4>

        <p>
          Many best-practice activities in distance learning are designed to allow students to share and develop content such as
          forums, wikis, glossaries, databases, and so on. This encourages students to add to the total course experience for others.
          Students can facilitate discussion forums, start new threads and design their own learning paths. Distance-learning tools and
          platforms give students more flexibility to do things that were previously thought of as something teachers or instructors
          should do.
        </p>
      </li>

      <li>
        <h4>We learn particularly well from the act of creating or expressing something for others to see.</h4>

        <p>
          Distance-learning tools and platforms provide a wide range of ways for people to create and share representations of their
          knowledge. The course structure itself is a terrific way to construct a shared and active representation of the common learning
          journey. Forums, of course, form the very core of this notion, providing places to discuss and share media, links and
          attachments. Wikis can be used as collaboratively built pages useful for group work and other negotiations. Glossaries are
          collaboratively built lists of definitions that can then appear throughout the course. Databases are an extension of this idea,
          allowing participants to enter structured media of any type (for example, a collection of digital photos or a library of
          references).
        </p>
      </li>

      <li>
        <h4>We learn a lot just by observing the activity of our peers</h4>

        <p>
          This refers to "classroom culture." We are good at learning what to do in a given situation by taking our clues from others'
          behaviour. By observing the activity of others online, we learn how best to learn in the online environment. The instructor
          should model posting and challenging behaviours. Research shows that what students miss from their online experience is
          learning how to be an online learner.
        </p>
      </li>

      <li>
        <h4>By understanding the contexts of others, we can teach in a more transformational way (constructivism).</h4>

        <p>
          There are many different ways to find out about people. Individual blogs, for example, allow people to express themselves
          publicly in a thoughtful way, often providing access to thinking that might not normally be expressed in, say, a forum. Others
          can comment on these blog posts. In an effective distance-learning program, separate sessions of the same course will unfold
          differently over time because the community of learners will be different in each session. each learner brings a different
          perspective and his or her contributions will be unique.
        </p>
      </li>

      <li>
        <h4>
          A learning environment needs to be flexible and adaptable, so that it can quickly respond to the needs of the participants
          within it.
        </h4>

        <p>
          As a learning facilitator, you want to take advantage of your growing knowledge of the students and how they are interacting
          to adjust activities and add to or modify discussion forum threads. &nbsp;By being actively "present" in the course, the
          effective instructor can gauge how to adapt the course or program to best meet the needs of the participants.
        </p>
      </li>
