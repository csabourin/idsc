    <h2>Best Practice: Asking Good Questions</h2>
    <p>
      The effective use of the discussion board goes far beyond simply asking students to respond to textbook readings or assignments.
      Asking good questions is a great place to start.
    </p>
    <h3>Questions to Consider When Planning Discussion Board Activities</h3>
    <p>There are three key questions to ask yourself as you design discussion board activities:</p>
    <ul>
      <li>How do the ideas and information to be discussed fit into the overall course or program?</li>
      <li>What skills, knowledge, perspectives or sensibilities do you want students to walk away from the discussion with?</li>
      <li>What follow-up call to action do you want as an outcome of participation in the discussion forum?</li>
    </ul>
    <p>
      Effective questions are the key to an effective discussion. Analysis, synthesis, and evaluation questions generally lend themselves
      to more in-depth discussion of the topic.&nbsp; However, starting with a few knowledge-type questions is an excellent way to get
      students warmed-up. Then progressing through comprehension and application questions will ensure that they understand the topic before
      you get to the in-depth discussion.
    </p>
