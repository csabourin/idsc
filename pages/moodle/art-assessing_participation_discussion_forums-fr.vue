    <h2>Évaluation de la participation des apprenants aux forums de discussion</h2>
    <p>Les forums de discussion constituent dans la plupart des cas un moyen d'atteindre les objectifs (en favorisant l'apprentissage et la réflexion) plutôt qu'une fin en soi. Les forums de discussion doivent avoir une valeur intrinsèque et aider les étudiants à atteindre les objectifs généraux du cours ou les résultats en matière d'apprentissage. Par conséquent, il est préférable de centrer l'évaluation sur les résultats visés du cours et de reconnaître que les étudiants choisiront des parcours différents pour y arriver.</p>
    <p>
      L'évaluation des activités relatives aux forums de discussion peut prendre diverses formes. Peu importe le type de stratégie ou d'outil d'évaluation adopté, voici certains éléments clés qu'il faut garder à l'esprit :
    </p>
    <ul>
      <li>Les évaluations doivent toujours être associées à des résultats en matière d'apprentissage.</li>
      <li>Les évaluations doivent porter sur la formation. </li>
      <li>Les étudiants doivent avoir reçu des directives claires en ce qui concerne la façon dont leur travail et leur participation seront évalués.</li>
    </ul>
    <p>Dans bon nombre de programmes d'apprentissage à distance, la participation des étudiants ainsi que la qualité de cette participation font l'objet d'une évaluation. Parfois, le simple recours à un système d'attribution de points pour la participation est suffisant. Dans d'autres situations, il est possible de donner de la rétroaction concernant la participation de l'ensemble du groupe en y intégrant des messages publiés par les étudiants. Toutefois, il est parfois nécessaire de réaliser une évaluation plus détaillée.</p>

    <p><strong>Les évaluations par le formateur</strong> représentent probablement le type d'évaluation le plus souvent utilisé. Il existe toutefois également de nombreux avantages à recourir à des évaluations par les pairs et à des auto évaluations. Les travaux et les projets constituent probablement une meilleure méthode pour évaluer les étudiants à long terme que la mesure de leur contribution aux forums de discussion. Les formateurs peuvent également utiliser leur pouvoir discrétionnaire et prendre en considération la contribution d'un étudiant aux forums de discussion en ligne lorsqu'il s'agit de noter les travaux.</p>

    <p><strong>Les évaluations par les pairs</strong> favorisent la participation active des étudiants au processus d'évaluation et leur permettent de mettre à profit leurs capacités critiques. Les évaluations par les pairs pour les forums de discussion peuvent servir à différentes fins. Elles permettent aux étudiants d'apprendre les uns des autres en lisant les travaux de leurs collègues. Elles peuvent également favoriser la qualité en diffusant les travaux effectués par les étudiants, ces derniers étant tenus de respecter les lignes directrices en matière d'évaluation lorsqu'il s'agit de formuler des commentaires à l'intention des autres étudiants. Les évaluations par les pairs peuvent être utilisées pour évaluer les travaux écrits ainsi que les produits préparés par les étudiants.</p>

    <p><strong>Les auto évaluations</strong> encouragent les étudiants à effectuer une réflexion sur leur propre apprentissage, leurs forces et leurs faiblesses. Il est possible de demander aux étudiants d'évaluer la fréquence ainsi que la qualité des messages qu'ils ont publiés sur les différents forums et d'évaluer ou de mesurer leur rôle au sein de la communauté d'apprentissage. De quelle façon évalueraientÂ­ils la fréquence ou la qualité de leurs messages? OntÂ­ils contribué à soutenir l'apprentissage des autres étudiants? OntÂ­ils contribué à soutenir l'apprentissage global de la communauté dans son ensemble? </p>

