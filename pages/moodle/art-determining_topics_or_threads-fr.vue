    <h2>Déterminez les sujets ou les fils de discussion</h2>
    <p>
      Le choix des sujets ou des fils de discussion doit être lié aux résultats attendus de la formation. Les sujets de discussion susciteront
      toujours une meilleure participation des étudiants s'ils sont liés aux évaluations ou aux travaux ou s'ils représentent un certain intérêt
      pour eux. Les sujets peuvent être déterminés par le formateur ou par les étudiants.
    </p>
    <p>
      Les sujets doivent être choisis et formulés de façon à encourager les discussions. Les sujets qui suggèrent qu'il existe une « bonne »
      réponse ou une « bonne » façon de faire les choses ou qui font en sorte que les étudiants répètent simplement le contenu vu en classe ne
      mènent nulle part, puisque lorsque la réponse appropriée a été donnée par un ou deux étudiants, il reste peu de choses à ajouter à la
      discussion.
    </p>
    <p>
      Il existe différentes façons d'utiliser des sujets ou des fils de discussion structurés au sein d'un forum de discussion et notamment
      les suivantes :
    </p>
    <ul>
      <li>
        <strong>Sujet lié aux travaux :</strong> Ce type de sujet est ou pourrait être directement lié à un travail à réaliser sans
        toutefois constituer le travail en soi. Le sujet peut permettre aux étudiants de discuter des différents aspects du travail ou de
        réaliser une partie du travail. Par exemple, si les étudiants doivent travailler à un projet, la discussion pourrait porter sur le
        cadre ou le plan d'exécution du projet. Si des concepts importants doivent être abordés dans un travail, le formateur pourrait remettre
        aux étudiants une étude de cas ou un exemple et leur demander d'appliquer ces concepts à l'étude ou à l'exemple. Par exemple, si le
        formateur a enseigné aux étudiants la façon d'établir un plan d'affaires, il pourrait leur demander de discuter des meilleures méthodes
        à utiliser pour réaliser une étude de marché relative à une proposition en particulier, alors que dans le cadre de leur travail, ils
        doivent établir un plan d'affaires complet englobant une étude de marché. Les forums de discussion peuvent être utilisés pour faciliter
        les discussions au sujet d'un travail de groupe.
      </li>
      <li>
        <strong>Sujet lié aux évaluations :</strong> Par exemple, le formateur pourrait remettre à un groupe d'étudiants un problème à
        résoudre. Le forum de discussion pourrait permettre de consigner les discussions engagées au sujet du problème ainsi que la démarche
        qui les a amenés à trouver une solution. Les étudiants sont ensuite évalués tant sur le plan des résultats que de la démarche
        entreprise pour résoudre le problème.
      </li>
      <li>
        <strong>Sujet lié aux intérêts :</strong> Ce type de sujet n'est pas directement lié au travail à réaliser par les étudiants, mais
        permet tout de même de susciter leur intérêt. Par exemple, les étudiants peuvent souhaiter discuter de récents événements liés au cours
        qui ne figurent toutefois pas parmi les principaux sujets abordés dans leur formation.
      </li>
    </ul>
