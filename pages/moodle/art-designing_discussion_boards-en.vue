    <h2>Designing Discussion Boards</h2>
    <p>
      Discussion Board activities can serve a variety of purposes and can be used to meet a wide range of instructional objectives.
      Instructional Discussion Boards should be used to meet specific course objectives and should be aligned with course content.
    </p>
    <p>
      It takes more than a skilled facilitator to develop and sustain meaningful dialogue on a discussion forum. The instructional design
      of the course is critical. Course discussions are most successful when embedded into the design, are tied to learning objectives or
      outcomes that allows for purposeful discussions.
    </p>
    <p>Advance planning reaps huge benefits. Decide how you will use and set up your discussion forums.</p>
    <ul>
      <li>Are you planning on having many discussions and will you use discussion forums extensively?</li>
      <li>Will you only be using it once or twice during a program?</li>
      <li>Will you be linking it to a blog? A video? Reading assignments?</li>
      <li>Will you organize threads (main discussion themes) with sub-threads?</li>
      <li>Will you lead all of the discussion forums or will students undertake leading some discussion threads?</li>
    </ul>
    <p>
      Once you have clear goals and objectives for using discussion forums, you can begin to plan your discussion forums.
    </p><h3>Be Clear About Your Goals</h3>
    <p>
      In most cases the educational goals for online learning are the same as in class, but in some ways discussion is even more important
      for online learners who otherwise are working in isolation, without regular contact with other students. Purposeful discussion is
      therefore critical in getting online students engaged as learners, and feeling they are part of a learning community.
    </p>
    <p>
      The emphasis should be is on purposeful discussion. There is a good deal of research evidence to show that students will not
      participate in online discussions if they feel the discussion forums are not directly helpful to their learning.
    </p>
    <p>There are a wide variety of possible goals for online learning discussions. Your forum may serve several goals.</p>
    <ul>
      <li>
        <strong>Improved comprehension</strong><strong>:</strong> to enable students to clarify their understanding of key concepts in
        the course through discussion with other students;
      </li>
      <li>
        <strong>Building an argument</strong><strong>:</strong> analyze the logic or power of someone else's argument and build and
        defend one's own position, using materials from the course or other relevant materials;
      </li>
      <li>
        <strong>Knowledge construction</strong><strong>:</strong> to encourage a deeper understanding of concepts, their applicability
        in different contexts and their relatedness to other concepts, or to acquire new meanings;
      </li>
      <li>
        <strong>Reflective learning:</strong> reflective activities require students to share a synthesis of the learning experience,
        or to describe how a situation or experience has personal value to them. These kinds of activities should allow for honest and open
        responses;
      </li>
      <li>
        <strong>Create a learning community:</strong> through collaborative learning enable students to learn from each other, to share
        knowledge and experience, and to learn to support and help each other;
      </li>
      <li>
        <strong>Critical thinking</strong><strong>:</strong> to encourage students to engage constructively in critical analysis of
        ideas, concepts, philosophies and processes/procedures, and to form, defend and reflect on their own position on issues raised in
        the teaching;
      </li>
      <li>
        <strong>Consensus building:</strong> consensus-building activities require students to work together to create a product or to
        come to an agreement on a topic.
      </li>
      <li>
        <strong>Expert support:</strong> Facilitate engagement with the instructor in the learning, and to provide an opportunity for
        additional teaching and support for the students as a group.
      </li>
      <li><strong>Enable student leadership:</strong> students can be assigned responsibilities to lead discussion threads</li>
      <li>
        <strong>Provide varied and alternative perspectives:</strong> forums provide an excellent opportunity to provide alternate
        perspectives on a topic or issue by inviting guest subject matter experts or other instructors to host discussion threads.
      </li>
    </ul>
