    <h2>Outils de scénarisation pour Moodle</h2>
    <p>Pour vous aider à créer du nouveau contenu dans Moodle, nous avons créé une série d'outils de scénarisation qui correspondent aux activités et ressources que l'on trouve actuellement dans Moodle.</p>
