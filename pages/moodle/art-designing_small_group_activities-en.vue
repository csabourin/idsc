    <h2>Distance Learning: Designing Small Group Activities</h2>

    <p>
      Many of the small group activities and projects that can be accomplished in a face-to-face classroom can be adapted for use in an
      online environment. Most students enjoy working in groups and like the social aspect that accompanies working together.
    </p>
    <p>
      When designing discussion board activities for your students, consider ways that you can encourage problem-solving skills. From
      lower level activities designed to assess students' knowledge of a particular topic, to higher-level activities that require students
      to think critically, they all have their place in the online classroom.<br>
      The table below offers some suggestions for using discussion board activities across the spectrum.
    </p>

