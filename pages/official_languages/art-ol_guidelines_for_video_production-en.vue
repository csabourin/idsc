    <h2><abbr title="Official Languages">OL</abbr> Guidelines for Video Production</h2>
    <p>The School offers an internal video production service. If you plan on integrating videos into your products, it is important to contact Studio&nbsp;2136 at the outset of the development process to determine the most effective options for meeting the School's official languages obligations.</p>
    <p>The studio's practices with respect to official languages are as follows:</p>

    <h3>Videos produced in the two official languages</h3>
    <ul>
      <li>If a video is made in both english and French, transcription and closed captioning are required in each language to ensure accessibility.</li>
      <li>Note that the english and French versions of the videos do not have to be exactly the same if they meet the same objective and are of equal quality in terms of content and language.</li>
      <ul>
        <li>For example, the content of a video report in english and a video report in French can be different if the objective is to report on an accident and the objective of the video is to launch a discussion.</li>
      </ul>
    </ul>

    <h3>Videos produced in only one official language</h3>
    <p>This practice is used only in the case of interviews or unilingual presentations.</p>
    <ul>
      <li>Transcription</li>
      <li>Translation</li>
      <li>Dubbing or subtitling in the other official language: In the case of videos with dubbing or subtitling, the School is seeking to have a comparable number of original videos in French and in english, while ensuring that they are of equal quality in both official languages.</li>
      <li>Closed captioning of both products for hearing-impaired persons.</li>
    </ul>

    <h3>Graphic animation with on&#8209;screen text</h3>
    <ul>
      <li>Translation</li>
      <li>Transcription and closed captioning of the two versions</li>
      <li>Subtitling or text substitution to make a version in each language</li>
    </ul>

    <h3>Graphic animation in the two official languages with no on&#8209;screen text</h3>
    <ul>
      <li>Transcription and closed captioning of both versions for hearing-impaired persons</li>
    </ul>

    <h3>Graphic animation in one official language with no on&#8209;screen text</h3>
    <ul>
      <li>Transcription</li>
      <li>Translation</li>
      <li>Synchronized audio dubbing: The version produced in the other official language should be of equal quality. This means complete postproduction for the other version to synchronize images and sound.</li>
      <li>Transcription and closed captioning of both versions for hearing-impaired persons</li>
    </ul>
