    <h2>Guidelines for eSchool Content</h2>
    <h3>events, Workshops, Webcasts</h3>

    <p>
      School events, workshops and webcasts are often bilingual. However, it is important to also offer unilingual
      events in a proportional manner.
    </p>
    <p>Recommended practice for bilingual events:</p>
    <ul>
      <li>
        Set up two screens: one presentation in english and one in French. Participants have the right to use the
        official language of their choice and facilitators should alternate equally between the two languages to ensure
        their equal representation. Organizers and presenters must make sure that when employees from the two
        linguistic groups participate, all participants are able to follow and intervene in the official language of
        their choice and this must be mentioned at the beginning of the event.
      </li>
      <li>
        When it is not possible to use two screens, alternate equally between english and French slides and make
        sure the presentation is distributed to participants in both official languages so they can follow at all
        times.
      </li>
      <li>You may also consider interpretation services.</li>
    </ul>
    <p>
      Consult with the Conferences And Special events (CASe) group about the use of official languages for events,
      workshops and webcasts.
    </p>

    <h3>Self-paced Online Courses</h3>

    <p>Self-paced online courses must be available in the two official languages.</p>
    <h4>external resources</h4>
    <p>
      Where a self-paced course includes links to external resources (documents, videos, Web sites), these resources
      must be offered in both official languages. The english and French content can be different as long as it meets the
      same objectives and is of equivalent quantity and quality.
    </p>

