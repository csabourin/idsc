    <h2>Obligations de langues officielles pour Gcconnex et GCpedia</h2>

    <h3>Blogues/Forums contenu libre &ndash; Gcconnex/GCpedia</h3>

    <p>Règles concernant les langues officielles <sup><a href="#_ftnref1">[1]</a></sup></p>

    <ul>
      <li>
        Les règles qui s'appliquent à tous les aspects de votre travail s'appliquent aussi
        aux Outils GC2.0. Tout document ou information que vous feriez normalement traduire si vous les diffusiez par
        courriel ou en remettiez des copies papier en personne devrait aussi être traduits pour affichage sur les
        Outils GC2.0. GCpédia et GCconnex ne sont pas des moyens de vous soustraire à vos obligations en
        matière de langues officielles&nbsp;: toutes les politiques valides dans le mode &laquo; hors ligne &raquo;
        s'appliquent &laquo; en ligne &raquo;.
      </li>

      <li>
        Les employés sont libres de contribuer dans la langue de leur choix dans les média sociaux, et ce
        dans toutes les sphères de leur travail, y compris sur les Outils GC2.0. De sorte que si vous avez une
        page/section en français et une page/section en anglais o&ugrave; vous invitez les employés à
        s'exprimer, l'une ne sera pas la copie parfaite de l'autre et cela est tout à fait
        légitime.
      </li>

      <li>
        Il faut aussi savoir que les fonctionnaires n'ont pas tous les mêmes obligations en matière
        de langues officielles. Par exemple, les fonctionnaires de régions unilingues peuvent verser du contenu sur
        GCpédia et GCconnex dans une seule langue. Ici aussi, la même règle s'applique &laquo; en
        ligne &raquo; et &laquo; hors ligne &raquo;.
      </li>

      <li>
